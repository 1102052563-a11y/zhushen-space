任务简报、录像带交接、追加名单电话、现场封锁线、企业律师到场的速度。银翼杀手表面酷，实际是流程动物。

**【可失败状态】**
救不了霍登；拦不住佐拉死亡的围观冷漠；来不及阻止弑神；屋顶坠落；蕾切尔被别的银翼杀手先退役。失败推进故事，不应用「主角光环」抹掉。

