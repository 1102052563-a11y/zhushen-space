    entry = re.search(r"## 阶位切入点\n(.*?)## 来源", text, re.S)
    pc = cc(plot.group(1)) if plot else 0
    ec = cc(entry.group(1)) if entry else 0
    print(f"{name}: plot={pc} entry={ec} total={cc(text)}")
    return p

