function charCount(s) {
  return (s || '').replace(/\s/g, '').length;
}

function sectionCounts(text) {
  const plot = (text.match(/## 剧情\n([\s\S]*?)(?=\n## )/) || [])[1] || '';
  const entry = (text.match(/## 休闲切入点\n([\s\S]*?)(?=\n## )/) || [])[1] || '';
  return { plot: charCount(plot), entry: charCount(entry) };
}

// 跨文件人名不重复
function checkNameUniqueness(worlds) {
  const seen = new Map();
  for (const w of worlds) {
    for (const [k, v] of Object.entries(w.cast)) {
      if (seen.has(v)) throw new Error(`人名重复: ${v} 在 ${seen.get(v)} 与 ${w.title}`);
      seen.set(v, w.title);
    }
  }
}

