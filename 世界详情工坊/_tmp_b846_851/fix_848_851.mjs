function processFile(d, f, opts = {}) {
  const fp = path.join(ROOT, `批次${d}`, f);
  let t = fs.readFileSync(fp, 'utf8');
  const name = f.replace(/\.md$/, '');

  if (name === '尸祸一六四四') {
    const sh = buildShihuo();
    const meta = '<!--meta lib=主库 tiers=一、二、三、四-->';
    const out = joinMain({ title: name, meta, plot: sh.plot, entry: sh.entry, src: sh.src });
    fs.writeFileSync(fp, out, 'utf8');
    return { d, f, action: 'rewrite', plot: nw(sh.plot), entry: nw(sh.entry) };
  }

  const parts = splitMain(t);
  if (!parts.plot) return { d, f, action: 'skip-parse' };

  let { plot, entry } = stripJunk(parts.plot, parts.entry);
  let src = parts.src;

  // 849 thin books
  if (FACTS_849[name]) {
    const r = deepExpandThin(name, plot, entry, src, FACTS_849[name]);
    plot = r.plot;
    entry = r.entry;
    src = r.src;
  } else {
    // 通用：补剧情字数
    if (nw(plot) < 11000) {
      plot = thickenFromSelf(name, plot, 11000 - nw(plot) + 50);
    }
    // 切入点若模板化则重建
    if (nw(entry) < 1500 || /本阶身份与时点锚|画面钩子点名主角|本阶独有字段/.test(entry)) {
      const tiers =
        (parts.meta.match(/tiers=([^>]+)/) || [, '一、二、三'])[1]
          .split('、')
          .map((x) => x.trim())
          .filter(Boolean);
      entry = ensureEntry(name, entry, tiers, {
        protag: (plot.match(/\*\*([^*]{2,8})\*\*/) || [, '主角'])[1],
        map: ((plot.match(/乐园阶位映射[^\n]+/) || [])[0] || '').replace(/乐园阶位映射（宁低勿高）：?/, '') || undefined,
      });
    }
  }

  // 最终再 strip 一次残留
  ({ plot, entry } = stripJunk(plot, entry));
  if (nw(plot) < 11000) plot = thickenFromSelf(name, plot, 11000 - nw(plot) + 80);
  if (nw(entry) < 1500) {
    const tiers =
      (parts.meta.match(/tiers=([^>]+)/) || [, '一、二、三'])[1]
        .split('、')
        .map((x) => x.trim())
        .filter(Boolean);
    entry = ensureEntry(name, entry, tiers, { protag: name.slice(0, 2) });
  }

  const out = joinMain({
    title: parts.title || name,
    meta: parts.meta || '<!--meta lib=主库 tiers=一、二、三-->',
    plot,
    entry,
    src: src || parts.src,
  });
  fs.writeFileSync(fp, out, 'utf8');
  return {
    d,
    f,
    action: 'fix',
    plot: nw(plot),
    entry: nw(entry),
    junk: hasHardJunk(out),
  };
}

