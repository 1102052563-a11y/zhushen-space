const ROOT = '产出';
const nw = (s) => [...(s || '').replace(/\s/g, '')].length;

function splitMain(t) {
  t = t.replace(/^\uFEFF/, '').replace(/\r\n/g, '\n');
  const title = (t.match(/^# (.+)$/m) || [, ''])[1];
  const meta = (t.match(/<!--meta[^>]*-->/) || [''])[0];
  const plotM = t.match(/## 剧情\s*\n([\s\S]*?)(?=\n## (?:阶位切入点|休闲切入点|来源))/);
  const entryM = t.match(/## 阶位切入点\s*\n([\s\S]*?)(?=\n## 来源)/);
  const srcM = t.match(/## 来源\s*\n([\s\S]*)$/);
  return {
    title,
    meta,
    plot: plotM ? plotM[1] : '',
    entry: entryM ? entryM[1] : '',
    src: srcM ? srcM[1] : '',
    raw: t,
  };
}

function joinMain(p) {
  return (
    `# ${p.title}\n${p.meta}\n\n## 剧情\n\n${p.plot.trim()}\n\n## 阶位切入点\n\n${p.entry.trim()}\n\n## 来源\n\n${p.src.trim()}\n`
  );
}

function stripJunk(plot, entry) {
  let p = plot;
  let e = entry;

  // 场记/卷宗/独有卷宗整块
  p = p.replace(/\n*\*\*【[^】]*(?:场记|卷宗|独有卷宗)[^】]*】\*\*\n?(?:[^\n]*\n)?/g, '\n');
  e = e.replace(/\n*\*\*【[^】]*(?:场记|卷宗|独有卷宗)[^】]*】\*\*\n?(?:[^\n]*\n)?/g, '\n');

  // 补阶细节块
  p = p.replace(/\n\*\*补阶细节[^\n]*\*\*\n[^\n]*(?:\n(?!\*\*|##)[^\n]*)*/g, '');
  e = e.replace(/\n\*\*补阶细节[^\n]*\*\*\n[^\n]*(?:\n(?!\*\*|##)[^\n]*)*/g, '');

  // 补N / 深补N 空壳
  e = e.replace(/\n\*\*(?:补|深补|补切入)\d+\*\*[^\n]*/g, '');

  // 因果补述 / 卷段推演 灌水
  p = p.replace(/\n\*\*[^*\n]+·因果补述\d+\*\*\n[^\n]*(?:\n(?!\*\*|##)[^\n]*)*/g, '');
  p = p.replace(/\n\*\*卷段推演\d+[^\n]*\*\*\n[^\n]*(?:\n(?!\*\*|##)[^\n]*)*/g, '');

  // 分阶段详述套话（含【地理·舞台】占位）
  const padStarts = [
    '**【世界剧情线 · 分阶段详述',
    '**【主要人物 · 扩写卡',
    '**【势力图谱 · 补】',
    '**【贵重物品 · 补】',
    '**【隐藏剧情 · 伏笔 · 补】',
    '**【大事记时间线 · 补】',
    '**【叙事基调 · 雷区 · 补】',
    '**【长写·',
  ];
  for (const s of padStarts) {
    const i = p.indexOf(s);
    if (i >= 800) p = p.slice(0, i);
  }

  // 阶段一~四 通用模板段（含「本阶再给」类）
  p = p.replace(
    /\n\*\*阶段[一二三四][^\n]*\*\*\s*\n(?:[^\n]*【地理 · 舞台】[^\n]*\n|[^\n]*核心人物包括[^\n]*\n|[^\n]*不可逆站队[^\n]*\n|[^\n]*体系代价[^\n]*\n|[^\n]*谈判\/护送[^\n]*\n)+/g,
    '\n',
  );

  // hex 标记尾巴
  p = p.replace(/(?:盐记|独有标记|标记)\s*[a-f0-9]{6,}/gi, '');
  e = e.replace(/(?:盐记|独有标记|标记)\s*[a-f0-9]{6,}/gi, '');
  p = p.replace(/盐\s*[a-f0-9]{6,}/gi, '');
  e = e.replace(/盐\s*[a-f0-9]{6,}/gi, '');

  // 人物行若为章节占位名则删
  p = p.replace(
    /\n- \*\*【(?:地理 · 舞台|主要人物|势力图谱|贵重物品|隐藏剧情 · 伏笔|大事记时间线|叙事基调 · 雷区)】\*\*[^\n]*/g,
    '',
  );
  p = p.replace(/\n- \*\*——[^*\n]+\*\*[^\n]*/g, '');

  // 切入点模板句
  e = e.replace(/画面钩子点名主角。?/g, '');
  e = e.replace(/本阶身份时点。?/g, '');
  e = e.replace(/本阶独有字段齐全。?/g, '');
  e = e.replace(/具体冲突。?/g, '');
  e = e.replace(/忌模板化他书；忌无真名；忌顶点归零。?/g, '');

  p = p.replace(/\n{3,}/g, '\n\n').trim();
  e = e.replace(/\n{3,}/g, '\n\n').trim();
  return { plot: p, entry: e };
}

function hasHardJunk(t) {
  return /独有卷宗|场记\d+|【[^】]*场记|【[^】]*卷宗|补阶细节|阶段一模板|本阶独有字段齐全|画面钩子点名主角|盐记\s*[a-f0-9]|独有标记\s*[a-f0-9]/.test(
    t,
  );
}

