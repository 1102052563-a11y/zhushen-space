function ensureEntry(name, entry, tiers, facts) {
  let e = entry;
  if (nw(e) >= 1500 && !/本阶身份与时点锚|画面钩子|本阶独有字段|具体冲突（人名地点利益）/.test(e)) {
    return e;
  }
  // rebuild minimal solid entry from facts
  const map = facts.map || '凡人≈一；精锐≈二；一方≈三；更高＝超阶阴影';
  const lines = [`> 阶位↔：${map}。超阶＝情报优先／条件性胜利；严禁顶点战力归零。\n`];
  const tierList = tiers.length ? tiers : ['一', '二', '三'];
  for (const t of tierList) {
    const tf = facts.tiers?.[t] || {};
    lines.push(
      `**${t}阶（${name}）**\n` +
        `切入身份/时点：${tf.id || `《${name}》${t}阶覆盖层公开时点，贴阶身份。`}\n` +
        `初始事件：${tf.event || `与${facts.protag || '主角'}相关的贴阶冲突，落到具体地点与利益。`}\n` +
        `开场白建议：「${tf.open || `${facts.hook || name}的空气里，你被点名。`}」\n` +
        `关键NPC立场：${tf.npcs || `**${facts.protag || '主角'}**；同伴；对手侧。`}\n` +
        `主线钩子/支线：${tf.hooks || '主线贴本阶；支线可观察人情/物资。'}\n` +
        `危险度/规避：${tf.danger || '贴阶；顶点情报优先，严禁硬刚本体。'}\n` +
        `任务方向/奖励：${tf.reward || '贴阶物资/编制/情报，禁止越阶神装。'}\n`,
    );
  }
  // pad entry if still short
  let body = lines.join('\n');
  let k = 0;
  while (nw(body) < 1520 && k < 12) {
    body += `\n**${name}·切入备忘${k + 1}**\n本阶再写清：在场者真名、地点气味、资源数字、一句未说完的话、下一钩子。奖励不越级，失败惩罚可观察。\n`;
    k++;
  }
  return body;
}

