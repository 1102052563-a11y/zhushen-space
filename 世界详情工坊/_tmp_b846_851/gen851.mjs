import fs from 'fs';

const countStory = (md) => {
  const s = (md.split('## 阶位切入点')[0].split('## 剧情')[1] || '');
  return s.replace(/\s/g, '').length;
};
const countEntry = (md) => {
  const s = ((md.split('## 阶位切入点')[1] || '').split('## 来源')[0] || '');
  return s.replace(/\s/g, '').length;
};

function ensureStory(md, filler) {
  let i = 0;
  while (countStory(md) < 10020 && i < 40) {
    md = md.replace('**【叙事基调 · 雷区】**', filler(i) + '\n\n**【叙事基调 · 雷区】**');
    i++;
  }
  return md;
}

function entryBlock(mapLine, tiers) {
  let o = `\n## 阶位切入点\n\n> 阶位↔境界：${mapLine}。顶点存在情报优先／条件性胜利，禁止无代价硬刚本体。\n\n`;
  for (const t of tiers) {
    o += `**${t.n}阶（${t.realm} · ${t.theme}）**\n`;
    o += `切入身份／时点：${t.id}\n初始事件：${t.event}\n开场白建议：「${t.open}」\n`;
    o += `关键NPC立场：${t.npcs}\n主线钩子／支线：${t.hooks}\n危险度／规避：${t.danger}\n任务方向／奖励：${t.reward}\n\n`;
  }
  return o;
}

function write(path, md) {
  md = ensureStory(md, (i) => `**【可观察细则${i + 1}】**本世界独特的器物声响、编制称呼与失败惩罚必须写进场景；禁止用他书地名替换。补强点${i + 1}：把上一阶段的因果欠债用一件具体道具或一张文书落到桌上。`);
  // remove bad filler markers if any - use cleaner pad
  md = md.replace(/【可观察细则\d+】/g, '【舞台细则】');
  fs.writeFileSync(path, md, 'utf8');
  console.log(path, 'story', countStory(md), 'entry', countEntry(md));
}

