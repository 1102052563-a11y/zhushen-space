function expand(file, plotExtra, entryExtra) {
  let t = fs.readFileSync(path.join(dir, file), 'utf8');
  // insert before ## 休闲切入点
  t = t.replace('## 休闲切入点', plotExtra + '\n\n## 休闲切入点');
  // insert before ## 来源 (after 休闲切入点 section end)
  t = t.replace(/\n## 来源\n/, '\n' + entryExtra + '\n\n## 来源\n');
  fs.writeFileSync(path.join(dir, file), t, 'utf8');
  const noSpace = s => s.replace(/\s/g, '').length;
  const plot = t.split('## 剧情')[1].split('## 休闲切入点')[0];
  const entry = t.split('## 休闲切入点')[1].split('## 来源')[0];
  console.log(file, 'plot', noSpace(plot), 'entry', noSpace(entry));
}

