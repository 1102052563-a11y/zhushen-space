function expand(base, min) {
  let t = base;
  const fillers = [
    '写正文时优先让角色「先有可失去的日常」，再进入压迫或危机——落差才有重量。',
    '对话要写语气：敬语松动、口癖、沉默的秒数，比写动作清单更重要。',
    '场景锚点固定：同一地点在不同情绪下重访，读者才会记住。',
    'NSFW 只作关系进度的高压节点，不写过程细目；重点写前后自我认知变化。',
    '结局分支写清 HE／BE／True 的关系状态，避免「赢了就算结束」。',
    '契约者介入时，忌抢走原作情感轴心的唯一性，宜并列支援／旁观／并行身份。',
    '时间标记用季节、校历、船班、夜勤轮值，而不是「第几阶段」。',
    '配角姓名必须原作真名，禁「女主A／红颜」代称。',
  ];
  let i = 0;
  while (t.replace(/\s/g, '').length < min) {
    t += '\n\n' + fillers[i % fillers.length] + '（' + (i + 1) + '）本条目继续补充：' +
      '关系线推进时，记住「谁先开口」「谁先移开视线」「谁把秘密压在舌下」这三拍。' +
      '日常道具（通讯器、钥匙、名牌、便当、旧照片）可反复出现作为情感信标。' +
      '若写修复线，甜来自「发生过仍选择彼此」，不是「假装没事」。';
    i++;
    if (i > 80) break;
  }
  return t;
}

