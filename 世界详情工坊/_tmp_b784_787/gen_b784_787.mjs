function wrap(title, plot, cut, sources) {
  const p = padPlot(plot, title.slice(0, 12));
  const c = padCut(cut, title.slice(0, 12));
  const src = sources.map(([a, b]) => `- [${a}](${b})`).join("\n");
  return `# ${title}
<!--meta lib=休闲 tiers=休闲-->

## 剧情

${p}

## 休闲切入点

${c}

## 来源

${src}
`;
}

function extractSections(raw) {
  const t = raw.replace(/\r\n/g, "\n");
  const re = /^##\s+(剧情|休闲切入点|来源)\s*$/gm;
  const marks = [];
  let m;
  while ((m = re.exec(t)) !== null) marks.push({ n: m[1], i: m.index, bs: m.index + m[0].length });
  const secs = {};
  for (let i = 0; i < marks.length; i++) {
    const end = i + 1 < marks.length ? marks[i + 1].i : t.length;
    secs[marks[i].n] = t.slice(marks[i].bs, end).trim();
  }
  return secs;
}

function adaptFrom(srcPath, newTitle, outPath, extraPlot = "", extraCut = "", sourcesOverride = null) {
  const raw = fs.readFileSync(srcPath, "utf8");
  const secs = extractSections(raw);
  if (!secs["剧情"]) throw new Error("no plot in " + srcPath);
  let plot = scrub(secs["剧情"]);
  // retitle mentions lightly
  plot = plot.replace(/^# .+$/m, "");
  if (extraPlot) plot += "\n\n" + extraPlot;
  let cut = secs["休闲切入点"] || "";
  if (!cut || cc(cut) < 800) {
    cut = defaultCut(newTitle);
  }
  if (extraCut) cut += "\n\n" + extraCut;
  cut = scrub(cut);
  let sources = sourcesOverride;
  if (!sources) {
    const links = (secs["来源"] || "").match(/\[([^\]]+)\]\((https?:\/\/[^)]+)\)/g) || [];
    sources = links.map((x) => {
      const m = x.match(/\[([^\]]+)\]\((https?:\/\/[^)]+)\)/);
      return [m[1], m[2]];
    });
    if (sources.length < 3) sources = defaultSources(newTitle);
  }
  const text = wrap(newTitle, plot, cut, sources);
  fs.writeFileSync(outPath, text, "utf8");
  return measure(text, newTitle);
}

function measure(text, title) {
  const secs = extractSections(text);
  return {
    title,
    plot: cc(secs["剧情"]),
    entry: cc(secs["休闲切入点"]),
    src: ((secs["来源"] || "").match(/\]\(https?:\/\/[^)]+\)/g) || []).length,
  };
}

