for (const f of fs.readdirSync(outDir).filter((x) => x.endsWith('.md'))) {
  let c = fs.readFileSync(path.join(outDir, f), 'utf8');
  const conf = extraByFile[f];
  if (!conf) continue;

  // strip previous 可核细节档案 loops if any to rebuild cleanly - keep base
  c = c.replace(/\n\n\*\*【[^】]*·可核细节档案\d+】\*\*[\s\S]*?(?=\n## 阶位切入点)/g, '\n');

  let st = counts(c);
  let i = 0;
  while (st.plot < 10000 && i < conf.plot.length) {
    const block = `\n\n${conf.plot[i]}\n`;
    if (c.includes('## 阶位切入点')) {
      c = c.replace('## 阶位切入点', block + '## 阶位切入点');
    }
    i++;
    st = counts(c);
  }
  let g = 0;
  while (st.plot < 10000 && g < 20) {
    g++;
    const name = f.replace('.md', '');
    const block =
      `\n\n**【${name}·舞台零件${g}】**\n` +
      conf.plot.map((p, idx) => p.replace(/\s/g, '').slice(0, 30)).join('；') +
      `。零件${g}要求写入一次具体交易：谁给什么、换什么、见证人是谁、毁约如何被追。禁止空话。连载边界：不宣布最终胜者。\n`;
    c = c.replace('## 阶位切入点', block + '## 阶位切入点');
    st = counts(c);
  }

  // entry pad
  if (!c.includes('各阶加注')) {
    c = c.replace('## 来源', '\n' + conf.entry + '\n\n## 来源');
  }
  st = counts(c);
  let e = 0;
  while (st.entry < 1500 && e < 10) {
    e++;
    c = c.replace(
      '## 来源',
      `\n**切入补充${e}**：本阶初始事件须60秒内可读；开场白含专有地标；危险度写清规避对象；奖励与本阶资源树一致。\n\n## 来源`
    );
    st = counts(c);
  }

  fs.writeFileSync(path.join(outDir, f), c, 'utf8');
  st = counts(c);
  console.log(f, st.plot, st.entry);
}
