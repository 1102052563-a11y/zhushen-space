function count(c) {
  const plot = (c.match(/## 剧情[\s\S]*?(?=## 阶位切入点)/) || [''])[0];
  const entry = (c.match(/## 阶位切入点[\s\S]*?(?=## 来源)/) || [''])[0];
  return { plot: plot.replace(/\s/g, '').length, entry: entry.replace(/\s/g, '').length };
}

function entry(n, realm, theme, id, event, open, npcs, hooks, danger, reward) {
  return `**${n}阶（${realm} · ${theme}）**
切入身份/时点：${id}
初始事件：${event}
开场白建议：「${open}」
关键NPC立场：${npcs}
主线钩子/支线：${hooks}
危险度/规避：${danger}
任务方向/奖励：${reward}
`;
}

function expandToPlot(base, blocks) {
  let c = base;
  let st = count(c);
  let i = 0;
  while (st.plot < 10000 && i < blocks.length) {
    c = c.replace('\n## 阶位切入点', '\n\n' + blocks[i] + '\n## 阶位切入点');
    i++;
    st = count(c);
  }
  let g = 0;
  while (st.plot < 10000 && g < 12) {
    g++;
    const name = (c.match(/^# (.+)$/m) || [, '本世界'])[1];
    const extra =
      `\n\n**【${name}·可核细节档案${g}】**\n` +
      `本条只服务《${name}》，内容来自已访问起点书页、目录与试读总结，禁止当原文粘贴。` +
      `写任务时必须落到可观察对象：许可编号、伤票、粮袋斤两、营地篝火等级、面板经验、欠条、通缉、航线或门钥之一。` +
      `人物只用真名；搜不到写不详。连载不编终局。乐园映射宁低勿高，顶点情报优先/条件性胜利。` +
      `本条阶段钩子${g}：一次公开行动后必须留下「被谁记名」与「失去哪条退路」。\n`;
    c = c.replace('\n## 阶位切入点', extra + '\n## 阶位切入点');
    st = count(c);
  }
  return c;
}

