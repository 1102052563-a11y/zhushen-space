function thicken(text, min, lines) {
  let t = text, i = 0;
  while (cc(t) < min && i < lines.length) { t += '\n\n' + lines[i]; i++; }
  let d = 0;
  while (cc(t) < min && d < 40) {
    const line = lines[d % lines.length];
    t += `\n\n（日程续·${d + 1}）` + line + ' 只记本舞台专属物件与真名。';
    d++;
  }
  return t;
}
