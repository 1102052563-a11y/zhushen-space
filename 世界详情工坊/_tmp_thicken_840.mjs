function process(name, extra, opts = {}) {
  const fp = path.join(out, name)
  let txt = fs.readFileSync(fp, 'utf8')
  let { head, cut } = splitFile(txt)
  // head includes title+meta+##剧情+plot
  const plotStart = head.search(/^## 剧情\s*$/m)
  const before = head.slice(0, plotStart + '## 剧情\n'.length)
  let plot = head.slice(plotStart + '## 剧情\n'.length)

  if (opts.stripPad) plot = stripPadHeaders(plot)
  // inject extra before end of plot (before trailing whitespace)
  plot = plot.trimEnd() + '\n' + extra + '\n'

  if (opts.stripCut) cut = stripCutPad(cut)
  // ensure cut length
  let cutLen = cut.replace(/\s/g, '').length
  if (cutLen < 1500) {
    cut = cut.trimEnd() + `\n\n**阶位写作备忘**  \n各阶关键NPC必须加粗真名；禁止各阶复制同一句填充；胜利定义随阶变化，不写「本阶可刷」套话。超阶存在情报优先／条件性胜利。\n`
  }

  const newTxt = before + plot + '\n' + cut
  fs.writeFileSync(fp, newTxt)
  // measure
  const t2 = fs.readFileSync(fp, 'utf8')
  const p2 = (t2.split(/^## 剧情\s*$/m)[1] || '').split(/^## /m)[0] || ''
  const c2 = (t2.split(/^## 阶位切入点\s*$/m)[1] || '').split(/^## 来源/m)[0] || ''
  console.log(name, 'plot', p2.replace(/\s/g, '').length, 'cut', c2.replace(/\s/g, '').length)
}

