const fs = require("fs");
const path = require("path");
const dir = path.join("产出", "批次49");

const noWs = s => s.replace(/\s/g, "").length;
const plotLen = t => noWs(t.split("## 阶位切入点")[0]);
const entryLen = t => {
  const a = t.split("## 阶位切入点")[1];
  return a ? noWs(a.split("## 来源")[0]) : 0;
};

// Dense paragraph factory for a world-specific topic list
function densify(name, topics) {
  return topics.map((t, i) => {
    const [title, body] = t;
    return `**【${title}】**\n${body}`;
  }).join("\n\n");
}

// ========== Shared tier skeleton customized per world ==========
function writeWorld(file, head, plotBody, tiersLine, tiersBlocks, sources) {
  const md = `# ${file}
<!--meta lib=主库 tiers=${tiersLine}-->

## 剧情

${plotBody}

## 阶位切入点

${tiersBlocks}

## 来源

${sources}
`;
  fs.writeFileSync(path.join(dir, file + ".md"), md, "utf8");
  console.log(file, "plot", plotLen(md), "entry", entryLen(md), "totalChars", md.length);
}

