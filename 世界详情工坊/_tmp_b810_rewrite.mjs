function padEntry(base, name, scenes) {
  let t = base;
  let i = 0;
  while (cc(t) < 1850 && i < scenes.length) {
    t += `\n（${name}·切入细目${i + 1}）${scenes[i]}\n`;
    i++;
  }
  // 若仍不足，循环补独特句
  let k = 0;
  while (cc(t) < 1850) {
    const s = scenes[k % scenes.length];
    t += `\n（${name}·延展${k + 1}）在「${s.slice(0, 24)}…」之后，契约者须留下可观察痕迹：称呼变化、到场频率、是否主动帮忙、雨天是否共伞、短信是否还用敬语。细节贴「${name}」专属舞台，禁止跨世界套话。\n`;
    k++;
    if (k > 40) break;
  }
  return t;
}

