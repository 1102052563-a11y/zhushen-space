function padCut(body, min = 1550) {
  let s = body;
  const noSpace = (t) => [...t].filter((c) => !/\s/.test(c)).length;
  const extra = [
    "\n\n切入身份补充：优先选不破坏原作因果的日常岗位；禁止杀手/佣兵开局。",
    "\n\n切入时点补充：锁定原作共通线前半或粉丝盘后日谈入口，避免直接终盘剧透。",
    "\n\n初始处境补充：手机/现金/钥匙有限；社交起点是具名角色而非路人群像。",
    "\n\n开场白补充：第二人称、60–120字画面感；有气味与声响。",
    "\n\n日常玩法补充：共同通勤、共食、节日、秘密共有、可退出约会。",
    "\n\n氛围补充：保持原作气质；忌任务杀敌化；NSFW按原作尺度过程从略。",
    "\n\n关系进度：0初识/1同行/2秘密/3半公开/4冲突后再选/5确认。每级需代价。",
    "\n\n长线交付物：一封信、一件物证、一次当众或私下的再签名。",
    "\n\n周常：工作日公开脸；休息日私密缝；节日半公开风险最高；月末物证盘点。",
    "\n\n开场白备用：「你把钥匙放进掌心，金属还是凉的。门外是这个世界惯有的气味。你深吸一口气，决定先做一个普通人，再谈喜欢。」",
  ];
  let i = 0;
  while (noSpace(s) < min && i < 40) {
    s += extra[i % extra.length];
    i++;
  }
  return s;
}

function scrubCombat(s) {
  return s
    .replace(/力量体系/g, "超常设定")
    .replace(/巅峰战力/g, "顶点压迫")
    .replace(/战力榜/g, "关系压力表")
    .replace(/战力/g, "冲突强度")
    .replace(/阶位映射/g, "阶段对照")
    .replace(/阶位/g, "阶段")
    .replace(/危险度/g, "关系风险");
}

function wrap(title, plot, cut, sources) {
  const p = pad(scrubCombat(plot));
  const c = padCut(scrubCombat(cut));
  // 再扫一遍 pad 注入段
  const p2 = scrubCombat(p);
  const c2 = scrubCombat(c);
  const src = sources.map((x) => `- [${x[0]}](${x[1]})`).join("\n");
  return `# ${title}
<!--meta lib=休闲 tiers=休闲-->

## 剧情

${p2}

## 休闲切入点

${c2}

## 来源

${src}
`;
}

