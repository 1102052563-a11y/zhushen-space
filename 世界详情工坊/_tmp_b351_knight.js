function pack(title, body) {
  const md = body;
  const plot = md.split("## 剧情")[1].split("## 休闲切入点")[0];
  const entry = md.split("## 休闲切入点")[1].split("## 来源")[0];
  const pc = plot.replace(/\s/g, "").length;
  const ec = entry.replace(/\s/g, "").length;
  if (pc < 6000) throw new Error(title + " plot " + pc);
  if (ec < 1500) throw new Error(title + " entry " + ec);
  if (md.includes("关系细目") || md.includes("日程细目")) throw new Error(title + " has padding");
  fs.writeFileSync(path.join(dir, title + ".md"), md, "utf8");
  console.log("OK", title, pc, ec);
}

