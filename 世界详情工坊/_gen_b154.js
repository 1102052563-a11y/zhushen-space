开场白建议：「拳关的灯灭了一半，戴面具的少年馆主几乎不说话，但他的宝可梦会从影子里长出来。你还没从幽灵的寒意里缓过神，列车又把你送向更高的雪线——那里的馆主是奇巴纳的母亲。」
关键NPC立场：**欧尼奥**——害羞但赛场致命；**美蓉**——慈母外壳下的冰馆主；**奇巴纳**——以龙馆终关姿态注视；**彼特**——妖精线变数；**波普菈**——古怪考官。
