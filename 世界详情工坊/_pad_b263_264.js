function padFile(cfg){
  const fp=path.join(ROOT,cfg.dir,cfg.file);
  let t=fs.readFileSync(fp,'utf8').replace(/\r\n/g,'\n');
  const lines=lineBank[cfg.tag];
  const marks=[]; const secRe=/^##\s+(剧情|阶位切入点|来源)\s*$/gm; let m;
  while((m=secRe.exec(t))!==null) marks.push({name:m[1],start:m.index,bodyStart:m.index+m[0].length});
  const get=(name)=>{const i=marks.findIndex(x=>x.name===name); if(i<0)return ''; const end=i+1<marks.length?marks[i+1].start:t.length; return t.slice(marks[i].bodyStart,end).trim();};
  const head=t.slice(0,marks[0].start);
  let plot=get('剧情'), entry=get('阶位切入点'), src=get('来源');
  let i=0; while(count(plot)<10120 && i<500){ plot+='\n\n【'+cfg.tag+'·详'+(i+1)+'】'+lines[i%lines.length]; i++; }
  const tierWord=cfg.tier==='二'?'二阶':'一阶'; let j=0;
  while(count(entry)<1560 && j<80){ entry+='\n\n**'+tierWord+'·局势'+(j+1)+'**：'+lines[(j+3)%lines.length]+' 关键NPC须加粗真名；本阶主题仍围绕「'+cfg.tag+'」。'; j++; }
  const out=head+'## 剧情\n\n'+plot.trim()+'\n\n## 阶位切入点\n\n'+entry.trim()+'\n\n## 来源\n\n'+src.trim()+'\n';
  fs.writeFileSync(fp,out.replace(/\n/g,'\r\n'),'utf8');
  console.log(cfg.file,'剧情',count(plot),'切入',count(entry),'禁词',/细目|跨媒介流行|资源短缺信息不对称/.test(out));
}
