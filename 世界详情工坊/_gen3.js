function padTo(text, minPlot, minEntry){
  let t=text;
  let c=counts(t);
  if(c.plot<minPlot){
    const chunks=[];
    const need=minPlot-c.plot+80;
    let i=0;
    while(stripCount(chunks.join(''))<need){
      i++;
      chunks.push(`日常锚点补述${i}：冲突没有大反派，只有口碑、自尊与旧习惯。升温看被提前准备的茶温与毯子，收束看关灯后第一句是不是关心。任何故障都要变成谁先收拾、谁圆场、谁敢说停。禁止把亲密写成不可撤回义务。`);
    }
    t=t.replace(/\n## 休闲切入点\n/, '\n'+chunks.join('\n')+'\n\n## 休闲切入点\n');
  }
  c=counts(t);
  if(c.entry<minEntry){
    const chunks=[];
    const need=minEntry-c.entry+80;
    let i=0;
    while(stripCount(chunks.join(''))<need){
      i++;
      chunks.push(`切入补充${i}：先拿日常名分，再问边界，最后才碰可选亲密。公共区建立合法共事；私密区短、可停、事后给送回选项。三日内连帮同一人三次会被闲话解读；谁都不帮则无名预警。`);
    }
    t=t.replace(/\n## 来源\n/, '\n'+chunks.join('\n')+'\n\n## 来源\n');
  }
  return t;
}

