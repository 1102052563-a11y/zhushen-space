function stripCount(s){return (s||'').replace(/\s/g,'').length}
function counts(t){
  const plotM=t.match(/##\s*剧情\s*\n([\s\S]*?)(?=\n##\s)/);
  const entryM=t.match(/##\s*休闲切入点\s*\n([\s\S]*?)(?=\n##\s|$)/);
  return {plot:stripCount(plotM&&plotM[1]), entry:stripCount(entryM&&entryM[1])};
}

