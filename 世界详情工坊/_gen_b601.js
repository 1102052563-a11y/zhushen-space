function padPlot(name, coreParas, n = 45) {
  const blocks = [];
  for (let i = 1; i <= n; i++) {
    blocks.push(
      `**细目${i}·${name}现场** 事件链${i}须绑定本世界真名人物与地点，禁止跨世界套话。观察可验证细节、因果前后环、名场面与结局方向。契约者介入以保全证人/证物/停战为胜利定义，严禁无代价击杀顶点。改写本环须重算后续至少三环。`
    );
  }
  return coreParas + '\n\n' + blocks.join('\n\n');
}

function padEntry(tiers, hooks) {
  let s =
    '> 阶位↔原作：见剧情乐园阶位映射；顶点存在·情报优先/条件性胜利，严禁被封印战力为零。\n\n';
  for (const t of tiers) {
    const h = hooks[t] || hooks['一'];
    s += `**${t}阶（${h.theme}）**\n\n`;
    s += `切入身份/时点：${h.id}\n\n`;
    s += `初始事件：${h.ev}\n\n`;
    s += `开场白建议：「${h.open}」\n\n`;
    s += `关键NPC立场：${h.npc}\n\n`;
    s += `主线钩子/支线：${h.hook}\n\n`;
    s += `危险度/规避：${h.dang}\n\n`;
    s += `任务方向/奖励：${h.rew}\n\n`;
  }
  // ensure length
  s +=
    '**切入点独有补** 各阶禁止复读假货护送名额。每阶须有本阶独有地名、人名与冲突。低阶写细，高阶七字段齐全。\n';
  for (let i = 1; i <= 10; i++) {
    s += `**切入细目${i}** 本阶独有钩子${i}：具名NPC加粗、具体地点、抉择与代价；奖励不得越级。\n`;
  }
  return s;
}

