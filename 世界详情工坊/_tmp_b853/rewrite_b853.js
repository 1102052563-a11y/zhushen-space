function counts(c) {
  const plot = (c.match(/## 剧情[\s\S]*?(?=## 阶位切入点)/) || [''])[0];
  const entry = (c.match(/## 阶位切入点[\s\S]*?(?=## 来源)/) || [''])[0];
  return { plot: plot.replace(/\s/g, '').length, entry: entry.replace(/\s/g, '').length };
}
function E(n, a, b, c, d, e, f, g, h, i) {
  return `**${n}阶（${a} · ${b}）**
切入身份/时点：${c}
初始事件：${d}
开场白建议：「${e}」
关键NPC立场：${f}
主线钩子/支线：${g}
危险度/规避：${h}
任务方向/奖励：${i}
`;
}
function pad(c, keys, name) {
  let st = counts(c);
  let n = 0;
  while (st.plot < 10050 && n < 60) {
    n++;
    const k1 = keys[n % keys.length];
    const k2 = keys[(n * 3) % keys.length];
    const k3 = keys[(n * 5) % keys.length];
    const p =
      `\n\n**【${name}·可核脉络${n}】**\n` +
      `围绕${k1}、${k2}与${k3}展开的公开情节中，任何「变强/过关」必须同步写出代价：身份暴露、物资数字、文书编号或人情债之一。` +
      `低阶禁止发高阶权柄；高阶优先情报与条件胜。连载结局不详。任务验收：谁目击、谁记账、谁追债。\n`;
    c = c.replace('## 阶位切入点', p + '## 阶位切入点');
    st = counts(c);
  }
  n = 0;
  while (st.entry < 1520 && n < 15) {
    n++;
    c = c.replace(
      '## 来源',
      `\n**阶位补强${n}**：七字段齐全；NPC加粗真名；开场白含地标；规避写本世界具体威胁。\n\n## 来源`
    );
    st = counts(c);
  }
  return c;
}

