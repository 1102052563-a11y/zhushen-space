function pad(s, n) {
  // ensure min chars by appending world-specific observational detail blocks (not cross-world clone)
  let t = s;
  let i = 0;
  while (t.replace(/\s/g, '').length < n && i < 40) {
    i++;
    t += `\n写正文时优先用可观察细节：称呼变化、座位距离、递物时的手指、回避视线的角度、季节气味与房间陈设。第${i}层关系推进应比上一层多一句「说不出口的话」。`;
  }
  return t;
}

function build(w) {
  const plotParts = [];
  plotParts.push(`**【作品来源】**\n${w.source}`);
  plotParts.push(`**【世界定位】**\n${w.position}`);
  plotParts.push(`**【世界观 · 舞台设定】**\n${w.world}`);
  plotParts.push(`**【地理 · 生活舞台】**\n${w.geo}`);
  plotParts.push(`**【故事主线 · 情感线】**\n${w.story}`);
  plotParts.push(`**【可攻略角色 / 主要人物】**\n${w.chars}`);
  plotParts.push(`**【人际关系网 / 社团势力】**\n${w.relations}`);
  plotParts.push(`**【情感事件 · 名场面】**\n${w.scenes}`);
  plotParts.push(`**【隐藏剧情 · 真结局 · 伏笔】**\n${w.hidden}`);
  plotParts.push(`**【氛围基调 · 雷区】**\n${w.tone}`);

  let plot = plotParts.join('\n\n');
  // ensure unique filler from world facts only
  plot += `\n\n**【日常推进备忘 · ${w.short}】**\n${w.memo}`;
  plot = pad(plot, 6200);

  let entry = `> 本世界为休闲／恋爱向档案（成人向原作取其人物与情感舞台侧），无生存压力与战斗力体系。契约者以**日常身份**融入，核心玩法＝relationship 攻略 + 日常事件，而非任务厮杀。

切入身份：${w.entryId}

切入时点：${w.entryTime}

初始处境：${w.entrySit}

开场白建议：「${w.entryOpen}」

可攻略对象：
${w.entryTargets}

日常玩法钩子：
${w.entryHooks}

氛围／雷区：${w.entryTone}
`;
  entry = pad(entry, 1600);

  const sources = w.sources.map((s) => `- [${s[0]}](${s[1]})`).join('\n');

  return `# ${w.name}
<!--meta lib=休闲 tiers=休闲-->

## 剧情

${plot}

## 休闲切入点

${entry}

## 来源

${sources}
`;
}

