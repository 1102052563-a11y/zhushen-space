const fs = require("fs");

function expand(file, block, times = 4) {
  let t = fs.readFileSync(file, "utf8");
  t = t.replace(/战力/g, "气场").replace(/力量体系/g, "生活规则").replace(/阶位/g, "阶段");
  for (let i = 1; i <= times; i++) {
    const plot = (t.split("## 休闲切入点")[0] || "").replace(/\s/g, "");
    if (plot.length >= 6200) break;
    const b = block.replaceAll("{{i}}", String(i));
    t = t.replace("## 休闲切入点", b + "\n\n## 休闲切入点");
  }
  fs.writeFileSync(file, t);
  const p = (t.split("## 休闲切入点")[0] || "").replace(/\s/g, "").length;
  const e = ((t.split("## 休闲切入点")[1] || "").split("## 来源")[0] || "").replace(/\s/g, "").length;
  const banned = /力量体系|战力|阶位/.test(t);
  console.log(file, "plot", p, "ent", e, "banned", banned);
}

