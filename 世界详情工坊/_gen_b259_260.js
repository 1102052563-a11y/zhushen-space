function entryExp(name, tiers) {
  let e = `\n\n**开局十分钟物证目标（${name}）**\n1. 带日期书面。2. 可验证影像/录音（≥30秒）。3. 一名可能清醒NPC真名+暗语。4. 一条撤离路线。未完成1+2前勿公开撕破表面。\n`;
  if (tiers.includes('一')) {
    e += `\n**一阶补充遭遇（${name}）**\n走廊目击、保健室排队、部活钥匙、家长电话、雨天留堂——每条点名本世界真名NPC，给正手（保护/取证）与反手（利用/交易）。危险度维持中~高，不引入二阶未覆盖存在。\n`;
  }
  if (tiers.includes('二')) {
    e += `\n**二阶补充遭遇（${name}）**\n仪式材料失窃、双笔迹手记、金库值班表、血誓假账、圣遗物传闻——每条要求契约者在「程序正确」与「现场硬闯」间二选一。顶点存在只作情报与撤离条件，不写战力归零。\n`;
  }
  // pad entry uniquely until 1550
  const lines = [
    `暗语示例：「今天的咖啡是冷的」=仍清醒；「咖啡很香」=已改写。`,
    `物证保管：原件与副本分两处，避免单点团灭。`,
    `对话优先问「谁还按自己的意志说话」，而非「你爱不爱他」。`,
    `若对方突然公文腔复读，停止输入新情报。`,
    `撤离优先证人，其次物证，最后才是对施术者的象征性反击。`,
  ];
  return { e, lines };
}

