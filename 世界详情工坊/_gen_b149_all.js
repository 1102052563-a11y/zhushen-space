const fs = require("fs");
const path = require("path");
const dir = "产出/批次149";

function stats(t) {
  const i = t.indexOf("## 剧情");
  const j = t.indexOf("## 阶位切入点");
  const k = t.indexOf("## 来源");
  return {
    plot: t.slice(i, j).replace(/\s/g, "").length,
    entry: t.slice(j, k).replace(/\s/g, "").length,
  };
}

function write(name, t) {
  const p = path.join(dir, name + ".md");
  fs.writeFileSync(p, t, "utf8");
  const s = stats(t);
  console.log(name, "plot", s.plot, "entry", s.entry, s.plot >= 10000 && s.entry >= 1500 ? "OK" : "SHORT");
}

