// ========== helpers for long world files ==========
function ensureMinPlot(body, fillerBlocks) {
  let t = body;
  let s = stats(t);
  let i = 0;
  while (s.plot < 10000 && i < fillerBlocks.length) {
    t = t.replace("\n## 阶位切入点", "\n" + fillerBlocks[i] + "\n## 阶位切入点");
    s = stats(t);
    i++;
  }
  return t;
}

