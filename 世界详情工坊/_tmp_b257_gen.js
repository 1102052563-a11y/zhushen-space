// helper to expand short plots with unique factual paragraphs
function expandTo(s, min, extraBlocks){
  let out = s;
  let i = 0;
  while (wc(out) < min && i < extraBlocks.length) {
    out += '\n\n' + extraBlocks[i++];
  }
  // if still short, weave more unique sentences
  let n = 0;
  while (wc(out) < min && n < 40) {
    out += `\n\n**【场景颗粒·${n+1}】**本档可写：具体气味（木地板汗、消毒水、转播机热风）、可被夺走的物证名、二十四小时内反噬的势力名、以及至少两名具名人物的当场立场句。失败默认态不是立刻死亡，而是名单被盯、合同生效、队友路线分裂——把失败写成下一场开局条件。`;
    n++;
  }
  return out;
}

