const fs = require('fs');

function stats(path) {
  const t = fs.readFileSync(path, 'utf8');
  const plot = t.split('## 剧情')[1].split('## 阶位切入点')[0];
  const entry = t.split('## 阶位切入点')[1].split('## 来源')[0];
  return {
    t,
    plotN: plot.replace(/\s/g, '').length,
    entryN: entry.replace(/\s/g, '').length,
  };
}

function injectBefore(path, marker, content, flag) {
  let t = fs.readFileSync(path, 'utf8');
  if (t.includes(flag)) return stats(path);
  t = t.replace(marker, content + '\n' + marker);
  fs.writeFileSync(path, t);
  return stats(path);
}

