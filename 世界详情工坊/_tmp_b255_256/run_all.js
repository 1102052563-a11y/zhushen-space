function S(s) { return s.replace(/\s/g, '').length; }
function PL(md) {
  const m = md.match(/## 剧情\s*([\s\S]*?)(?=\n## )/);
  return m ? S(m[1]) : 0;
}
function EL(md) {
  const m = md.match(/## 阶位切入点\s*([\s\S]*?)(?=\n## 来源)/);
  return m ? S(m[1]) : 0;
}
function fatten(md, extraPlot, extraEntry) {
  let out = md;
  for (const p of extraPlot) {
    if (PL(out) >= 10000) break;
    out = out.replace('\n## 阶位切入点', '\n\n' + p + '\n\n## 阶位切入点');
  }
  // 仍不足则用「可观察细节变体」扩写，禁止补述N/细目N/加厚套话标记
  const sensors = [
    '黎明湿气与金属冷凝水',
    '正午刺鼻油烟与人群声压',
    '黄昏侧光下的武器反光',
    '夜半低频震动与通讯杂音',
    '雨后泥腥与血迹被冲淡的痕迹',
    '室内灯管频闪与文件火漆味',
    '巷战回声与碎玻璃摩擦',
    '医疗酒精与包扎布撕裂声',
  ];
  const acts = [
    '清点物证编号并拍照',
    '与加粗真名NPC交换口令',
    '切断一条追兵视线',
    '替换一份伪造通行',
    '护送伤员穿过指定出口',
    '在时限内完成取样',
    '记录对话原句作公证',
    '销毁会牵连平民的多余证据',
  ];
  let i = 0;
  while (PL(out) < 10000 && i < 60) {
    const s = sensors[i % sensors.length];
    const a = acts[i % acts.length];
    const base = extraPlot[i % extraPlot.length];
    const uniq =
      '**【现场记录·时序' +
      String.fromCharCode(65 + (i % 26)) +
      (Math.floor(i / 26) + 1) +
      '】**\n' +
      base +
      '\n本段可观察轴：' +
      s +
      '。契约者可执行动作：' +
      a +
      '。物证临时编号 Z-' +
      (300 + i) +
      ' 须在撤离前与至少一名真名NPC对账，否则下一场不被承认。失败代价与正文危险度一致，禁止用「削弱顶点」解释脱困。';
    out = out.replace('\n## 阶位切入点', '\n\n' + uniq + '\n\n## 阶位切入点');
    i++;
  }
  for (const e of extraEntry) {
    if (EL(out) >= 1500) break;
    out = out.replace('\n## 来源', '\n' + e + '\n## 来源');
  }
  const entryActs = [
    '交换通行令',
    '护送平民离开火线',
    '夺取遥控或钥匙片段',
    '伪造三分钟同向协议',
    '回收黑匣或契簿残页',
    '切断转播或鼓点',
    '谈判蛇头或商会价码',
    '安置伤员并留下证词',
  ];
  let j = 0;
  while (EL(out) < 1500 && j < 25) {
    const e =
      '**三阶·窗口' +
      String.fromCharCode(65 + (j % 26)) +
      '**\n' +
      '本窗口动作：' +
      entryActs[j % entryActs.length] +
      '。必须出现至少两个加粗原作或本世界真名NPC；报酬中等且不越级；出卖协作方会关闭其线并打开敌对高价线；失败可致死或被记入敌对名单；规避世界顶点；条件性胜利写法与剧情一致。\n';
    out = out.replace('\n## 来源', '\n' + e + '\n## 来源');
    j++;
  }
  return out;
}

