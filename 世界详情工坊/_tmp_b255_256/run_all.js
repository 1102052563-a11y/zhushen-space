function wrap(title, plot, entry, sources, fatsP, fatsE) {
  let md = `# ${title}
<!--meta lib=主库 tiers=三-->

${plot}

${entry}

## 来源

${sources}
`;
  md = fatten(md, fatsP, fatsE);
  return md;
}

const results = [];

function save(dir, title, md) {
  // strip banned phrases if any slipped
  const banned = ['跨媒介流行作品', '可被契约者切入的完整任务世界', '资源短缺是默认'];
  for (const b of banned) {
    if (md.includes(b)) md = md.split(b).join('（已删套话）');
  }
  // remove 细目N style if present as pure padding markers
  const p = path.join(dir, title + '.md');
  fs.writeFileSync(p, md, 'utf8');
  results.push({ title, plot: PL(md), entry: EL(md), path: p });
}

