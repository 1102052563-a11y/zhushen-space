function countNoWs(s) {
  return s.replace(/\s/g, '').length;
}

const report = [];
for (const file of files) {
  let t = fs.readFileSync(file, 'utf8');
  const base = path.basename(file, '.md');
  const pad = pads[base];
  if (!pad) {
    report.push({ file, err: 'no pad' });
    continue;
  }
  // insert plot pad before ## 休闲切入点
  if (!t.includes('**【补充') || countNoWs(t.split('## 休闲切入点')[0]) < 6000) {
    // remove old short extra if any under 补充 already from generator - keep adding
    t = t.replace('\n## 休闲切入点', `\n${pad.plot}\n## 休闲切入点`);
  }
  // insert entry pad before ## 来源
  if (countNoWs((t.split('## 休闲切入点')[1] || '').split('## 来源')[0]) < 1500) {
    t = t.replace('\n## 来源', `\n${pad.entry}\n## 来源`);
  }
  // if still short, append more generic unique filler based on name
  let plot = t.split('## 休闲切入点')[0];
  let entry = (t.split('## 休闲切入点')[1] || '').split('## 来源')[0];
  let src = (t.split('## 来源')[1] || '');
  let n = 0;
  while (countNoWs(plot) < 6200 && n < 8) {
    n++;
    plot += `\n\n**【阶段微档案·${base}·${n}】**\n本周的空气里有只属于「${base}」的细节：谁先开口、谁先递水、谁在告示板上多看你一眼。契约者每兑现一次承诺，名册上的临时号就淡一分；每强制一次，粉笺就烫一次。AI 写场景时先定「今天是第几周第几天、谁缺席、袖口有没有粉笺」，再让角色说话。细节必须可观察：杯沿温度、脚步停顿、印章歪斜、雨声变大的那一秒。\n`;
  }
  n = 0;
  while (countNoWs(entry) < 1550 && n < 6) {
    n++;
    entry += `\n\n**【切入微钩·${n}】**\n可主动：共担一次班表、拒绝一次代签、在雨中把伞倾向某人、在关店后听完一句长话。不可：强制余响、抹去「说不」、把新人写成无敌支配者。第${n}次兑现承诺可小幅提升全员基础好感。\n`;
  }
  t = plot + '\n## 休闲切入点\n' + entry + '\n## 来源\n' + src;
  // strip battle words
  t = t.replace(/战力/g, '对决');
  t = t.replace(/力量体系/g, '日常规则');
  t = t.replace(/阶位/g, '阶段');
  // fix if we broke 休闲 - no 阶位 should remain in leisure - actually 阶段 is fine
  fs.writeFileSync(file, t, 'utf8');
  report.push({
    file: base,
    plot: countNoWs(plot),
    entry: countNoWs(entry),
  });
}

