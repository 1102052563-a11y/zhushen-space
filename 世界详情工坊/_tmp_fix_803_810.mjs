import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = path.dirname(fileURLToPath(import.meta.url));
const nw = (s) => (s || '').replace(/\s/g, '').length;

function split(t) {
  t = t.replace(/\r\n/g, '\n').replace(/^\uFEFF/, '');
  const i1 = t.indexOf('\n## 剧情\n');
  const i2 = t.indexOf('\n## 休闲切入点\n');
  const i3 = t.indexOf('\n## 来源\n');
  if (i1 < 0 || i2 < 0 || i3 < 0) throw new Error('parse fail: ' + [i1, i2, i3].join(','));
  return {
    head: t.slice(0, i1),
    plot: t.slice(i1 + '\n## 剧情\n'.length, i2),
    entry: t.slice(i2 + '\n## 休闲切入点\n'.length, i3),
    src: t.slice(i3 + '\n## 来源\n'.length),
  };
}
function join(p) {
  return (
    p.head +
    '\n## 剧情\n' +
    p.plot.trimEnd() +
    '\n\n## 休闲切入点\n' +
    p.entry.trimEnd() +
    '\n\n## 来源\n' +
    p.src.replace(/^\n+/, '').replace(/\n+$/, '\n')
  );
}

function stripPad(plot, entry) {
  // 情感日志整块
  plot = plot.replace(
    /\n\n?\*\*【[^】]*·情感日志 \d+】\*\*[\s\S]*?(?=\n\*\*【|\n## |$)/g,
    '',
  );
  // 切入细目 / 延展
  entry = entry.replace(/\n\n?（[^）]*·切入细目\d+）[^\n]*/g, '');
  entry = entry.replace(/\n\n?（[^）]*·延展\d+）[^\n]*/g, '');
  // 残留「本日只推进关系」套话段
  plot = plot.replace(
    /\n\n?\*\*【[^】]*】\*\*\n本日只推进关系与日常：[\s\S]*?(?=\n\*\*【|\n## |$)/g,
    '',
  );
  plot = plot.replace(/\n{3,}/g, '\n\n');
  entry = entry.replace(/\n{3,}/g, '\n\n');
  return { plot, entry };
}

function ensure(fileRel, plotAdd, entryAdd, { strip = false } = {}) {
  const fp = path.join(ROOT, fileRel);
  let t = fs.readFileSync(fp, 'utf8');
  let p = split(t);
  if (strip) {
    const s = stripPad(p.plot, p.entry);
    p.plot = s.plot;
    p.entry = s.entry;
  }
  if (plotAdd && nw(p.plot) < 7600) p.plot = p.plot.trimEnd() + '\n\n' + plotAdd.trim();
  if (entryAdd && nw(p.entry) < 1850) p.entry = p.entry.trimEnd() + '\n\n' + entryAdd.trim();
  // one more soft top-up if still short (unique text only once more)
  if (plotAdd && nw(p.plot) < 7500) p.plot = p.plot.trimEnd() + '\n\n' + plotAdd.trim();
  if (entryAdd && nw(p.entry) < 1800) p.entry = p.entry.trimEnd() + '\n\n' + entryAdd.trim();
  fs.writeFileSync(fp, join(p), 'utf8');
  p = split(fs.readFileSync(fp, 'utf8'));
  const ok = nw(p.plot) >= 7500 && nw(p.entry) >= 1800;
  const pad =
    /情感日志|切入细目|·延展\d|【加厚|跨媒介流行作品|可被契约者切入的完整任务世界/.test(
      join(p),
    );
  console.log(
    fileRel,
    'plot',
    nw(p.plot),
    'entry',
    nw(p.entry),
    ok ? 'OK' : 'SHORT',
    pad ? 'PAD!' : 'clean',
  );
  return ok && !pad;
}

