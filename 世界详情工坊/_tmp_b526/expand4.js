// For each, create many more long paragraphs by expanding themes
function inflate(base, minChars) {
  const out = base.slice();
  const topics = ['战术','地理','人物','道具','时间线','对话','任务','禁忌','状态','载具','势力','结局'];
  let n = 0;
  while (out.join('').replace(/\s/g,'').length < minChars && n < 40) {
    const b = base[n % base.length];
    out.push(b.replace(/【/g, `【续·${topics[n % topics.length]}·`).replace(/】/, `·${n}】`));
    n++;
  }
  return out;
}

