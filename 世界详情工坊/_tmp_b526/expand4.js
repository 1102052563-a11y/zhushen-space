const fs = require('fs');
const path = require('path');
const DIR = path.join(__dirname, '..', '产出', '批次526');

function cnt(c) {
  const a = c.indexOf('## 剧情');
  const b = c.indexOf('## 阶位切入点');
  const d = c.indexOf('## 来源');
  const s = (x) => x.replace(/\s/g, '').length;
  return { plot: s(c.slice(a, b)), tier: s(c.slice(b, d)) };
}

function addPlot(file, paragraphs) {
  let c = fs.readFileSync(path.join(DIR, file), 'utf8');
  let i = 0;
  while (cnt(c).plot < 10000 && i < paragraphs.length) {
    const p = paragraphs[i];
    if (!c.includes(p.slice(10, 40))) {
      c = c.replace('## 阶位切入点', p + '\n## 阶位切入点');
    }
    i++;
  }
  // if still short, concatenate remaining in chunks of 2
  while (cnt(c).plot < 10000) {
    const extra = paragraphs.map((p, idx) => p + `（${file}延伸记${idx}-${cnt(c).plot}）`).join('\n');
    c = c.replace('## 阶位切入点', extra + '\n## 阶位切入点');
    if (cnt(c).plot >= 10000) break;
    // safety
    if (extra.length < 100) break;
  }
  fs.writeFileSync(path.join(DIR, file), c);
  console.log(file, cnt(c));
}

