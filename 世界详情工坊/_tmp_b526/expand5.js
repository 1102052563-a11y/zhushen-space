const fs = require('fs');
const DIR = require('path').join(__dirname, '..', '产出', '批次526');
function cnt(c) {
  const a = c.indexOf('## 剧情'), b = c.indexOf('## 阶位切入点'), d = c.indexOf('## 来源');
  const s = (x) => x.replace(/\s/g, '').length;
  return { plot: s(c.slice(a, b)), tier: s(c.slice(b, d)) };
}
function bump(file, text) {
  let c = fs.readFileSync(DIR + '/' + file, 'utf8');
  let guard = 0;
  while (cnt(c).plot < 10000 && guard < 5) {
    c = c.replace('## 阶位切入点', text + '\n## 阶位切入点');
    guard++;
  }
  fs.writeFileSync(DIR + '/' + file, c);
  console.log(file, cnt(c));
}
