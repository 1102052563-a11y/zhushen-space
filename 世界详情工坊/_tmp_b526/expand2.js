const fs = require('fs');
const path = require('path');
const DIR = path.join(__dirname, '..', '产出', '批次526');

function cnt(c) {
  const a = c.indexOf('## 剧情');
  const b = c.indexOf('## 阶位切入点');
  const d = c.indexOf('## 来源');
  const s = (x) => x.replace(/\s/g, '').length;
  return { plot: s(c.slice(a, b)), tier: s(c.slice(b, d)) };
}

function pad(file, plotBlock, tierBlock) {
  let c = fs.readFileSync(path.join(DIR, file), 'utf8');
  const keyP = plotBlock.slice(0, 24);
  const keyT = tierBlock.slice(0, 24);
  if (!c.includes(keyP)) c = c.replace('## 阶位切入点', plotBlock + '\n## 阶位切入点');
  if (!c.includes(keyT)) c = c.replace('## 来源', tierBlock + '\n## 来源');
  // keep padding until thresholds
  let n = 0;
  while (cnt(c).plot < 10000 && n < 8) {
    c = c.replace('## 阶位切入点', `\n**【原作细节补录·${n + 1}】**\n` + plotBlock + '\n## 阶位切入点');
    n++;
  }
  n = 0;
  while (cnt(c).tier < 1500 && n < 6) {
    c = c.replace('## 来源', `\n**【切入补录·${n + 1}】**\n` + tierBlock + '\n## 来源');
    n++;
  }
  fs.writeFileSync(path.join(DIR, file), c);
  console.log(file, cnt(c));
}

