function injectBefore(c, marker, block) {
  if (c.includes(block.slice(0, 40))) return c;
  const i = c.indexOf(marker);
  if (i < 0) throw new Error('marker not found: ' + marker);
  return c.slice(0, i) + block + c.slice(i);
}

function injectAfter(c, marker, block) {
  if (c.includes(block.slice(0, 40))) return c;
  const i = c.indexOf(marker);
  if (i < 0) throw new Error('marker not found: ' + marker);
  const j = i + marker.length;
  return c.slice(0, j) + block + c.slice(j);
}

function count(c) {
  const a = c.indexOf('## 剧情');
  const b = c.indexOf('## 阶位切入点');
  const d = c.indexOf('## 来源');
  const strip = s => s.replace(/\s/g, '').length;
  return { plot: strip(c.slice(a, b)), tier: strip(c.slice(b, d)) };
}

