function pad(name, scenes, people, places, acts) {
  const lines = [];
  for (let i = 0; i < 28; i++) {
    const sc = scenes[i % scenes.length];
    const pe = people[i % people.length];
    const pl = places[i % places.length];
    const ac = acts[i % acts.length];
    lines.push(
      `（${name}日常${i + 1}）在${pl}，${pe}做出「${ac}」。对话只留半句，其余用动作完成；好感体现在称呼、距离与是否分享食物。季节气味与物件声钉住这一刻——只属于《${name}》，禁止套用其他世界模板句。`
    );
  }
  return lines.join('\n\n');
}

function cutPad(name, people, places, hooks) {
  const lines = [];
  for (let i = 0; i < 18; i++) {
    const pe = people[i % people.length];
    const pl = places[i % places.length];
    const hk = hooks[i % hooks.length];
    lines.push(
      `- 事件钩子${i + 1}：在${pl}与**${pe}**相遇，触发「${hk}」。完成后好感微增，并解锁下一句只能对你说的私话。忌战斗任务化、忌角色OOC。`
    );
  }
  return lines.join('\n');
}

