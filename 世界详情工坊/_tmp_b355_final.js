function measure(name, text) {
  const sl = s => s.replace(/\s/g, '').length;
  const pm = text.match(/## 剧情([\s\S]*?)## 休闲切入点/);
  const em = text.match(/## 休闲切入点([\s\S]*?)## 来源/);
  const p = pm ? sl(pm[1]) : 0;
  const e = em ? sl(em[1]) : 0;
  const rel = (text.match(/关系细目/g) || []).length;
  const pad = (text.match(/可观察片段/g) || []).length;
  const pwr = (text.match(/力量体系|战力|阶位|巅峰战力/g) || []).length;
  const src = (text.match(/\]\(https?:\/\//g) || []).length;
  console.log(`${name} plot=${p} entry=${e} rel=${rel} pad=${pad} pwr=${pwr} src=${src}`);
  return p >= 6000 && e >= 1500 && rel === 0 && pad === 0 && pwr === 0 && src >= 3;
}

function save(name, text) {
  const p = path.join(base, name);
  const tmp = p + '.tmp';
  fs.writeFileSync(tmp, text.replace(/\r?\n/g, '\n'), 'utf8');
  fs.renameSync(tmp, p);
  const t = fs.readFileSync(p, 'utf8');
  const ok = measure(name, t);
  console.log(ok ? '  PASS' : '  FAIL');
  return ok;
}

