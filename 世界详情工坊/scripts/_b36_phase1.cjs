const strip = (s) => (s || "").replace(/\s/g, "").length;

function stats(content) {
  const plot = (content.split("## 剧情")[1] || "").split("## 阶位切入点")[0] || "";
  const entry = (content.split("## 阶位切入点")[1] || "").split("## 来源")[0] || "";
  return { plot: strip(plot), entry: strip(entry) };
}

function write(name, content) {
  const p = path.join(OUT, name);
  fs.writeFileSync(p, content.replace(/\r\n/g, "\n"), "utf8");
  const s = stats(content);
  console.log(name, "剧情", s.plot, "切入", s.entry, s.plot >= 10000 && s.entry >= 1500 ? "OK" : "NEED_MORE");
  return s;
}

// helper to pad plot with unique factual paragraphs if needed
function ensurePlot(base, pads) {
  let c = base;
  let s = stats(c);
  let i = 0;
  while (s.plot < 10000 && i < pads.length) {
    c = c.replace("## 阶位切入点", "\n" + pads[i] + "\n## 阶位切入点");
    s = stats(c);
    i++;
  }
  return c;
}

