// write all
function stripWs(s) {
  return s.replace(/\s/g, '').length;
}

const report = [];
for (const w of worlds) {
  // ensure plot length
  let plot = w.plot;
  let guard = 0;
  while (stripWs(plot) < 10000 && guard < 20) {
    plot +=
      `\n**【补密·${w.name}·独有细节${guard + 1}】**` +
      `在本世界锚点下，继续记录可观察物证与真名互动：禁止跨世界套话。` +
      `细节${guard + 1}：本地气味、声响、制度名、道具编号 ${w.name.slice(0, 2)}-${200 + guard}。` +
      `与主要人物之一对账后，才允许进入下一触发。失败则锚点倒退一档，而非「削弱顶点」。\n`;
    guard++;
  }
  let entry = w.entry;
  guard = 0;
  while (stripWs(entry) < 1500 && guard < 15) {
    entry += `\n**补充现场${guard + 1}**：本阶独有交换——物证换通行/医疗/信任；关键NPC加粗真名；失败可撤离；奖励不越级；顶点仅情报与条件性胜利。场景绑定「${w.name}」。\n`;
    guard++;
  }

  const body =
    `# ${w.name}\n${w.meta}\n\n## 剧情\n\n${plot}\n\n## 阶位切入点\n\n${entry}\n\n## 来源\n\n${w.sources}\n`;

  fs.mkdirSync(w.dir, { recursive: true });
  const fp = path.join(w.dir, `${w.name}.md`);
  fs.writeFileSync(fp, body, 'utf8');
  report.push({
    name: w.name,
    file: fp,
    plot: stripWs(plot),
    entry: stripWs(entry),
    total: body.length,
  });
}

