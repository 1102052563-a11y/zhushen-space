  let p = plot, e = entry;
  const padP = `
补充说明（生活细节与情感逻辑，供入世稳定）：在${name}的日常里，时间以学期、节日、社团活动日推进。角色的好感变化应来自「是否被尊重地记住」，而不是突然的命运强加。对话优先使用原作真名与原作口吻关键词。约会失败可以发生，但应带来关系修复的机会，而不是人格侮辱。家庭背景事件按原作已揭示部分展开，不新增原作没有的重大悲剧反转。季节描写（樱、梅雨、夏日蝉、秋祭、冬雪）用于烘托情绪。配角存在的意义是让主攻略对象更立体：朋友的调侃、家人的期待、同学的传闻，都应服务人物魅力。契约者融入后，第一人称生活细节（便当、電車、部活、打工薪水、短信）越多，世界越像原作。避免用战斗术语比喻恋爱；用「心跳、犹豫、鼓起勇气、失约、和解」描写冲突。长期关系中允许小别扭，不允许无动机黑化。`;
