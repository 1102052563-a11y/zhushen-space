function inject(file, block) {
  const fp = path.join(DIR, file);
  let t = fs.readFileSync(fp, 'utf8');
  const mark = '## 阶位切入点';
  if (!t.includes(mark)) throw new Error('no entry section ' + file);
  t = t.replace(/\n\*\*【加厚·档案】\*\*[\s\S]*?(?=\n## 阶位切入点)/, '\n');
  t = t.replace(mark, block.trim() + '\n\n' + mark);
  fs.writeFileSync(fp, t, 'utf8');
  const a = t.indexOf('## 剧情');
  const b = t.indexOf('## 阶位切入点');
  const plot = t.slice(a, b).replace(/\s/g, '').length;
  const entry = t.slice(b, t.indexOf('## 来源')).replace(/\s/g, '').length;
  console.log(file, 'plot', plot, 'entry', entry);
  return plot;
}

