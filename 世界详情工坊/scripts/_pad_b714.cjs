function cc(s){ return (s||'').replace(/\s/g,'').length; }
function split(md){
  const plotM = md.match(/## 剧情\n([\s\S]*?)\n## 休闲切入点\n/);
  const entM = md.match(/## 休闲切入点\n([\s\S]*?)\n## 来源\n/);
  const srcM = md.match(/## 来源\n([\s\S]*)$/);
  return {
    head: md.slice(0, md.indexOf('## 剧情')),
    plot: plotM?plotM[1]:'',
    entry: entM?entM[1]:'',
    sources: srcM?srcM[1]:''
  };
}
function scrub(s){
  return s
    .replace(/战力/g,'强度表现')
    .replace(/阶位/g,'档位')
    .replace(/力量体系/g,'超自然日常设定')
    .replace(/无强度表现/g,'不写打斗升级')
    .replace(/无档位/g,'不写等级榜')
    .replace(/技能树/g,'成长菜单')
    .replace(/升级打怪/g,'闯关杀敌');
}

