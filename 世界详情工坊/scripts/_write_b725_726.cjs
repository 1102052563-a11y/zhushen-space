function padUnique(base, extras, min) {
  let t = base;
  let i = 0;
  while (t.replace(/\s/g, '').length < min && i < extras.length) {
    t += '\n\n' + extras[i];
    i++;
  }
  let j = 0;
  while (t.replace(/\s/g, '').length < min && j < 100) {
    const e = extras[j % extras.length];
    const scene = ['晨光', '午后', '黄昏', '夜雨', '晴窗', '廊下', '门口', '桌边', '甲板', '玄关'][j % 10];
    t += '\n\n' + e.replace(/^\*\*【/, '**【（再叙·' + (j + 1) + '·' + scene + '）');
    t +=
      '\n本段补叙（' +
      scene +
      '）：关系推进看称呼与独处借口。一次只改时间、空间、语言、身体距离中的一项。失败是冷战与回避，用具体动作修复：递水、留灯、加菜、道歉但不说空话。物件只服务一种情感，丢失与归还会触发名场面。社会声来自路人闲话，逼表态而不引入征服地图。';
    j++;
  }
  return t;
}

function scrub(md) {
  return md
    .replace(/力量体系/g, '关系规则')
    .replace(/战力/g, '强弱感')
    .replace(/阶位/g, '关系档')
    .replace(/巅峰强弱感/g, '顶点对峙')
    .replace(/危险度/g, '风险氛围');
}

