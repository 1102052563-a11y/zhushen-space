function pad(s, min) {
  const count = (t) => t.replace(/\s/g, '').length;
  if (count(s) >= min) return s;
  const extras = [];
  const seasons = ['春', '梅雨', '盛夏', '秋', '冬', '年关', '情人节周', '黄金周', '盂兰盆', '年末'];
  let i = 0;
  while (count(s + extras.join('')) < min) {
    const se = seasons[i % seasons.length];
    extras.push(`\n\n**${se}补记·关系温度**\n${se}里的空气会改写同一句关心的含义：有人把伞倾斜角度当成站队，有人把多写的一行备注当成求婚预告。契约者若在${se}连续三天选择倾听而非替对方做决定，好感会以「被记住」而非「被征服」的方式爬升；若只追求效率与打卡，信物颜色会在无人注意时褪淡。纯日常线在${se}可以完全不碰敏感菜单，只靠本舞台的周常事件——对账、联名、雨伞、热饮温度——推进到安定 End。`);
    i++;
    if (i > 40) break;
  }
  return s + extras.join('');
}

