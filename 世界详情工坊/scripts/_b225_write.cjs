function pc(t) {
  return (t.split('## 剧情')[1] || '').split('## 阶位切入点')[0].replace(/\s/g, '').length;
}
function ec(t) {
  return (t.split('## 阶位切入点')[1] || '').split('## 来源')[0].replace(/\s/g, '').length;
}

function padPlot(t, blocks) {
  let i = 0;
  while (pc(t) < 10000 && i < blocks.length) {
    t = t.replace('\n## 阶位切入点', `\n${blocks[i]}\n\n## 阶位切入点`);
    i++;
  }
  let n = 0;
  while (pc(t) < 10000) {
    const line = blocks[n % blocks.length];
    const extra = `\n**【细录·${pc(t)}】**\n${line.replace(/\*\*【.*?】\*\*/g, '').trim()}\n`;
    t = t.replace('\n## 阶位切入点', extra + '\n## 阶位切入点');
    n++;
    if (n > 100) break;
  }
  return t;
}

function padEntry(t, extras) {
  let i = 0;
  while (ec(t) < 1500 && i < extras.length) {
    t = t.replace('\n## 来源', `\n${extras[i]}\n\n## 来源`);
    i++;
  }
  let n = 0;
  while (ec(t) < 1500) {
    const e = extras[n % extras.length];
    t = t.replace('\n## 来源', `\n${e}\n\n## 来源`);
    n++;
    if (n > 40) break;
  }
  return t;
}

function writeOne(name, body) {
  if (!fs.existsSync(DIR)) fs.mkdirSync(DIR, { recursive: true });
  const fp = path.join(DIR, name + '.md');
  fs.writeFileSync(fp, body, 'utf8');
  const r = spawnSync(process.execPath, [CHECK, '--check', fp], { encoding: 'utf8' });
  const out = (r.stdout || '') + (r.stderr || '');
  console.log(out.trim());
  console.log(`[count] ${name} plot=${pc(body)} entry=${ec(body)} exit=${r.status}`);
  return r.status === 0;
}

