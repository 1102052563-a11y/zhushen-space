function buildWorld(w){
  // Expand dense unique sections from world config
  const castBlocks = w.cast.map(c =>
`${c.name}（${c.title}）｜外貌：${c.look}｜性格：${c.personality}｜角色类型：${c.type}｜萌点/魅力：${c.charm}｜个人线剧情：${c.route}；HE：${c.he}；BE：${c.be}｜与主角关系：${c.rel}`
  ).join('\n\n');

  const routeBlocks = w.cast.map(c =>
`**${c.name}线（${c.title}）**
${c.routeDetail || c.route}
关键事件：${c.events || '共管信标物、公开说停、出口验证、只谈天气的缓冲日'}。
攻略核：${c.core || c.charm}。
结局：HE＝${c.he}；BE＝${c.be}；Bittersweet＝${c.bit || '体面离开仍互道真名'}。`
  ).join('\n\n');

  const scenes = (w.scenes||[]).map((s,i)=>`${i+1}. **${s.t}**：${s.d}`).join('\n');
  const places = (w.places||[]).map(p=>`- **${p.n}**：${p.d}`).join('\n');
  const hooks = (w.hooks||[]).map((h,i)=>`${i+1}. ${h}`).join('\n');
  const targets = (w.targets||w.cast.slice(0,5)).map(t=>{
    const n = t.n || t.name;
    const d = t.d || `在${w.stage}接触；好感起点＝礼貌距离；钩子＝${t.charm||t.core||'尊重中止与真名'}；心结＝${t.knot||'怕被工具化'}。`;
    return `- **${n}**：${d}`;
  }).join('\n');

  // Extra density paragraphs unique per world
  const thicken = w.thicken || '';

  const plot = `**【作品来源】**
${w.source}

**【世界定位】**
${w.position}

**【世界观 · 舞台设定】**
${w.world}

软规则（写作必须遵守）：
${w.rules.map((r,i)=>`${i+1}. ${r}`).join('\n')}

世界的温度来自：${w.warmth}

本世界为休闲恋爱向档案：**不写**力量体系、战力对比、阶位评级、巅峰清单或清剿式任务。若出现超自然／异质元素，只作情感、羞耻、压迫或治愈装置，服务关系选择与可退出的亲密。

**【地理 · 生活舞台】**
${places}

**【故事主线 · 情感线】**

**共通线：${w.commonTitle}**
${w.common}

时间节拍参考：${w.timeline}

${routeBlocks}

**微观日常事件池（每次细节须变化，禁止复制粘贴灌水）**
${(w.micro||[]).map(m=>`- ${m}`).join('\n')}

**结局方向**
${(w.endings||[]).map(e=>`- **${e.n}**：${e.d}`).join('\n')}

${thicken}

**【可攻略角色 / 主要人物】**

${castBlocks}

契约者视点（${w.playerRole}）｜姓名外貌自定；默认细心、不站队、坚持可退出与真名。成长体现为：${w.playerGrowth}

**【人际关系网 / 社团势力】**
${w.relations}

**【情感事件 · 名场面】**
${scenes}

**【隐藏剧情 · 真结局 · 伏笔】**
${w.hidden}

**【氛围基调 · 雷区】**
${w.vibe}
NSFW 尺度：${w.nsfw}
忌：
${w.taboos.map((t,i)=>`${i+1}. ${t}`).join('\n')}
最适合切入：${w.bestCut}`;

  const entry = `> 本世界为休闲/恋爱向，无生存压力与战斗。契约者以**日常身份**融入，核心玩法＝relationship 攻略 + 日常事件，而非任务厮杀。全员成年；任何亲密须可观察同意与可中止信号。

切入身份：${w.entryId}

切入时点：${w.entryWhen}

初始处境：${w.entrySit}

开场白建议：「${w.entryOpen}」

可攻略对象：
${targets}

日常玩法钩子：
${hooks}

氛围/雷区：${w.entryVibe}
忌：${w.taboos.join('；')}。**严禁**力量体系／战力／阶位／任务厮杀套话；**严禁**未成年性化；**严禁**无同意强制与不可退出的囚禁浪漫化。
最适合切入：${w.bestCut}。

补充代入细节：你的第一周不必完成任何「攻略进度条」。优先做三件可见的小事——${w.entryExtras || '确认中止信号有效、确认出口可走、记住至少三个真名的忌口或小习惯'}。正文应允许尴尬、停顿、说错话后的道歉；最动人的高感往往来自「你在该停的时候停」，而不是「你在最快的时候推进」。若原作题材含强制、监禁、触手、催眠等硬核标签，本库一律改写为**可中止、可离场、可双签可拒签**的成人合意情景；拒绝把伤害写成必经通关。`;

  const body = `# ${w.name}
<!--meta lib=休闲 tiers=休闲-->

## 剧情

${plot}

## 休闲切入点

${entry}

## 来源

${sources(w.links)}
`;
  return body;
}

