function padTo(section, min, padBlocks) {
  let t = section;
  let i = 0;
  while (charCount(t) < min && i < padBlocks.length * 8) {
    t += '\n\n' + padBlocks[i % padBlocks.length];
    i++;
  }
  return t;
}

