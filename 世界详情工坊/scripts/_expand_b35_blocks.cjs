// If still short, pad with more unique factual paragraphs
function ensureMin(file, more) {
  let c = fs.readFileSync(file, 'utf8');
  let s = stats(c);
  let n = 0;
  while (s.p < 10000 && n < 5) {
    c = c.replace('\n## 阶位切入点', '\n' + more + '\n## 阶位切入点');
    fs.writeFileSync(file, c, 'utf8');
    s = stats(c);
    n++;
    console.log('pad', path.basename(file), s, 'round', n);
  }
  if (s.p < 10000) console.log('STILL SHORT', path.basename(file), s);
  else console.log('OK', path.basename(file), s);
}

