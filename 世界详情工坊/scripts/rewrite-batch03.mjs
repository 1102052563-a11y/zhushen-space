import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'node:url';
import { spawnSync } from 'node:child_process';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const DIR = path.join(ROOT, '产出', '批次03');
const BATCH_MD = path.join(ROOT, '清单', '批次表.md');
fs.mkdirSync(DIR, { recursive: true });

function write(name, body) {
  const fp = path.join(DIR, name);
  fs.writeFileSync(fp, body.replace(/\r\n/g, '\n').replace(/\n/g, '\r\n'), 'utf8');
  const r = spawnSync(process.execPath, [path.join(ROOT, 'scripts', 'compile-worldbook.mjs'), '--check', fp], {
    encoding: 'utf8',
    cwd: ROOT,
  });
  process.stdout.write(r.stdout || '');
  if (r.status !== 0) process.stderr.write(r.stderr || '');
  return r.status === 0;
}

// 通用：把剧情段撑到≥10000的「本世界」增密（禁止跨书复制）
function densify(plotCore, entryCore, sources, worldKey, densifyParas) {
  let plot = plotCore;
  let i = 0;
  while (plot.replace(/\s/g, '').length < 10000 && i < densifyParas.length) {
    plot += '\n\n' + densifyParas[i];
    i++;
  }
  let entry = entryCore;
  let j = 0;
  const entryPads = densifyParas.slice(0, 6).map(
    (p, n) =>
      `**支线事件池${n + 1}（${worldKey}）**\n` +
      p.slice(0, 400) +
      `\n本阶可刷：护送、拍卖骗局、名额战、与原作配角短暂雇佣、高阶余波迁移。写清报酬与死亡率。`,
  );
  while (entry.replace(/\s/g, '').length < 1500 && j < entryPads.length) {
    entry += '\n\n' + entryPads[j];
    j++;
  }
  return plot.trim() + '\n\n## 阶位切入点\n\n' + entry.trim() + '\n\n## 来源\n\n' + sources.trim() + '\n';
}

