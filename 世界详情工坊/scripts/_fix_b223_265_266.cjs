const cc = (s) => (s || '').replace(/\s/g, '').length;

function splitDoc(t) {
  const nameM = t.match(/^#\s+(.+?)\s*$/m);
  const metaM = t.match(/<!--\s*meta\s+([^>]*?)-->/);
  const entryIdx = t.search(/^## 阶位切入点\s*$/m);
  const srcIdx = t.search(/^## 来源\s*$/m);
  if (entryIdx < 0 || srcIdx < 0) throw new Error('missing entry/src sections');
  const head = t.slice(0, t.search(/^## 剧情\s*$/m));
  const plotRaw = t.slice(t.search(/^## 剧情\s*$/m) + '## 剧情'.length, entryIdx).replace(/^\s*\n/, '');
  const entry = t.slice(entryIdx, srcIdx);
  const src = t.slice(srcIdx);
  return {
    name: nameM ? nameM[1].trim() : '',
    meta: metaM ? metaM[0] : '',
    head,
    plotRaw,
    entry,
    src,
  };
}

/** 去掉 densify / 移动细录 / 时序循环等灌水段，保留必备段与本世界专属写实段 */
function stripPad(plot) {
  const lines = plot.replace(/\r\n/g, '\n').split('\n');
  const out = [];
  let skip = false;
  for (let i = 0; i < lines.length; i++) {
    const L = lines[i];
    // 进入灌水块
    if (
      /·现场细则\d|·时序\d|·扩写|移动细录|【扩写|【细目|【补密|【加厚|关系温度计|冲突与修复|可重复周循环|NSFW与伦理边界|结算四格/.test(
        L
      )
    ) {
      skip = true;
      continue;
    }
    // 新的真段落标题可恢复（必备【】且不含灌水词）
    if (skip && /^\*\*【/.test(L) && !/现场细则|时序|扩写|细目|补密|加厚|温度计|冲突与修复|周循环|NSFW|结算四格|移动细录/.test(L)) {
      skip = false;
    }
    // 纯重复的短「移动细录」标题行也跳
    if (/^\*\*【移动细录/.test(L)) {
      skip = true;
      continue;
    }
    if (!skip) out.push(L);
  }
  // 去掉连续重复段落（完全相同的非空行块）
  const text = out.join('\n');
  // 再切一次：从第一个 densify 残留标题起丢弃到文末（若 strip 不彻底）
  const cut = text.search(/\*\*【[^】]*(现场细则|时序\d+|扩写|细目|补密|加厚)/);
  return (cut >= 0 ? text.slice(0, cut) : text).trim() + '\n';
}

function writeDoc(file, plotBody) {
  const t = fs.readFileSync(file, 'utf8');
  const d = splitDoc(t);
  const body = plotBody.trim() + '\n';
  const out = `${d.head}## 剧情\n\n${body}\n${d.entry}${d.src}`;
  fs.writeFileSync(file, out.replace(/\n{3,}/g, '\n\n'));
  const plotCC = cc(body);
  const entryCC = cc(d.entry.replace(/^## 阶位切入点\s*/, '').split('## 来源')[0]);
  console.log(path.basename(file), 'plot', plotCC, 'entry', entryCC, plotCC >= 10000 ? 'OK' : 'NEED+' + (10000 - plotCC));
}

// ─── 223 黑兽三档：在去重后的核心上追加专属块 ───
function expandKuroju(file, extra) {
  const t = fs.readFileSync(file, 'utf8');
  const d = splitDoc(t);
  let core = stripPad(d.plotRaw);
  // 去掉重复的「移动细录」循环：保留到第一次出现「**【佣兵·」类之后若再整段复制则截
  // 更稳：按行去重完全相同的 **【xxx】** 标题体
  const seen = new Set();
  const lines = core.split('\n');
  const kept = [];
  let i = 0;
  while (i < lines.length) {
    const L = lines[i];
    if (/^\*\*【/.test(L)) {
      // 取到下一个标题或空行块结束
      let j = i + 1;
      while (j < lines.length && !/^\*\*【/.test(lines[j])) j++;
      const block = lines.slice(i, j).join('\n');
      const key = L.replace(/\d+/g, '#'); // 数字归一
      if (seen.has(key) && /移动细录|佣兵·|时序/.test(L)) {
        i = j;
        continue;
      }
      // 对「移动细录」一律丢弃（纯重复）
      if (/移动细录/.test(L)) {
        i = j;
        continue;
      }
      seen.add(key);
      kept.push(...lines.slice(i, j));
      i = j;
      continue;
    }
    kept.push(L);
    i++;
  }
  core = kept.join('\n').trim() + '\n\n' + extra.trim() + '\n';
  writeDoc(file, core);
}

