// ── 写文件 + 机检 ──
const allNames = [];
const results = [];

for (const w of worlds) {
  for (const c of w.cast) allNames.push(`${w.name}::${c.name}`);
  const text = buildFile(w);
  for (const b of BANNED) {
    if (text.includes(b)) {
      console.error(`BANNED in ${w.name}: ${b}`);
      process.exit(1);
    }
  }
  if (/关系细目|日程细目/.test(text)) {
    console.error(`PAD MARKER in ${w.name}`);
    process.exit(1);
  }
  if (/战力|阶位|巅峰战力/.test(text)) {
    console.error(`POWER WORD in ${w.name}`);
    process.exit(1);
  }
  const dir = path.join(OUT_ROOT, `批次${w.batch}`);
  fs.mkdirSync(dir, { recursive: true });
  const outPath = path.join(dir, w.file);
  fs.writeFileSync(outPath, text, 'utf8');

  const r = spawnSync(process.execPath, [path.join(ROOT, 'scripts', 'compile-worldbook.mjs'), '--check', outPath], {
    encoding: 'utf8',
  });
  const out = ((r.stdout || '') + (r.stderr || '')).trim();
  const ok = /✓|过关|PASS|ok/i.test(out) && r.status === 0;
  const body = fs.readFileSync(outPath, 'utf8');
  const plot = (body.split('## 休闲切入点')[0] || '').split('## 剧情')[1] || '';
  const entry = (body.split('## 休闲切入点')[1] || '').split('## 来源')[0] || '';
  const cc = (s) => s.replace(/\s/g, '').length;
  const ximu = (body.match(/关系细目|日程细目/g) || []).length;
  const srcN = (body.match(/https?:\/\//g) || []).length;
  const pass = ok && ximu === 0 && cc(plot) >= 6000 && cc(entry) >= 1500 && srcN >= 3;
  results.push({
    batch: w.batch,
    name: w.name,
    plot: cc(plot),
    entry: cc(entry),
    ximu,
    srcN,
    check: ok ? 'PASS' : 'FAIL',
    pass: pass ? 'PASS' : 'FAIL',
    note: ok ? '' : out.split(/\n/).slice(0, 2).join(' | '),
  });
  console.log(`${pass ? '✓' : '✗'} b${w.batch}/${w.file} plot=${cc(plot)} entry=${cc(entry)} ximu=${ximu} src=${srcN} check=${ok ? 'ok' : 'fail'}`);
}

