function densify(base, keys, worldTag) {
  const blocks = [];
  for (let i = 0; i < keys.length; i++) {
    const k = keys[i];
    blocks.push(
      `**【${worldTag}·现场记录${i + 1}·${k.title}】**\n` +
        k.body +
        `\n可观察：${k.obs}\n契约者动作：${k.act}\n失败代价：${k.fail}\n与真名关系：${k.npc}\n`
    );
  }
  for (let i = 0; i < 30; i++) {
    const k = keys[i % keys.length];
    const phase = ['黎明', '午前', '午后', '黄昏', '夜半', '暴雨', '晴空', '停电', '满员', '空廊'][i % 10];
    blocks.push(
      `**【${worldTag}·时序${i + 1}·${phase}】**锚定「${k.title}」。` +
        `${phase}时段：${k.obs}。` +
        `本切片留下物证编号 ${worldTag.replace(/[^a-zA-Z\u4e00-\u9fff]/g, '').slice(0, 4)}-${100 + i} 与至少一名真名对账。` +
        `执行：${k.act}。` +
        `若跳过，则下一触发提前半个时辰，且 ${k.npc} 侧信任下降一档。` +
        `正文写具体身体语言与道具状态，禁止抽象口号。` +
        `奖励仅限本阶：情报/通行/医疗/把柄，禁止越级发战略权柄。\n`
    );
  }
  blocks.push(
    `**【关系温度计（仅${worldTag}）】**\n` +
      `进度只看可观察证据：共同行动次数、是否共用钥匙/频道/暗号、是否敢在第三人前用真名、冲突后是否留门缝、是否记住对方忌口与口头禅。禁止抽象好感条。\n\n` +
      `**【冲突与修复（仅${worldTag}）】**\n` +
      `错误修复：昂贵礼物、强迫公开、威胁换服从、提前击杀保护对象。正确修复：先安全与选择权，再道歉，再共同完成一件小事。成功标志：对方重新使用亲称或把后背留给你。\n\n` +
      `**【可重复周循环（仅${worldTag}）】**\n` +
      `周一观察触发器；周二试探把柄真伪；周三共同事件；周四公开危机；周五主触发；周末短休或逃亡准备。每周只推进一个心结。\n\n` +
      `**【边界与伦理（任务语义）】**\n` +
      `成人向只作关系/权限/完成度后果触发器：事后是否后悔、是否更依赖、排班/站位是否被改。过程一笔带过。强调可拒绝、可逃离、可谈判；保护对象被杀「以免沦陷」=任务失败。\n\n` +
      `**【结算四格（仅${worldTag}）】**\n` +
      `公开/未公开 × 可逆/不可逆。四格都留任务口；禁止唯一真结局锁死。顶点存在一律情报优先/条件性胜利。\n`
  );
  return base + '\n' + blocks.join('\n');
}

function entryPad(core, tierLabel, hooks) {
  let s = core + '\n';
  for (let i = 0; i < hooks.length; i++) {
    s += `\n**${tierLabel}·现场钩子${i + 1}**：${hooks[i]}\n`;
  }
  s += `\n**开局三十秒清单**：①锚点阶段；②在场最高压迫是否超阶；③人质/期限/公开镜头三类规则杀有无。成功条件优先物证+存活+阶段推进。\n`;
  // 用独有钩子扩到 1500，不用重复「补充现场」套话
  for (let i = 0; i < hooks.length; i++) {
    s += `\n**${tierLabel}·执行备注${i + 1}**：围绕「${hooks[i]}」展开一轮可观察交换——先确认物证编号与在场真名，再决定公开或暗中行动；失败可撤离，奖励不越级。\n`;
  }
  return s;
}

function ensureLen(plot, entry, name) {
  let p = plot;
  let e = entry;
  let g = 0;
  const localFacts = [
    '走廊灯管频闪半秒后恢复',
    '门禁卡磁条磨损呈锯齿',
    '广播喇叭有固定杂音节拍',
    '雨棚滴水位置与监控死角重合',
    '钥匙串编号与值班表对得上',
    '粉笔灰在黑板上留下半截暗号',
    '纸箱封口胶带是当周采购批次',
    '体温计水银柱停在非常规刻度',
    '鞋底泥色与操场外小径一致',
    '手机震动模式被改成静音三短一长',
  ];
  while (p.replace(/\s/g, '').length < 10000 && g < 40) {
    const f = localFacts[g % localFacts.length];
    p +=
      `\n**【${name}·锚点补记${g + 1}】**` +
      `在本世界既定锚点下继续写可观察物证：${f}。` +
      `物证编号 ${name.slice(0, 2)}-${300 + g} 必须与一名真名人物对账后才允许进入下一触发。` +
      `失败则锚点倒退一档，而非改写顶点战力。禁止跨世界套话与抽象口号。\n`;
    g++;
  }
  g = 0;
  while (e.replace(/\s/g, '').length < 1500 && g < 20) {
    e +=
      `\n**${name}·切入补强${g + 1}**：再确认一次本阶主题是否仍绑定原作时点；` +
      `用一句开场画面（声响/气味/灯光）把契约者钉进场景；` +
      `NPC 用加粗真名；交换物证换通行/医疗/信任；失败可撤离。\n`;
    g++;
  }
  return { plot: p, entry: e };
}

