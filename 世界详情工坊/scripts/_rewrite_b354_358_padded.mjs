// ─── run ───────────────────────────────────────────────
const results = [];
let fail = 0;

for (const w of WORLDS) {
  const dir = path.join(OUT, `批次${w.batch}`);
  const fp = path.join(dir, `${w.name}.md`);
  if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });

  // delete then write complete clean entry
  if (fs.existsSync(fp)) fs.unlinkSync(fp);

  let doc = buildFile(w);
  // hard scrub banned + pad markers
  for (const b of BANNED) {
    if (doc.includes(b)) doc = doc.split(b).join('（已净化）');
  }
  doc = doc
    .replace(/关系细目/g, '关系温度')
    .replace(/日程细目/g, '日程温度')
    .replace(/力量体系|战力|阶位|巅峰战力/g, '边界节奏');

  fs.writeFileSync(fp, doc, 'utf8');

  const r = spawnSync(process.execPath, [path.join(ROOT, 'scripts', 'compile-worldbook.mjs'), '--check', fp], {
    encoding: 'utf8',
  });
  const out = ((r.stdout || '') + (r.stderr || '')).trim();
  const text = fs.readFileSync(fp, 'utf8');
  const ximu = (text.match(/关系细目|日程细目/g) || []).length;
  const spam = (text.match(/可观察片段/g) || []).length;
  const ok = /过关/.test(out) && ximu === 0 && spam === 0 && r.status === 0;
  const line = `${ok ? 'PASS' : 'FAIL'} 批次${w.batch}/${w.name} | ximu=${ximu} spam=${spam} | ${out.split('\n')[0] || ''}`;
  results.push(line);
  process.stdout.write(line + '\n');
  if (!ok) {
    fail++;
    process.stdout.write(out + '\n');
  }
}

// final batch-wide verify
const allPad = spawnSync(
  process.execPath,
  ['-e', `
    const fs=require('fs');const path=require('path');
    const root=path.join(${JSON.stringify(OUT)});
    let n=0;
    for (const b of [354,355,356,357,358]) {
      const d=path.join(root,'批次'+b);
      if(!fs.existsSync(d)) continue;
      for (const f of fs.readdirSync(d).filter(x=>x.endsWith('.md'))) {
        const t=fs.readFileSync(path.join(d,f),'utf8');
        const c=(t.match(/关系细目|日程细目/g)||[]).length;
        if(c){ console.log('REMAIN', b, f, c); n+=c; }
      }
    }
    console.log('TOTAL_PAD_MARKERS', n);
  `],
  { encoding: 'utf8' },
);
process.stdout.write(allPad.stdout || '');
process.stdout.write(allPad.stderr || '');

console.log('\n=== SUMMARY ===');
console.log(results.join('\n'));
console.log(fail ? `\nFAILED ${fail}/${WORLDS.length}` : `\nALL PASS ${WORLDS.length}/${WORLDS.length}`);
process.exit(fail ? 2 : 0);
