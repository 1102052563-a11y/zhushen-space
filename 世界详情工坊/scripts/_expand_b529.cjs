const fs = require("fs");
const path = require("path");
const DIR = path.join(__dirname, "../产出/批次529");

function expand(file, plotExtra, entryExtra) {
  let t = fs.readFileSync(path.join(DIR, file), "utf8").replace(/\r\n/g, "\n");
  // insert before ## 阶位切入点
  const i = t.indexOf("\n## 阶位切入点");
  if (i < 0) throw new Error("no entry " + file);
  t = t.slice(0, i) + "\n\n" + plotExtra.trim() + "\n" + t.slice(i);
  // insert before ## 来源
  const j = t.indexOf("\n## 来源");
  if (j < 0) throw new Error("no src " + file);
  t = t.slice(0, j) + "\n\n" + entryExtra.trim() + "\n" + t.slice(j);
  fs.writeFileSync(path.join(DIR, file), t.replace(/\n/g, "\r\n"), "utf8");
  const plot = (t.split("## 剧情")[1] || "").split("## 阶位切入点")[0] || "";
  const entry = (t.split("## 阶位切入点")[1] || "").split("## 来源")[0] || "";
  console.log(file, "plot", plot.replace(/\s/g, "").length, "entry", entry.replace(/\s/g, "").length);
}

