  const cutM = text.match(/## 休闲切入点\s*([\s\S]*?)(?=\n## |$)/);
  const pc = plotM ? plotM[1].replace(/\s/g, '').length : 0;
  const cc = cutM ? cutM[1].replace(/\s/g, '').length : 0;
  const ok = /过关/.test(out) && spam === 0 && !hasProg && !hasFrag && r.status === 0 && pc >= 6000 && cc >= 1500;
