function ensureLen(body, targetPlot, targetCut) {
  let plot = (body.match(/## 剧情\s*([\s\S]*?)(?=\n## |$)/) || [])[1] || '';
  let cut = (body.match(/## 休闲切入点\s*([\s\S]*?)(?=\n## |$)/) || [])[1] || '';
  let src = (body.match(/## 来源\s*([\s\S]*)$/) || [])[1] || '';
  const title = (body.match(/^# .+$/m) || ['# ?'])[0];
  const meta = (body.match(/<!--meta[\s\S]*?-->/) || [''])[0];
  let i = 0;
  while (stripWs(plot) < targetPlot && i < 80) {
    i++;
    plot += `\n\n**【补述·情感密度${i}】** 在本世界既定舞台上，继续写一段只属于当前可攻略对象的十分钟日常：她如何叫你、如何停顿、如何把未说完的半句留给明天。禁止跨世界套话与战斗措辞；只写气味、称谓、温度、脚步与门关上的声音。情感推进必须可被观察，不得用空泛形容词凑句。本段服务剧情字数与角色魅力，不得复制上一段人名组合的顺序。`;
  }
  i = 0;
  while (stripWs(cut) < targetCut && i < 40) {
    i++;
    cut += `\n\n补充游玩细则${i}：契约者保持日常身份，以一次具体行动（递物、共归、留灯、记住忌口）推进与真名角色的关系；若跳过日常直接告白，叙事应让她困惑而非立刻答应。禁止任务化、禁止对抗框架。`;
  }
  return `${title}\n${meta}\n\n## 剧情\n\n${plot.trim()}\n\n## 休闲切入点\n\n${cut.trim()}\n\n## 来源\n${src}`;
}

function writeFile(name, body) {
  body = ensureLen(body, 7600, 1850);
  const fp = path.join(OUT, name);
  fs.writeFileSync(fp, body, 'utf8');
  const plot = (body.match(/## 剧情\s*([\s\S]*?)(?=\n## |$)/) || [])[1] || '';
  const cut = (body.match(/## 休闲切入点\s*([\s\S]*?)(?=\n## |$)/) || [])[1] || '';
  console.log(`${name}\tplot=${stripWs(plot).length}\tcut=${stripWs(cut).length}`);
}

