function stripWs(s) {
  return (s || '').replace(/\s/g, '');
}

function padDaily(tag, places, people, props, n = 36) {
  const days = ['月曜朝', '月曜夜', '火曜雨', '水曜午后', '木曜祭', '金曜终电', '土曜海', '日曜宿舍'];
  const acts = [
    '她把信物往你这边推了半寸，又像后悔似的收回一指',
    '你先叫她的全名，她才肯把视线从别处挪回来',
    '窗外天气忽然变了，两人同时伸手去够同一件外套',
    '有人经过时她压低声音，等脚步远了才把后半句说完',
    '她笑着否认心动，手指却一直摩挲信物的边角',
    '你把行程空出来的那一刻，她的肩膀明显松了一下',
    '共同完成一件小事后，她用新的称呼试探你',
    '沉默不是冷淡，是她在组织一句不会伤人的真话',
  ];
  const lines = [];
  for (let i = 0; i < n; i++) {
    const day = days[i % days.length];
    const pl = places[i % places.length];
    const pe = people[i % people.length];
    const pr = props[i % props.length];
    const act = acts[i % acts.length];
    const trust = (i % 5) + 1;
    lines.push(
      `【${tag}·情境${i + 1}·${day}】在${pl}，与**${pe}**围绕「${pr}」发生可观察互动：谁先开口、谁先移开视线、谁把${pr}放回原位。${act}。空气里有本世界独有的气味与声响，禁止套用其他条目人名或跨世界地名。信任刻度${trust}/5；边界是否被尊重＝${i % 2 ? '待确认' : '是'}。正文用${pr}作信标，重复出现三次以上才允许告白级对白；若她心结未被回应，下一情境信任刻度强制-1。此节只服务${tag}的情感进度，可接续到共食、共归、共撑伞或共写一张便条。`
    );
  }
  return lines.join('\n\n');
}

