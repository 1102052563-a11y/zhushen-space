function counts(s) {
  const m = s.match(/## 剧情([\s\S]*?)## 休闲切入点/)
  const plot = m ? m[1].replace(/\s/g, '').length : 0
  const m2 = s.match(/## 休闲切入点([\s\S]*?)## 来源/)
  const cut = m2 ? m2[1].replace(/\s/g, '').length : 0
  return { plot, cut }
}
