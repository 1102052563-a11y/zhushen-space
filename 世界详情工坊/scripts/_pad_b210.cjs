const fs = require('fs');
const path = require('path');
const OUT = path.join(__dirname, '..', '产出', '批次210');

const cc = (s) => (s || '').replace(/\s/g, '').length;

function split(md) {
  const i = md.indexOf('## 剧情');
  const j = md.indexOf('## 阶位切入点');
  const k = md.indexOf('## 来源');
  return {
    head: md.slice(0, i + '## 剧情'.length),
    plot: md.slice(i + '## 剧情'.length, j).trim(),
    entry: md.slice(j + '## 阶位切入点'.length, k).trim(),
    src: md.slice(k).trim(),
  };
}

function save(file, head, plot, entry, src) {
  const body = `${head}\n\n${plot.trim()}\n\n## 阶位切入点\n\n${entry.trim()}\n\n${src.trim()}\n`;
  fs.writeFileSync(path.join(OUT, file), body.replace(/\r\n/g, '\n'), 'utf8');
  console.log(file, 'plot', cc(plot), 'entry', cc(entry), cc(plot) >= 10000 && cc(entry) >= 1500 ? 'OK' : 'NEED');
}

