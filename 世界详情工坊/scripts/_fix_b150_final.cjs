function mdLinks(urls) {
  return urls.map((u) => {
    const name = u.replace(/^https?:\/\//, '').split('/')[0] + ' · ' + decodeURIComponent(u.split('/').pop() || u);
    return `- [${name}](${u})`;
  }).join('\n');
}

