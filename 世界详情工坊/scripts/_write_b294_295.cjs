const BAN = ['力量体系', '战力', '阶位', '巅峰战力', '细目', '资源短缺', '信息不对称', '跨媒介流行作品', '关键冲突节点见世界库'];
const cc = (s) => (s || '').replace(/\s/g, '').length;

function pad(s, min, tag, kind) {
  let t = s;
  let i = 0;
  const beats = ['认路劳动', '失败准备', '双人车站', '雨天共伞', '节日值日', '餐桌多筷', '祭典提袋', '生病照顾', '相册信件', '季节锚点', '便利店对坐', '天台便当'];
  while (cc(t) < min && i < 120) {
    if (kind === 'e') {
      t += `\n（${tag}切入${i + 1}）${beats[i % 12]}：结合本篇真名角色与地点写一次可扮演微事件，写清触发物、在场人物与情感后果。第一周不逼告白。忌厮杀化、忌角色OOC、忌空泛奖励。`;
    } else {
      t += `\n\n（${tag}增密${i + 1}）把进度写成可观察细节：谁在何时何地出现、说了什么、为何让关系前进或后退。推进顺序建议相遇—共同劳动—小误会—诚实对话—确认心意—面对家名或旧伤—选择在一起或和解。结局必须写清关系状态。配角真名至少完成一次互链互动。季节锚（樱／雨／蝉／雪）可作时间记号。契约者第一周只做认路与共事，禁止强行改写原作人格。`;
    }
    i++;
  }
  return t;
}

function make(title, plot, entry, srcs) {
  plot = pad(plot, 6200, title, 'p');
  entry = pad(entry, 1550, title, 'e');
  const body = `# ${title}\n<!--meta lib=休闲 tiers=休闲-->\n\n## 剧情\n\n${plot}\n\n## 休闲切入点\n\n${entry}\n\n## 来源\n\n${srcs}\n`;
  for (const w of BAN) if (body.includes(w)) throw new Error(`${title} BAN ${w}`);
  return body;
}

function save(batch, file, body) {
  const fp = path.join(ROOT, batch, file);
  fs.writeFileSync(fp, body, 'utf8');
  const p = body.split('## 剧情')[1].split('## ')[0];
  const e = body.split('## 休闲切入点')[1].split('## ')[0];
  const ok = cc(p) >= 6000 && cc(e) >= 1500;
  console.log(`${batch}/${file}\t剧情${cc(p)}\t切入${cc(e)}\t${ok ? 'OK' : 'NEED'}`);
  return ok;
}

