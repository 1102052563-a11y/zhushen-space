const BAN = /力量体系|战力|阶位|巅峰战力|危险度|贵重法宝/g;

function noWs(s) {
  return (s || '').replace(/\s/g, '').length;
}

function assertClean(name, body) {
  const m = body.match(BAN);
  if (m) throw new Error(name + ' still has banned: ' + [...new Set(m)].join(','));
}

function padTo(base, extras, min) {
  let t = base;
  let i = 0;
  while (noWs(t) < min && i < extras.length) {
    t += '\n\n' + extras[i++];
  }
  let j = 0;
  while (noWs(t) < min && j < 50) {
    const e = extras[j % extras.length];
    t += '\n\n' + e.replace(/（本世界专属[：:]/, '（再补·' + (j + 1) + '：');
    j++;
  }
  if (noWs(t) < min) throw new Error('pad failed ' + noWs(t) + '<' + min);
  return t;
}

function doc(name, plot, entry, sources, plotExtras, entryExtras) {
  const p = padTo(plot, plotExtras, 6200);
  const e = padTo(entry, entryExtras, 1550);
  const body = `# ${name}
<!--meta lib=休闲 tiers=休闲-->

## 剧情

${p}

## 休闲切入点

${e}

## 来源

${sources}
`;
  assertClean(name, body);
  return body;
}

