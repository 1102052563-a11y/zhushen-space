function safeFile(name) {
  // Windows 非法: < > : " / \ | ? *  → 全角；文内 # 标题仍用原名
  return (
    name
      .replace(/\?/g, '？')
      .replace(/!/g, '！')
      .replace(/</g, '＜')
      .replace(/>/g, '＞')
      .replace(/:/g, '：')
      .replace(/"/g, '＂')
      .replace(/\//g, '／')
      .replace(/\\/g, '＼')
      .replace(/\|/g, '｜')
      .replace(/\*/g, '＊') + '.md'
  );
}

