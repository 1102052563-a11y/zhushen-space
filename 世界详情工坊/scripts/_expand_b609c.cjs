const fs = require('fs');
const path = require('path');
const dir = path.join(__dirname, '../产出/批次609');

function addPlot(file, block) {
  const p = path.join(dir, file);
  let t = fs.readFileSync(p, 'utf8');
  const marker = '## 阶位切入点';
  const i = t.indexOf(marker);
  if (!t.includes(block.slice(0, 20))) {
    t = t.slice(0, i) + block + '\n\n' + t.slice(i);
    fs.writeFileSync(p, t, 'utf8');
  }
  t = fs.readFileSync(p, 'utf8');
  const plot = (t.match(/## 剧情\s*([\s\S]*?)(?=\n## 阶位切入点)/) || [,''])[1].replace(/\s/g, '').length;
  const entry = (t.match(/## 阶位切入点\s*([\s\S]*?)(?=\n## 来源)/) || [,''])[1].replace(/\s/g, '').length;
  console.log(file, 'plot=', plot, 'entry=', entry, plot >= 10000 && entry >= 1500 ? 'OK' : 'NEED');
}

