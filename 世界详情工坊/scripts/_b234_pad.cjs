function fixTierLine(t) {
  if (!t.includes('## 阶位切入点')) return t;
  const parts = t.split('## 阶位切入点');
  let rest = parts[1];
  // remove leading short > lines that aren't 阶位↔
  rest = rest.replace(/^\s*>[^\n]*\n+/, '\n' + TIER_LINE);
  if (!rest.includes('阶位↔')) {
    rest = '\n' + TIER_LINE + rest;
  }
  return parts[0] + '## 阶位切入点' + rest;
}

function injectBefore(t, marker, block) {
  if (!t.includes(marker)) return t;
  return t.replace(marker, block + '\n\n' + marker);
}

