function padFile(name) {
  const fp = path.join(DIR, name);
  let t = fs.readFileSync(fp, 'utf8');
  t = fixTierLine(t);
  const plots = PLOT[name] || [];
  const entries = ENTRY[name] || [];
  let i = 0;
  while (pc(t) < 10000 && i < plots.length) {
    t = injectBefore(t, '## 阶位切入点', plots[i]);
    i++;
  }
  // if still short, repeat with numbered unique headers
  let n = 0;
  while (pc(t) < 10000 && plots.length) {
    const base = plots[n % plots.length];
    const block = `**【${name.replace('.md', '')}·战术附录${n + 1}】**\n${base.replace(/\*\*【.*?】\*\*/g, '').trim()}\n补充要点：本附录仅服务「${name.replace('.md', '')}」线，禁止套用到系列其他副标题。执行时优先真名NPC与本线地点，拒绝跨线复制任务板。\n`;
    t = injectBefore(t, '## 阶位切入点', block);
    n++;
    if (n > 60) break;
  }
  i = 0;
  while (ec(t) < 1500 && i < entries.length) {
    t = injectBefore(t, '## 来源', entries[i]);
    i++;
  }
  n = 0;
  while (ec(t) < 1500 && entries.length) {
    t = injectBefore(
      t,
      '## 来源',
      `${entries[n % entries.length]}\n（切入附录${n + 1}·${name.replace('.md', '')}）\n`
    );
    n++;
    if (n > 30) break;
  }
  fs.writeFileSync(fp, t, 'utf8');
  const r = spawnSync(process.execPath, [CHECK, '--check', fp], {
    encoding: 'utf8',
    cwd: path.join(__dirname, '..'),
  });
  console.log(((r.stdout || '') + (r.stderr || '')).trim());
  console.log(`[count] ${name} plot=${pc(t)} entry=${ec(t)} exit=${r.status}`);
  return r.status === 0;
}

