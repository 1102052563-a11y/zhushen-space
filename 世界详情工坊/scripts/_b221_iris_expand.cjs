const charCount = (s) => s.replace(/\s/g, '').length;

function parse(file) {
  const text = fs.readFileSync(file, 'utf8').replace(/\r\n/g, '\n');
  const nameM = text.match(/^#\s+(.+?)\s*$/m);
  const metaM = text.match(/<!--\s*meta\s+([^>]*?)-->/);
  const sections = {};
  const secRe = /^##\s+(剧情|阶位切入点|来源)\s*$/gm;
  const marks = [];
  let m;
  while ((m = secRe.exec(text)) !== null) marks.push({ name: m[1], start: m.index, bodyStart: m.index + m[0].length });
  for (let i = 0; i < marks.length; i++) {
    const end = i + 1 < marks.length ? marks[i + 1].start : text.length;
    sections[marks[i].name] = text.slice(marks[i].bodyStart, end).trim();
  }
  return { name: nameM[1].trim(), meta: metaM ? metaM[0] : '<!--meta lib=主库 tiers=四-->', sections };
}

function pack(doc) {
  return `# ${doc.name}\n${doc.meta}\n\n## 剧情\n\n${doc.sections['剧情']}\n\n## 阶位切入点\n\n${doc.sections['阶位切入点']}\n\n## 来源\n\n${doc.sections['来源']}\n`;
}

