const fs = require("fs");
const path = require("path");
const DIR = path.join(__dirname, "../产出/批次224");
const cc = (s) => (s || "").replace(/\s/g, "").length;
const BAD = /细目|资源短缺|信息不对称|可介入节点|战场细录|本阶补记|加厚·|扩写·|补密|剧情补述|清单\d/;

function split(t) {
  const pre = t.split("## 剧情")[0] + "## 剧情";
  const mid = (t.split("## 剧情")[1] || "").split("## 阶位切入点")[0];
  const entFull = (t.split("## 阶位切入点")[1] || "");
  const entry = entFull.split("## 来源")[0];
  const src = "## 来源" + (entFull.split("## 来源")[1] || "");
  return { pre, mid, entry, src };
}

function expand(name, plotBlocks, entryBlocks) {
  const fp = path.join(DIR, name);
  let t = fs.readFileSync(fp, "utf8");
  let { pre, mid, entry, src } = split(t);
  let i = 0;
  while (cc(mid) < 10050 && i < plotBlocks.length) {
    mid += "\n\n" + plotBlocks[i++];
  }
  if (cc(mid) < 10050) throw new Error(name + " plot still short " + cc(mid) + " need more blocks");
  let j = 0;
  while (cc(entry) < 1550 && j < entryBlocks.length) {
    entry += "\n\n" + entryBlocks[j++];
  }
  if (cc(entry) < 1550) throw new Error(name + " entry still short " + cc(entry));
  const body = pre + mid + "\n\n## 阶位切入点\n" + entry + "\n\n" + src;
  if (BAD.test(body)) throw new Error(name + " banned " + body.match(BAD)[0]);
  fs.writeFileSync(fp, body.replace(/\r\n/g, "\n"), "utf8");
  console.log(name, "plot", cc(mid), "entry", cc(entry), "OK");
}

