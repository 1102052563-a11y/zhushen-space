function dedupeParagraphs(text) {
  const parts = text.split(/\n\n+/);
  const seen = new Set();
  const out = [];
  for (const p of parts) {
    const key = p.replace(/\s/g, '').slice(0, 120);
    if (!key) continue;
    if (seen.has(key)) continue;
    // drop generic pad lines
    if (/本阶可刷：假货|NPC用牙人、同期|跨媒介流行作品|阶段档案/.test(p)) continue;
    if (/群像|红颜线|相关存在|气压|标尺角色|代称/.test(p) && p.includes('**【')) continue;
    seen.add(key);
    out.push(p);
  }
  return out.join('\n\n');
}

