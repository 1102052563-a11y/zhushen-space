const fs = require("fs");
const path = require("path");
const OUT = path.join(__dirname, "..", "产出", "批次215");
const cc = (s) => s.replace(/\s/g, "").length;

function expandFile(file, insertBeforeEntry) {
  const p = path.join(OUT, file);
  let t = fs.readFileSync(p, "utf8");
  if (!t.includes(insertBeforeEntry.slice(0, 40))) {
    t = t.replace("## 阶位切入点", insertBeforeEntry + "\n## 阶位切入点");
  }
  // pad entry if needed
  const entry = (t.split("## 阶位切入点")[1] || "").split("## 来源")[0] || "";
  if (cc(entry) < 1500) {
    const pad = `
**本阶补充（各阶独有执行细则）**
一阶补充：优先完成「活着离开第一现场」与「建立可信身份」；勿展示越阶破坏力。
二阶补充：介入资源与情报交换，允许有限战斗，禁止挑战本阶段天花板。
三阶补充：可改局部战役结果，须承担追杀与政治后果。
五阶补充：触达质变装备/形态，任务以协作机制与情报为主。
六阶补充：顶点相关仅条件性胜利，奖励为人脉与残响而非弑神。
`;
    t = t.replace("## 来源", pad + "\n## 来源");
  }
  fs.writeFileSync(p, t.replace(/\r\n/g, "\n"), "utf8");
  const plot = (t.split("## 剧情")[1] || "").split(/## 阶位切入点/)[0] || "";
  const ent = (t.split("## 阶位切入点")[1] || "").split("## 来源")[0] || "";
  console.log(file, "plot", cc(plot), "entry", cc(ent));
}

