const fs = require('fs');
const path = require('path');
const OUT = path.join(__dirname, '..', '产出', '批次210');
const cc = (s) => (s || '').replace(/\s/g, '').length;

function parts(file) {
  const t = fs.readFileSync(path.join(OUT, file), 'utf8');
  const i = t.indexOf('## 剧情');
  const j = t.indexOf('## 阶位切入点');
  const k = t.indexOf('## 来源');
  return {
    head: t.slice(0, i + '## 剧情'.length),
    plot: t.slice(i + '## 剧情'.length, j).trim(),
    entry: t.slice(j + '## 阶位切入点'.length, k).trim(),
    src: t.slice(k).trim(),
  };
}
function save(file, p) {
  const body = `${p.head}\n\n${p.plot.trim()}\n\n## 阶位切入点\n\n${p.entry.trim()}\n\n${p.src}\n`;
  fs.writeFileSync(path.join(OUT, file), body.replace(/\r\n/g, '\n'), 'utf8');
  console.log(file, 'plot', cc(p.plot), 'entry', cc(p.entry), cc(p.plot) >= 10000 && cc(p.entry) >= 1500 ? 'OK' : 'NEED');
}

