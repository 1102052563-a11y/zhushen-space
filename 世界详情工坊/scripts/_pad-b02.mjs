import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'node:url';
import { spawnSync } from 'node:child_process';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const DIR = path.join(ROOT, '产出', '批次02');

function need(file) {
  const t = fs.readFileSync(path.join(DIR, file), 'utf8');
  const a = t.indexOf('## 剧情');
  const b = t.indexOf('## 阶位切入点');
  return t.slice(a, b).replace(/\s/g, '').length;
}

function insertBeforeEntry(file, chunk) {
  const fp = path.join(DIR, file);
  let t = fs.readFileSync(fp, 'utf8');
  t = t.replace('## 阶位切入点', chunk + '\n\n## 阶位切入点');
  fs.writeFileSync(fp, t, 'utf8');
}

function force(file, paragraphs) {
  let n = need(file);
  let k = 0;
  while (n < 10050 && k < paragraphs.length) {
    insertBeforeEntry(file, `**【补段${k + 1}】**${paragraphs[k]}`);
    n = need(file);
    k++;
  }
  console.log(file, 'plot', n, 'added', k);
}

