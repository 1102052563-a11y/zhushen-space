for (const w of worlds) {
  const outDir = path.join(ROOT, '产出', w.batch);
  fs.mkdirSync(outDir, { recursive: true });
  const text = buildFile(w);
  // hard gates before write
  const bad = BANNED.filter((b) => text.includes(b));
  const rel = (text.match(/关系细目/g) || []).length;
  const battle = /力量体系|战力|阶位|巅峰战力/.test(text);
  const adultOk = /成年|同意|可以停|可叫停|可拒绝/.test(text);
  if (bad.length || rel || battle || !adultOk) {
    report.push({ name: w.name, ok: false, bad, rel, battle, adultOk });
    console.error('FAIL precheck', w.name, { bad, rel, battle, adultOk });
    continue;
  }
  const fp = path.join(outDir, w.file);
  fs.writeFileSync(fp, text, 'utf8');
  report.push({ name: w.name, file: fp, ok: true, bytes: Buffer.byteLength(text, 'utf8') });
  console.log('WROTE', w.batch, w.file, Buffer.byteLength(text, 'utf8'));
}

