function process(file, plotBlocks, entryBlocks) {
  const fp = path.join(OUT, file);
  const raw = fs.readFileSync(fp, 'utf8');
  const { head, sections } = splitMd(raw);
  sections['剧情'] = padTo(sections['剧情'], 10000, plotBlocks);
  sections['阶位切入点'] = padTo(sections['阶位切入点'], 1500, entryBlocks);
  const out = joinMd(head, sections);
  fs.writeFileSync(fp, out, 'utf8');
  console.log(
    file,
    '剧情',
    charCount(sections['剧情']),
    '切入',
    charCount(sections['阶位切入点'])
  );
}

