const charCount = (s) => (s || '').replace(/\s/g, '').length;

function splitMd(text) {
  const re = /^##\s+(剧情|阶位切入点|来源)\s*$/gm;
  const marks = [];
  let m;
  while ((m = re.exec(text)) !== null) marks.push({ name: m[1], start: m.index, bodyStart: m.index + m[0].length });
  const sections = {};
  for (let i = 0; i < marks.length; i++) {
    const end = i + 1 < marks.length ? marks[i + 1].start : text.length;
    sections[marks[i].name] = text.slice(marks[i].bodyStart, end).trim();
  }
  const head = text.slice(0, marks[0].start);
  return { head, sections };
}

function joinMd(head, sections) {
  return (
    head +
    '## 剧情\n\n' +
    sections['剧情'].trim() +
    '\n\n## 阶位切入点\n\n' +
    sections['阶位切入点'].trim() +
    '\n\n## 来源\n\n' +
    sections['来源'].trim() +
    '\n'
  );
}

function padTo(text, min, blocks) {
  let t = text;
  let i = 0;
  while (charCount(t) < min && i < blocks.length * 3) {
    t += '\n\n' + blocks[i % blocks.length];
    i++;
  }
  // if still short, append numbered unique operational notes (no 灌水标记)
  let n = 1;
  while (charCount(t) < min) {
    const seed = blocks[(n - 1) % blocks.length];
    const focus = [
      '气味与光线',
      '通行证件',
      '通讯干扰',
      '平民疏散',
      '补给消耗',
      '盟友违约风险',
      '舆论剪辑',
      '地形杀',
    ][(n - 1) % 8];
    t += `\n\n**【现场执行笔记·${n}·${focus}】**\n本段服务 AI 在具体回合写「可观察细节」：围绕「${focus}」展开。` + seed.slice(0, 500) + ` 笔记编号${n}要求：先写环境与代价，再写胜负政治；禁止跨世界套话；人物必须真名出场。`;
    n++;
    if (n > 40) break;
  }
  return t;
}

