import fs from 'fs';

function padUnique(file, paragraphs) {
  let t = fs.readFileSync(file, 'utf8');
  let added = 0;
  for (const p of paragraphs) {
    if (!t.includes(p.slice(0, 12))) {
      t = t.replace('## 阶位切入点', p + '\n## 阶位切入点');
      added++;
    }
  }
  fs.writeFileSync(file, t);
  const plot = t.split('## 阶位切入点')[0].replace(/\s/g, '').length;
  console.log(file, 'added', added, 'plot', plot);
}

