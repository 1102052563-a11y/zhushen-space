function expand(file, block) {
  const p = path.join(dir, file);
  let t = fs.readFileSync(p, 'utf8');
  const marker = '## 阶位切入点';
  const i = t.indexOf(marker);
  if (i < 0) throw new Error('no marker ' + file);
  if (t.includes('【世界剧情线·分阶段细目】')) {
    console.log('skip already expanded', file);
  } else {
    t = t.slice(0, i) + block + '\n\n' + t.slice(i);
    fs.writeFileSync(p, t, 'utf8');
  }
  const m = t.match(/## 剧情\s*([\s\S]*?)(?=\n## 阶位切入点)/);
  const plot = m ? m[1].replace(/\s/g, '').length : 0;
  const e = t.match(/## 阶位切入点\s*([\s\S]*?)(?=\n## 来源)/);
  const entry = e ? e[1].replace(/\s/g, '').length : 0;
  console.log(file, 'plot=', plot, 'entry=', entry);
}

