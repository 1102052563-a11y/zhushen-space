function need() {
  const x = fs.readFileSync(fp, 'utf8');
  return x.slice(x.indexOf('## 剧情'), x.indexOf('## 阶位切入点')).replace(/\s/g, '').length;
}

function inject(block) {
  let t = fs.readFileSync(fp, 'utf8');
  t = t.replace('## 阶位切入点', block.trim() + '\n\n## 阶位切入点');
  fs.writeFileSync(fp, t, 'utf8');
}

