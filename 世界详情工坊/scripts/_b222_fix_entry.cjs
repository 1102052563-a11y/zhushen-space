const fs = require('fs');
const path = require('path');
const dir = path.join(__dirname, '..', '产出', '批次222');

function fix(file, mapLine, entryBlock) {
  const p = path.join(dir, file);
  let t = fs.readFileSync(p, 'utf8');
  if (!t.includes('乐园阶位映射')) {
    // insert after 力量体系 section roughly before 地理
    if (t.includes('**【地理 · 舞台】**')) {
      t = t.replace('**【地理 · 舞台】**', mapLine + '\n\n**【地理 · 舞台】**');
    } else {
      t = t.replace('## 阶位切入点', mapLine + '\n\n## 阶位切入点');
    }
  }
  // expand entry before ## 来源
  if (!t.includes('【切入点扩写完成】')) {
    t = t.replace('\n## 来源', '\n' + entryBlock + '\n\n【切入点扩写完成】\n\n## 来源');
  }
  fs.writeFileSync(p, t, 'utf8');
  const plot = (t.split('## 剧情')[1] || '').split('## 阶位切入点')[0].replace(/\s/g, '').length;
  const entry = (t.split('## 阶位切入点')[1] || '').split('## 来源')[0].replace(/\s/g, '').length;
  console.log(file, 'plot', plot, 'entry', entry, 'map', t.includes('乐园阶位映射'));
}

