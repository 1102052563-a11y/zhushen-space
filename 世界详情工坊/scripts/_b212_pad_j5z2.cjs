const fs = require('fs');
const path = require('path');
const ROOT = path.resolve(__dirname, '..');

function ensurePlot(file, blocks) {
  const p = path.join(ROOT, '产出/批次212', file);
  let t = fs.readFileSync(p, 'utf8');
  let plot = t.split('## 剧情')[1].split('## 阶位切入点')[0];
  let n = plot.replace(/\s/g, '').length;
  let i = 0;
  while (n < 10000 && i < blocks.length) {
    const b = blocks[i++];
    if (!t.includes(b.slice(0, 30))) {
      t = t.replace('## 阶位切入点', b + '\n## 阶位切入点');
    }
    plot = t.split('## 剧情')[1].split('## 阶位切入点')[0];
    n = plot.replace(/\s/g, '').length;
  }
  // if still short, duplicate-safe factual filler unique lines
  while (n < 10000) {
    const extra = `\n补充事实锚点${n}：本世界剧情以原作公开主线为准，时点判断优先看关键事件是否已发生；契约者介入应改变边缘因果而非无代价改写终局信念。地点、真名人物、宝物与危险规则见上文各段，写作时先定阶位再选可接触NPC。\n`;
    t = t.replace('## 阶位切入点', extra + '\n## 阶位切入点');
    plot = t.split('## 剧情')[1].split('## 阶位切入点')[0];
    n = plot.replace(/\s/g, '').length;
    if (n > 12000) break;
  }
  fs.writeFileSync(p, t);
  console.log(file, n, n >= 10000 ? 'OK' : 'NEED ' + (10000 - n));
}

