const fs = require("fs");
const path = require("path");
const DIR = path.join(__dirname, "../产出/批次224");
const cc = (s) => (s || "").replace(/\s/g, "").length;
const BAD = /细目|资源短缺|信息不对称|可介入节点|战场细录|本阶补记|加厚·|扩写·|补密|剧情补述|清单\d/;

function parts(t) {
  const pre = t.split("## 剧情")[0] + "## 剧情";
  const mid = (t.split("## 剧情")[1] || "").split("## 阶位切入点")[0];
  const rest = t.split("## 阶位切入点")[1] || "";
  const entry = rest.split("## 来源")[0];
  const src = "## 来源" + (rest.split("## 来源")[1] || "");
  return { pre, mid, entry, src };
}

function save(name, pre, mid, entry, src) {
  const body = (pre + mid + "\n\n## 阶位切入点\n" + entry + "\n\n" + src).replace(/\r\n/g, "\n");
  if (BAD.test(body)) throw new Error(name + " banned " + body.match(BAD)[0]);
  fs.writeFileSync(path.join(DIR, name), body, "utf8");
  console.log(name, "plot", cc(mid), "entry", cc(entry), cc(mid) >= 10000 && cc(entry) >= 1500 ? "OK" : "BAD");
}

