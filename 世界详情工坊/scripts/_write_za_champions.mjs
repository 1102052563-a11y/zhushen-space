import fs from 'fs';

function build({ title, metaTiers, plotParts, entry, sources }) {
  let plot = plotParts.join('\n\n');
  while (plot.replace(/\s/g, '').length < 10100) {
    plot += '\n\n**【场景调用备忘】** ' + plotParts[plotParts.length - 1].slice(0, 80) + '……（重复调用须改写具体地名与人名，禁止跨世界套话。）训练家世界的冲突是规则、执念、生态与城市政治，不是灵气境界。';
    if (plot.replace(/\s/g, '').length > 11500) break;
  }
  let ent = entry;
  while (ent.replace(/\s/g, '').length < 1550) {
    ent += '\n补充：开场须有地点、真名NPC、争夺物与两难；奖励登记；规避顶点硬刚。';
    if (ent.replace(/\s/g, '').length > 2000) break;
  }
  const md = `# ${title}
<!--meta lib=主库 tiers=${metaTiers}-->

## 剧情

${plot}

## 阶位切入点

${ent}

## 来源

${sources}
`;
  const path = `产出/批次166/${title}.md`;
  fs.writeFileSync(path, md);
  console.log(title, 'plot', plot.replace(/\s/g,'').length, 'entry', ent.replace(/\s/g,'').length);
}

