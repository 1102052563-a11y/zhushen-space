for (const f of files) {
  let t = fs.readFileSync(path.join(OUT, f), 'utf8');
  const ent = entries[f];
  if (!ent) {
    console.log('no entry', f);
    continue;
  }
  const i = t.indexOf('## 阶位切入点');
  const j = t.indexOf('## 来源');
  if (i < 0 || j < 0) {
    console.log('struct', f);
    continue;
  }
  t = t.slice(0, i) + '## 阶位切入点\n\n' + ent + '\n\n' + t.slice(j);

  const m = t.match(/## 剧情([\s\S]*?)## 阶位切入点/);
  let plot = m[1];
  const tag = f.replace('.md', '');
  let n = 1;
  while (wc(plot) < 10000) {
    plot +=
      '\n\n【' +
      tag +
      '·补录' +
      n +
      '】六阶现场以物证链推进：样本编号、钥匙录音、地图水印、频谱峰型、名单列完整性、黑金等级——每项对应可观察世界状态变化。与顶点交互写条件不写秒杀。补录' +
      n +
      '锚定本切片独有冲突（' +
      tag +
      '），禁止替换为跨世界通用拍卖护送秘境。入场核验/中段补给/撤离窗口/失败态转进四段清单须在正文可被追问。\n';
    n++;
    if (n > 30) break;
  }
  t = t.replace(/## 剧情[\s\S]*?## 阶位切入点/, '## 剧情\n' + plot + '\n## 阶位切入点');

  fs.writeFileSync(path.join(OUT, f), t, 'utf8');
  const nt = fs.readFileSync(path.join(OUT, f), 'utf8');
  const p = wc(nt.match(/## 剧情([\s\S]*?)## 阶位切入点/)[1]);
  const e = wc(nt.match(/## 阶位切入点([\s\S]*?)## 来源/)[1]);
  console.log(f, '剧情', p, '切入', e, p >= 10000 && e >= 1500 ? 'OK' : 'FAIL');
}
