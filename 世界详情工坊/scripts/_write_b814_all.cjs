// thicken if under min
function thicken(label, text, min, padBlocks) {
  let t = text;
  let i = 0;
  while (cnt(t) < min && i < padBlocks.length) {
    t += '\n\n' + padBlocks[i];
    i++;
  }
  if (cnt(t) < min) {
    // last resort: repeat last pad with variation note
    while (cnt(t) < min) {
      t += '\n\n' + padBlocks[padBlocks.length - 1];
    }
  }
  return t;
}

