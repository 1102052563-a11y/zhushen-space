function main() {
  const args = process.argv.slice(2).map(Number).filter(Boolean);
  const batches = args.length ? args : Object.keys(BATCHES).map(Number);
  let ok = 0, fail = [];
  for (const b of batches) {
    const list = BATCHES[b];
    if (!list) { console.error('no batch', b); continue; }
    const outDir = path.join(ROOT, '产出', `批次${b}`);
    fs.mkdirSync(outDir, { recursive: true });
    for (const name of list) {
      const p = PROFILES[name];
      if (!p) { fail.push(name + '(no profile)'); continue; }
      const file = path.join(outDir, name + '.md');
      fs.writeFileSync(file, makeMd(name, b, p), 'utf8');
      if (runCheck(file)) ok++; else fail.push(name);
    }
  }
  console.log('\n=== SUMMARY ok=', ok, 'fail=', fail.length, fail);
}

