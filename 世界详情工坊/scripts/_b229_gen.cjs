const fs = require("fs");
const path = require("path");
const OUT = path.join(__dirname, "../产出/批次229");
const wc = (s) => (s || "").replace(/\s/g, "").length;

function writeWorld(name, meta, plot, entry, sources) {
  const md = [
    `# ${name}`,
    `<!--meta ${meta}-->`,
    ``,
    `## 剧情`,
    ``,
    plot.trim(),
    ``,
    `## 阶位切入点`,
    ``,
    entry.trim(),
    ``,
    `## 来源`,
    ``,
    sources.trim(),
    ``,
  ].join("\n");
  const fp = path.join(OUT, `${name}.md`);
  fs.writeFileSync(fp, md, "utf8");
  const plotN = wc(plot);
  const entryN = wc(entry);
  console.log(
    `${name}: 剧情${plotN} 切入${entryN} ${plotN >= 10000 && entryN >= 1500 ? "OK" : "NEED_PAD"}`
  );
  return { plotN, entryN, fp };
}

// ─── shared pad helper ───
function join(...parts) {
  return parts.filter(Boolean).join("\n\n");
}

