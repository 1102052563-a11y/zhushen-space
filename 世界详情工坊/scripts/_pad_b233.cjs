function morePad(name, n) {
  const lines = [];
  for (let i = 1; i <= n; i++) {
    lines.push(`**【${name}·战局切片${i}】** 本切片要求AI在正文中给出：地点专名、在场具名角色至少两名、一件可拾取或可 prioritise 的道具、一个不可逆选择及其三日内后果。禁止与其它切片复用同一句开场。切片编号仅供档案内部索引，对玩家不可见。`);
  }
  return '\n' + lines.join('\n');
}

