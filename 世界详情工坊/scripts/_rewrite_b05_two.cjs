初始事件：威士忌山峰「欢迎海贼」的宴会后刀光四起；雨之季谣言与舞踊沙时计成为硬通货。
开场白建议：「伟大航路的指针乱转，比人心更不可信。公主的真实姓名是禁忌，七武海的影子在沙漠里比太阳大——你手里的编号徽章烫得像烙印。」
