const charCount = (s) => s.replace(/\s/g, '').length;

function ensureLen(s, min, padBlocks) {
  let out = s;
  let i = 0;
  while (charCount(out) < min && i < padBlocks.length) {
    out += '\n\n' + padBlocks[i++];
  }
  while (charCount(out) < min) {
    out += '\n\n' + padBlocks[padBlocks.length - 1] + `（补述对齐公开剧情·序号${i++}）`;
  }
  return out;
}

function wrap(name, tiers, plotBody, entryBody, sources) {
  const plot = ensureLen(plotBody, 10000, plotPads(name));
  const entry = ensureLen(entryBody, 1500, entryPads(name));
  const md = `# ${name}\n<!--meta lib=主库 tiers=${tiers}-->\n\n## 剧情\n\n${plot}\n\n## 阶位切入点\n\n${entry}\n\n## 来源\n\n${sources}\n`;
  const fp = path.join(OUT, `${name}.md`);
  fs.writeFileSync(fp, md, 'utf8');
  console.log(name, '剧情', charCount(plot), '切入', charCount(entry));
  return fp;
}

