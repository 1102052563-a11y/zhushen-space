② **第一部：十大天神兵争夺**  
十八年后，问天在北冥成长。四大家族联姻与聚会遭天地盟重创。故事由虎魄现世、血腥江湖开启，中期蚩尤秘窟灭邪玄网，后现天晶之首与元祖天魔线索。编剧调整后玄天邪帝与大罗刹宗主成为转折，玄幻加重。问天逐步集齐/面对十大天神兵：天晶、虎魄、太虚、十方俱灭、神农尺、噬魂、天诛、惊邪、神舞、凤皇等（公开列表）。

③ **人物与势力绞杀**  
南宫铁心女扮男装曾掌南宫；实为东方雄与神算子卓不凡之女。玄天邪帝（独孤星夜）为天魔一口魔气投胎，成人礼魔眼重生，屠神道盟创邪道盟，被鬼谷先师封印百年后复出。北冥雪、西城秀树、武勇、乐娃、燕王（慕容儁伪天地盟主）等线交织。天地盟外征府天地双使、金银铜玉四法王，内务府与妖兽军。

④ **第二部：十大魔兵与天魔**  
魔珠吸负面情感成十大魔兵，异魔收集以释放天魔。神道盟复仇、邪帝与宗主决战为支线。高潮：问天以天晶子母剑合璧「完美天晶」联合天神兵对抗天魔与魔籽南宫太平（问天与铁心之子，天魔之籽，曾魔霸，后驱散魔性）。魔兵毁灭后异能以图纹附体。

⑤ **第三部及衍生**  
数百年后豪杰集五柄小天晶重组新天晶对妖兵；中期硬融隋唐与天子传奇人物，口碑两极。前传写玄天邪帝、武勇、太平；F系列清末二战；问天续篇等。本档案主库任务以**壹+贰神魔兵主线**为核。

