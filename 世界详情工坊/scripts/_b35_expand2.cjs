function stats(c) {
  const plot = (c.match(/## 剧情\s*([\s\S]*?)## 阶位切入点/) || [])[1] || '';
  const ent = (c.match(/## 阶位切入点\s*([\s\S]*?)## 来源/) || [])[1] || '';
  return { p: plot.replace(/\s/g, '').length, e: ent.replace(/\s/g, '').length };
}

function inject(file, marker, block) {
  let c = fs.readFileSync(file, 'utf8');
  if (!c.includes(marker)) {
    c = c.replace('\n## 阶位切入点', '\n' + block + '\n## 阶位切入点');
    fs.writeFileSync(file, c, 'utf8');
  }
  console.log(path.basename(file), stats(fs.readFileSync(file, 'utf8')));
}

