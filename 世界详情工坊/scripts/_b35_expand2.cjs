// keep padding with unique blocks until 10000
function padUntil(file, blocks) {
  let c = fs.readFileSync(file, 'utf8');
  let s = stats(c);
  let i = 0;
  while (s.p < 10000 && i < blocks.length) {
    const b = blocks[i];
    if (!c.includes(b.slice(0, 20))) {
      c = c.replace('\n## 阶位切入点', '\n' + b + '\n## 阶位切入点');
      fs.writeFileSync(file, c, 'utf8');
      c = fs.readFileSync(file, 'utf8');
      s = stats(c);
    }
    i++;
  }
  console.log('final', path.basename(file), s);
}

