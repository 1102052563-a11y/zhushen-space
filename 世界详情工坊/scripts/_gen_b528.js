开场白建议：「契约的红字比血还红。镜子里的你多了一道疤，那是上一次热度留下的。扎格笑：再高一档？你点头，像点死刑令。」
关键NPC立场：**扎格**——疯；**墨纪拉/塔纳托斯**——加强；**卡戎**——涨价；**混沌**——赌命祝福。
