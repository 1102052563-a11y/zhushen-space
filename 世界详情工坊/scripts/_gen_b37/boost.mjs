for (const [file, b] of Object.entries(boosts)) {
  const p = path.join(DIR, file);
  let t = fs.readFileSync(p, 'utf8');
  // remove 场面颗粒 and 执行补强 spam
  t = t.replace(/\n\n\*\*【场面颗粒·\d+】\*\*[^\n]*/g, '');
  t = t.replace(/\n\n执行补强\d+：[^\n]*/g, '');

  for (const block of b.plot) {
    if (!t.includes(block.slice(0, 25))) {
      t = t.replace('**【叙事基调 · 雷区】**', `${block}\n\n**【叙事基调 · 雷区】**`);
    }
  }
  for (const block of b.entry) {
    if (!t.includes(block.slice(0, 20))) {
      t = t.replace('\n## 来源', `\n${block}\n\n## 来源`);
    }
  }

  // hard pad with unique numbered factual sentences if needed
  let { plot, entry } = sections(t);
  let n = 0;
  const pool = b.plot.join('').match(/[\u4e00-\u9fff]{8,40}/g) || ['原作真名场面必须可观察'];
  while (count(plot) < 10000 && n < 50) {
    const sentence = pool[n % pool.length];
    const para = `\n\n**【补录${n + 1}】** 关于「${sentence}」：AI须能回答谁在场、谁敌对、代价是什么；契约者局部改写不得取消顶点逻辑。`;
    t = t.replace('**【叙事基调 · 雷区】**', `${para}\n\n**【叙事基调 · 雷区】**`);
    plot = sections(t).plot;
    n++;
  }
  let m = 0;
  while (count(sections(t).entry) < 1500 && m < 20) {
    t = t.replace('\n## 来源', `\n\n钩子补${m + 1}：本阶须有独特事件名与加粗真名NPC，禁止与其他阶复制同一句填充。\n\n## 来源`);
    m++;
  }

  fs.writeFileSync(p, t, 'utf8');
  const s = sections(t);
  console.log(file, count(s.plot), count(s.entry), count(s.plot) >= 10000 && count(s.entry) >= 1500 ? 'OK' : 'NO');
}
