function block(meta, i) {
  return `

**【常青档案扩写·${i}】**
本段服务AI对「任意时点世界状态」的判断，必须绑定真名：${meta.names}。关键地点：${meta.places}。关键宝物与规则物：${meta.items}。
写冲突时先定契约者处于哪一层地理，再选冲突强度：底层是生存、配额、欺压与小资源；中层是阵营战争、魔兽/Boss、公司追杀；高层是顶点规则与终局代价。本地人若听闻低阶要硬刚顶点，会当疯子——这是正确反应。
因果链要求：每个名场面写清起因、介入点、胜负代价、对后续的锁定。契约者可救一个路人、抢一件非核心物、让某场小战斗易主；不可无代价改写原作终局核心、顶点战力逻辑与主角关键底色。改了局部，原作因果会以追杀、取代、更惨的方式回流。
人物交互用真名称呼与立场句：谁可结盟、谁会背刺、谁只认利益、谁是条件性可谈的顶点阴影。装备与能力写到可辨识的招牌，而非空泛「很强」。势力之间写清临时同盟与必然反目的触发器。
叙事执行：开场给画面与钩子，中段给抉择，收尾给代价。禁止跨世界套话，禁止用群像/红颜/牙人/代行者单独成条。本世界卡片锚点相关场景必须可玩、可失败、可付出血价。
`;
}

function entryBlock(meta, i) {
  return `

切入补充${i}：本阶初始事件须点名至少两名真名NPC（候选：${meta.names.split('、').slice(0, 6).join('、')}），地点从（${meta.places.split('、').slice(0, 4).join('、')}）中选一。开场白保持第二人称60~120字画面。任务奖励贴合本阶，不得直接发放（${meta.items.split('、')[0]}）之完整顶点权柄。危险度写清须规避的存在，顶点仅情报优先/条件性胜利。
`;
}

