function count(s) {
  return (s || '').replace(/\s/g, '').length;
}

function write(name, body) {
  const p = path.join(DIR, name);
  fs.writeFileSync(p, body, 'utf8');
  const plot = (body.split('## 剧情')[1] || '').split('## 阶位切入点')[0] || '';
  const entry = (body.split('## 阶位切入点')[1] || '').split('## 来源')[0] || '';
  console.log(`${name}  剧情${count(plot)}  切入${count(entry)}  ${count(plot) >= 10000 && count(entry) >= 1500 ? '✓字数' : '✗短'}`);
}

function padPlot(base, blocks) {
  let s = base;
  for (const b of blocks) {
    if (count((s.split('## 剧情')[1] || '').split('## 阶位切入点')[0] || '') >= 10000) break;
    // inject before 【叙事基调
    s = s.replace('**【叙事基调 · 雷区】**', `${b}\n\n**【叙事基调 · 雷区】**`);
  }
  // if still short, keep adding detail blocks
  let i = 0;
  while (count((s.split('## 剧情')[1] || '').split('## 阶位切入点')[0] || '') < 10000 && i < blocks.length) {
    s = s.replace('**【叙事基调 · 雷区】**', `${blocks[i % blocks.length]}\n\n**【叙事基调 · 雷区】**`);
    i++;
  }
  return s;
}

