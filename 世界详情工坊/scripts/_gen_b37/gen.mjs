爱蜜莉雅阵营以罗兹瓦尔邸为据点，成员含爱蜜莉雅、昴、雷姆、拉姆、碧翠丝、奥托、加菲尔等，初期政治弱势，成长与凝聚力强。库珥修阵营为卡尔斯腾公爵家，威尔海姆、菲利斯等，是白鲸讨伐主力同盟。安娜塔西亚阵营依托卡拉拉基商会，尤里乌斯代表骑士道与利益平衡。普莉希拉·跋利耶尔阵营霸道自律，菲鲁特阵营绑定剑圣。魔女教以大罪司教分掌权能，全民公敌。王国骑士团与近卫是体制武力，莱因哈鲁特为超规格抑止力。圣域混血聚落与艾姬多娜遗产提供试炼与知识。佛拉基亚帝国与九神将在后期主线中成为第二主舞台。各势力之间可临时结盟讨伐魔兽，也可因王选利益反目；契约者站队即站历史。

