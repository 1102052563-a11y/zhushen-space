const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const DIR = path.join(ROOT, '产出', '批次02');

function need(file) {
  const t = fs.readFileSync(path.join(DIR, file), 'utf8');
  return t.slice(t.indexOf('## 剧情'), t.indexOf('## 阶位切入点')).replace(/\s/g, '').length;
}

function add(file, text) {
  const fp = path.join(DIR, file);
  let t = fs.readFileSync(fp, 'utf8');
  const mark = '## 阶位切入点';
  // remove previous 终卷补强 if any
  t = t.replace(/\n\*\*【终卷补强[^*]*】\*\*[\s\S]*?(?=\n## 阶位切入点)/, '\n');
  t = t.replace(mark, text.trim() + '\n\n' + mark);
  fs.writeFileSync(fp, t, 'utf8');
  console.log(file, need(file));
}

