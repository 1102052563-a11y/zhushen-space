for (const file of fs.readdirSync(DIR).filter((f) => f.endsWith('.md'))) {
  const name = file.replace(/\.md$/, '');
  let t = fs.readFileSync(path.join(DIR, file), 'utf8');
  if (t.includes('## 阶位切入点') && !t.includes('阶位↔')) {
    const fix =
      entryFix[name] ||
      '> 阶位↔战力：见正文乐园阶位映射。低阶规避顶点·情报优先。\n\n';
    t = t.replace('## 阶位切入点\n\n', '## 阶位切入点\n\n' + fix);
    t = t.replace('## 阶位切入点\n', '## 阶位切入点\n\n' + fix);
  }
  const list = pads[name] || [];
  let i = 0;
  while (pc(t) < 10000 && i < list.length) {
    t = t.replace(
      '\n## 阶位切入点',
      '\n\n**【档案增补·' + (i + 1) + '】**\n' + list[i] + '\n\n## 阶位切入点'
    );
    i++;
  }
  let n = 0;
  while (pc(t) < 10000 && list.length) {
    const base = list[n % list.length];
    const extra =
      '\n\n**【档案续页·' +
      pc(t) +
      '】**\n在前述基础上补充可观察细节：' +
      base +
      ' 本段编号' +
      n +
      '专供本世界状态机读取。\n';
    t = t.replace('\n## 阶位切入点', extra + '\n## 阶位切入点');
    n++;
    if (n > 50) break;
  }
  fs.writeFileSync(path.join(DIR, file), t, 'utf8');
  const r = spawnSync(process.execPath, [CHECK, '--check', path.join(DIR, file)], {
    encoding: 'utf8',
  });
  process.stdout.write((r.stdout || '') + (r.stderr || ''));
  console.log('[pad]', name, 'plot', pc(t), 'entry', ec(t), 'exit', r.status);
}
