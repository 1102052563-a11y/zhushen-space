function collectNames(w) {
  return w.cast.map((c) => c.name);
}

function assertNoDupNames() {
  const all = new Map();
  for (const w of worlds) {
    for (const n of collectNames(w)) {
      if (all.has(n)) throw new Error(`跨文件重名: ${n} (${all.get(n)} vs ${w.name})`);
      all.set(n, w.name);
    }
  }
}

