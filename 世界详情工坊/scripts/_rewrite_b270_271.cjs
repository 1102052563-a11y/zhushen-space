function resolveFile(preferred, nameHint) {
  if (preferred && fs.existsSync(preferred)) return preferred;
  const dir = path.dirname(preferred);
  if (!fs.existsSync(dir)) return preferred;
  const files = fs.readdirSync(dir);
  // exact name match first
  const byTitle = files.find((f) => f === path.basename(preferred));
  if (byTitle) return path.join(dir, byTitle);
  // fuzzy: starts with key token
  const key = (nameHint || path.basename(preferred)).replace(/\.md$/, '').slice(0, 12);
  const hit = files.find((f) => f.includes(key) || f.startsWith(key.slice(0, 6)));
  if (hit) return path.join(dir, hit);
  return preferred;
}

