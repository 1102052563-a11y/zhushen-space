function densify(tag, seedLines, targetNoWS) {
  const lines = [...seedLines];
  let i = 0;
  const templates = [
    (s, n) => `（${tag}细部${n}）${s} 可观察：表情、对白半句、季节气味与物件声。契约者选项以陪伴、倾听、共同劳动为主，禁止把危机做成刷好感道具。`,
    (s, n) => `（${tag}场景${n}）锚定「${s}」。写清地点＋在场真名＋触发物件；好感变化体现在称呼、距离、是否分享食物。`,
    (s, n) => `（${tag}关系${n}）围绕「${s}」推进一次微事件：相遇／误会／和解／并肩／分别五选一，结局写清情感后果而非数值奖励。`,
    (s, n) => `（${tag}日常${n}）${s} 世界独有符号反复出现形成记忆钉；配角保持原作真名，功能是衬托而非工具人。`,
    (s, n) => `（${tag}氛围${n}）${s} 危机后的第一顿热食比宣言更重要；对白只留半句，剩下用动作补完，避免演讲腔。`,
  ];
  while (lines.join('\n').replace(/\s/g, '').length < targetNoWS) {
    const seed = seedLines[i % seedLines.length];
    const t = templates[i % templates.length];
    lines.push(t(seed.replace(/^[（(].*?[）)]/, '').slice(0, 80), i + 1));
    i++;
    if (i > 400) break;
  }
  return lines.join('\n');
}

