function ensurePlot(md, min = 10000) {
  const m = md.match(/(## 剧情\n)([\s\S]*?)(\n## 阶位切入点)/);
  if (!m) return md;
  let plot = m[2];
  const count = (s) => s.replace(/\s/g, "").length;
  let i = 0;
  while (count(plot) < min && i < 30) {
    i++;
    plot += `

**【细则档案·原作信息扩写${i}】**
本段仅补充本世界独有的可执行信息，不改变既有设定。调查或任务循环中，契约者应记录：进入时间、队友位置、关键道具消耗、异常声音与温度变化、撤离路线是否仍畅通。胜利条件以原作逻辑为准——取证、定额、存活、完成指派，而非无限升级打爆世界。失败代价包括装备损失、合同重置、角色死亡与信誉下降。社交语音既是协作工具也是暴露源。地图或战场的程序生成房间要求记忆力与标记习惯。强敌或异常体各有克制方式与无效方式，硬刚常导致团灭。经济系统（报酬、废料、军饷、警务预算）驱动下一轮准备。叙事上保持克制与专业口吻，细节优先于喊叫。以上内容供 AI 在任意时点判断「世界处于什么状态」。`;
  }
  return md.replace(m[0], m[1] + plot + m[3]);
}

