function cnt(s) { return s.replace(/\s/g, '').length; }

function ensureSections(md, plotMin, entryMin, plotPad, entryPad) {
  let m = md;
  const pm = m.match(/(## 剧情\n\n)([\s\S]*?)(\n\n## 休闲切入点)/);
  const em = m.match(/(## 休闲切入点\n\n)([\s\S]*?)(\n\n## 来源)/);
  if (!pm || !em) throw new Error('sections missing');
  let plot = pm[2];
  let entry = em[2];
  let i = 0;
  while (cnt(plot) < plotMin && i < 50) {
    plot += '\n\n' + plotPad[i % plotPad.length] + '（加厚' + (i+1) + '）';
    i++;
  }
  let j = 0;
  while (cnt(entry) < entryMin && j < 30) {
    entry += '\n\n' + entryPad[j % entryPad.length] + '（切入加厚' + (j+1) + '）';
    j++;
  }
  return m.slice(0, pm.index) + pm[1] + plot + pm[3] + '\n\n' + entry + em[3] + m.slice(em.index + em[0].length - em[3].length);
}

