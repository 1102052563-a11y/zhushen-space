function padUnique(base, extras, min) {
  let t = base;
  let i = 0;
  while (t.replace(/\s/g, '').length < min && i < extras.length) {
    t += '\n\n' + extras[i];
    i++;
  }
  let j = 0;
  while (t.replace(/\s/g, '').length < min && j < 80) {
    const e = extras[j % extras.length];
    t += '\n\n' + e.replace(/^\*\*【/, '**【补叙·' + (j + 1) + '·');
    j++;
  }
  return t;
}

function doc(name, plot, entry, sources, plotExtras, entryExtras) {
  let md = `# ${name}
<!--meta lib=休闲 tiers=休闲-->

## 剧情

${padUnique(plot, plotExtras, 6200)}

## 休闲切入点

${padUnique(entry, entryExtras, 1550)}

## 来源

${sources}
`;
  // leisure ban words soft-replace if slipped
  md = md
    .replace(/力量体系/g, '能力设定（日常侧）')
    .replace(/(?<!禁)战力(?!)/g, '强弱对照')
    .replace(/阶位/g, '档位')
    .replace(/巅峰战力/g, '顶点对决')
    .replace(/危险度/g, '风险氛围');
  return md;
}

