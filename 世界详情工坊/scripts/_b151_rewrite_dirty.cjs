function stats(md) {
  const plot = (md.match(/## 剧情\s*([\s\S]*?)(?=## 阶位切入点)/) || [])[1] || '';
  const entry = (md.match(/## 阶位切入点\s*([\s\S]*?)(?=## 来源)/) || [])[1] || '';
  return {
    plot: plot.replace(/\s/g, '').length,
    entry: entry.replace(/\s/g, '').length,
  };
}

function stripFiller(md) {
  const lines = md.split(/\r?\n/);
  return lines.filter((l) => !l.includes('补充史实纹理：')).join('\n');
}

function insertBeforeTone(md, blocks) {
  const mark = '**【叙事基调 · 雷区】**';
  if (!md.includes(mark)) throw new Error('missing tone mark');
  return md.replace(mark, blocks.join('\n\n') + '\n\n' + mark);
}

function check(file) {
  const p = path.join(OUT, file);
  const r = spawnSync(process.execPath, [path.join(__dirname, 'compile-worldbook.mjs'), '--check', p], {
    encoding: 'utf8',
  });
  process.stdout.write(r.stdout || '');
  process.stderr.write(r.stderr || '');
  if (r.status !== 0) throw new Error('check fail: ' + file);
  const t = fs.readFileSync(p, 'utf8');
  if (t.includes('补充史实纹理')) throw new Error('still has filler: ' + file);
  return stats(t);
}

