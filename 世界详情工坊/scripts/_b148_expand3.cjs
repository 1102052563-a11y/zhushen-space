function injectPlot(file, text) {
  const full = path.join(dir, file);
  let t = fs.readFileSync(full, 'utf8').replace(/\r\n/g, '\n');
  const marker = '\n## 阶位切入点\n';
  const i = t.indexOf(marker);
  t = t.slice(0, i) + '\n\n' + text + '\n' + t.slice(i);
  fs.writeFileSync(full, t);
}

