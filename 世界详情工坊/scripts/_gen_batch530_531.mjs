const charCount = (s) => (s || '').replace(/\s/g, '').length;

function expand(base, min, blocks) {
  let t = base;
  let i = 0;
  const fb = (blocks && blocks.length) ? blocks : [
    `**【补述档案{{n}}】**本世界叙事强调具体人名、地名、规则因果与可观察细节；契约者切入须服从原作物理与社会逻辑，禁止无代价越阶与跨世界套话。关键冲突应落到场景、道具与对话选择，而非空泛情感形容词堆砌。`,
  ];
  while (charCount(t) < min) {
    t += '\n\n' + fb[i % fb.length].replace(/\{\{n\}\}/g, String(i + 1));
    i++;
    if (i > 200) break;
  }
  return t;
}

function entryExpand(base, min, blocks) {
  return expand(base, min, blocks);
}

function writeWorld(dir, name, tiers, plot, entry, sources) {
  const pText = typeof plot === 'string' ? plot : plot.text;
  const pBlocks = typeof plot === 'string' ? [] : (plot.blocks || []);
  const eText = typeof entry === 'string' ? entry : entry.text;
  const eBlocks = typeof entry === 'string' ? [] : (entry.blocks || []);
  const p = expand(pText, 10050, pBlocks);
  const e = expand(eText, 1550, eBlocks);
  const body = `# ${name}
<!--meta lib=主库 tiers=${tiers}-->

## 剧情

${p}

## 阶位切入点

${e}

## 来源

${sources}
`;
  const fp = path.join(dir, `${name}.md`);
  fs.writeFileSync(fp, body, 'utf8');
  console.log('WROTE', name, 'plot', charCount(p), 'entry', charCount(e));
  return fp;
}

