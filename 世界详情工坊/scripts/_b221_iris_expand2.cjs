function expandText(blocks, minExtra) {
  let out = "\n";
  let i = 0;
  while (cc(out) < minExtra) {
    const b = blocks[i % blocks.length];
    out += "\n" + b + " 在本分支时间线上，该节点表现为可观察制度与战术细节：谁下令、谁执行、谁记录、谁受益、谁承担伤亡。契约者应先改记录与链路，再改火力。名场面须含至少一个真名角色、一个地点、一个不可逆选择；提前或延后触发会改写项圈/联锁/融合度进度与敌方准备度。细节层" + (i + 1) + "：" + b + "\n";
    i++;
    if (i > 80) break;
  }
  return out;
}

