关键NPC立场：**封不觉（疯不觉）**——同期或邻队的怪人，可能嘲讽你的操作，也可能随口点破一条你忽略的规则，利益一致时是最强大脑；**悲灵**——相对好沟通的玩家，愿组队但讨厌被卖；**剧本护士长/院长（本内BOSS）**——规则杀核心，别用凡人逻辑硬刚；**大厅装备商NPC**——只认游戏币；**黑心队友（随机玩家）**——会把你当诱饵。

主线钩子/支线：通关首个完整剧本并安全结算；支线＝捡漏技能书、识破假攻略、在结算前抢隐藏成就、避免被系统标记异常行为（除非你想走疯不觉路线）。

