const fs = require("fs");

function charCount(s) {
  return (s || "").replace(/\s/g, "").length;
}
function stats(t) {
  const plot = t.split("## 休闲切入点")[0];
  const entry = (t.split("## 休闲切入点")[1] || "").split("## 来源")[0];
  return { plot: charCount(plot), entry: charCount(entry) };
}
function padFile(file, plotBlock, entryBlock) {
  let t = fs.readFileSync(file, "utf8");
  // insert before ## 休闲切入点
  if (!t.includes("## 休闲切入点")) throw new Error("no entry " + file);
  t = t.replace("\n## 休闲切入点", "\n" + plotBlock + "\n\n## 休闲切入点");
  // insert before ## 来源
  t = t.replace("\n## 来源", "\n" + entryBlock + "\n\n## 来源");
  // loop if still short
  let s = stats(t);
  let i = 0;
  while ((s.plot < 6100 || s.entry < 1550) && i < 5) {
    if (s.plot < 6100) {
      t = t.replace("\n## 休闲切入点", "\n\n" + plotBlock + "\n\n## 休闲切入点");
    }
    if (s.entry < 1550) {
      t = t.replace("\n## 来源", "\n\n" + entryBlock + "\n\n## 来源");
    }
    s = stats(t);
    i++;
  }
  fs.writeFileSync(file, t);
  console.log(file, s, "loops", i);
}

