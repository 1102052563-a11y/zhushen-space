const wc = s => (s||"").replace(/\s/g, "").length;

function padTo(text, min, blocks) {
  let t = text;
  let i = 0;
  while (wc(t) < min) {
    t += "\n\n" + blocks[i % blocks.length];
    i++;
    if (i > 40) break;
  }
  return t;
}

