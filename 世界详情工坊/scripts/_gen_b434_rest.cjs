function check(file) {
  const r = spawnSync('node', [checkScript, '--check', file], { encoding: 'utf8' });
  process.stdout.write(r.stdout || '');
  process.stderr.write(r.stderr || '');
  return (r.stdout || '').includes('✓') || (r.stdout || '').includes('过关');
}

