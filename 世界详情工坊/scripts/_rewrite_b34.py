from pathlib import Path
import re

OUT = Path(__file__).resolve().parent.parent / "产出" / "批次34"

def cc(s: str) -> int:
    return len(re.sub(r"\s+", "", s or ""))

def write(name: str, body: str):
    p = OUT / name
    p.write_text(body, encoding="utf-8")
    parts = body.split("## 剧情")
    rest = parts[1] if len(parts) > 1 else ""
    plot = rest.split("## 阶位切入点")[0]
    ent_rest = rest.split("## 阶位切入点")[1] if "## 阶位切入点" in rest else ""
    entry = ent_rest.split("## 来源")[0]
    src = body.split("## 来源")[-1] if "## 来源" in body else ""
    links = len(re.findall(r"\]\(https?://[^)]+\)", src))
    ok = cc(plot) >= 10000 and cc(entry) >= 1500 and links >= 3
    print(("OK" if ok else "FAIL"), name, "plot", cc(plot), "entry", cc(entry), "links", links)

