function charCount(s) {
  return (s || '').replace(/\s/g, '').length;
}

function pack(name, tiers, plot, entry, sources) {
  // densify plot uniquely if short
  let p = plot;
  let i = 0;
  const plotBlocks = densifyBlocks(name);
  while (charCount(p) < 10050 && i < plotBlocks.length) {
    p += '\n\n' + plotBlocks[i++];
  }
  if (charCount(p) < 10000) throw new Error(name + ' plot still short: ' + charCount(p));
  let e = entry;
  const ed = entryDensify(name);
  let j = 0;
  while (charCount(e) < 1550 && j < ed.length) {
    e += '\n\n' + ed[j++];
  }
  if (charCount(e) < 1500) throw new Error(name + ' entry still short: ' + charCount(e));
  const body = `# ${name}
<!--meta lib=主库 tiers=${tiers}-->

## 剧情

${p}

## 阶位切入点

${e}

## 来源

${sources.map((u) => `- ${u}`).join('\n')}
`;
  const fp = path.join(outDir, name + '.md');
  fs.writeFileSync(fp, body, 'utf8');
  return { name, plot: charCount(p), entry: charCount(e), total: body.length };
}

function densifyBlocks(name) {
  const map = {
    '黑兽魔王觉醒-大陆统一': dens.densUnify,
    '黑兽魔王觉醒-女神堕落': dens.densGoddess,
    '黑兽魔王觉醒-沃尔特进化': dens.densVolt,
    '魔界骑士深渊大门-大门开启': dens.densGate,
    '魔界骑士深渊大门-英格丽德深渊化': dens.densAbyssIngrid,
  };
  return map[name]();
}

function entryDensify(name) {
  return entryDensifyExt(name);
}

