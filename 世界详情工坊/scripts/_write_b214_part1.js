const path = require('path');
const OUT = path.join(__dirname, '..', '产出', '批次214');
function cc(s){return s.replace(/\s/g,'').length}
function stats(t){
  const plot=(t.split('## 剧情')[1]||'').split(/## 阶位切入点/)[0]||'';
  const entry=(t.split('## 阶位切入点')[1]||'').split(/## 来源/)[0]||'';
  return {plot:cc(plot),entry:cc(entry)};
}
function write(name, body){
  const p=path.join(OUT,name);
  fs.writeFileSync(p, body.replace(/\r\n/g,'\n'), 'utf8');
  const s=stats(body);
  const ok=s.plot>=10000 && s.entry>=1500;
  console.log(name, '剧情', s.plot, '切入', s.entry, ok?'✓':'✗');
  return ok;
}

