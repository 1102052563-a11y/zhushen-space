function padUnique(base, extras, min) {
  let t = base;
  let i = 0;
  while (t.replace(/\s/g, '').length < min && i < extras.length) {
    t += '\n\n' + extras[i];
    i++;
  }
  let j = 0;
  while (t.replace(/\s/g, '').length < min && j < 50) {
    const e = extras[j % extras.length];
    const tag = '再叙·' + (j + 1) + '·';
    t += '\n\n' + e.replace(/^\*\*【/, '**【' + tag);
    j++;
  }
  return t;
}

