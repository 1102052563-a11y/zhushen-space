const BAN = /力量体系|战力|阶位|巅峰战力|危险度|贵重法宝/g;
const noWs = (s) => (s || '').replace(/\s/g, '').length;

function padTo(base, extras, min) {
  let t = base;
  let i = 0;
  while (noWs(t) < min && i < extras.length) t += '\n\n' + extras[i++];
  let j = 0;
  while (noWs(t) < min && j < 40) {
    const e = extras[j % Math.max(extras.length, 1)] || '日常补记：校园走廊的光、社团室的灰尘、傍晚归路的脚步，都是关系升温的刻度。';
    t += '\n\n' + e + '（补记' + (j + 1) + '）';
    j++;
  }
  return t;
}

function doc(name, plot, entry, sources, plotX, entryX) {
  const p = padTo(plot, plotX, 6200);
  const e = padTo(entry, entryX, 1550);
  const body = `# ${name}
<!--meta lib=休闲 tiers=休闲-->

## 剧情

${p}

## 休闲切入点

${e}

## 来源

${sources}
`;
  if (BAN.test(body)) throw new Error(name + ' banned: ' + body.match(BAN));
  const fp = path.join(DIR, name + '.md');
  fs.writeFileSync(fp, body, 'utf8');
  console.log('WROTE', name, 'plot', noWs(p), 'entry', noWs(e));
  return fp;
}

