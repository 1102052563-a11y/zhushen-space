const fs = require('fs');
const path = require('path');
const dir = path.join(__dirname, '..', '产出', '批次148');

function count(s) {
  return (s || '').replace(/\s/g, '').length;
}

function injectPlot(file, blocks) {
  const full = path.join(dir, file);
  let t = fs.readFileSync(full, 'utf8').replace(/\r\n/g, '\n');
  const marker = '\n## 阶位切入点\n';
  const i = t.indexOf(marker);
  if (i < 0) throw new Error('no entry ' + file);
  const add = '\n\n' + blocks.join('\n\n') + '\n';
  t = t.slice(0, i) + add + t.slice(i);
  fs.writeFileSync(full, t);
}

function injectEntry(file, blocks) {
  const full = path.join(dir, file);
  let t = fs.readFileSync(full, 'utf8').replace(/\r\n/g, '\n');
  const marker = '\n## 来源\n';
  const i = t.indexOf(marker);
  if (i < 0) throw new Error('no src ' + file);
  const add = '\n\n' + blocks.join('\n\n') + '\n';
  t = t.slice(0, i) + add + t.slice(i);
  fs.writeFileSync(full, t);
}

