① **第一夜**  
契约者生成于某生物群系，用拳与木工具砍树挖石，在日落前挖洞或搭屋。僵尸敲门、骷髅射击、爬行者的嘶嘶声定义「生存恐惧」。床跳过夜，设置重生点。

