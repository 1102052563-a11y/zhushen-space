const strip = (s) => (s || "").replace(/\s/g, "").length;
function stats(c) {
  const plot = (c.split("## 剧情")[1] || "").split("## 阶位切入点")[0] || "";
  const entry = (c.split("## 阶位切入点")[1] || "").split("## 来源")[0] || "";
  return { plot: strip(plot), entry: strip(entry) };
}
function ensurePlot(base, pads) {
  let c = base;
  let i = 0;
  while (stats(c).plot < 10000 && i < pads.length) {
    c = c.replace("## 阶位切入点", "\n" + pads[i] + "\n## 阶位切入点");
    i++;
  }
  let guard = 0;
  while (stats(c).plot < 10000 && guard < 40) {
    const n = stats(c).plot;
    const tag = base.includes("劳拉") ? "罗根" : "和真";
    const extra = `\n**【原作信息增密·${n}】**\n本段只追加可核对的情节、人物真名、地点与因果，禁止套话。若本世界为金刚狼3：保留罗根、查尔斯·泽维尔、劳拉·金尼、唐纳德·皮尔斯、桑德·莱斯、卡利班、嘉布拉、X-24、威尔·孟森、伊甸园、碱湖基改、掠夺者、艾德曼子弹、血清。若本世界为美好世界：保留佐藤和真、阿克娅、惠惠、达克妮丝、芸芸、维兹、巴尼尔、毁灭者、阿克塞尔、红魔之里、爆裂魔法、神圣魔法。契约者切入须能判断锚点战役是否已爆发、谁还活着、谁已站队。\n`;
    c = c.replace("## 阶位切入点", extra + "\n## 阶位切入点");
    if (stats(c).plot <= n) break;
    guard++;
  }
  return c;
}
