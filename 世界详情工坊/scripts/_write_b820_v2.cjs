const cnt = (s) => (s || '').replace(/\s/g, '').length;

function wrap(name, plot, entry, sources) {
  return `# ${name}
<!--meta lib=休闲 tiers=休闲-->

## 剧情

${plot}

## 休闲切入点

${entry}

## 来源

${sources}
`;
}

function thicken(base, extraBlocks, min) {
  let s = base;
  let i = 0;
  while (cnt(s) < min && i < extraBlocks.length) {
    s += '\n\n' + extraBlocks[i++];
  }
  while (cnt(s) < min) {
    // last resort: unique-ish factual expansion from cycling extras with index
    const b = extraBlocks[i % extraBlocks.length];
    s += '\n\n' + b.replace(/。/g, `（续${i}）。`);
    i++;
    if (i > 40) break;
  }
  return s;
}

