import fs from 'fs';

function fill(text, min) {
  let t = text;
  let i = 0;
  while (t.replace(/\s/g, '').length < min) {
    i++;
    t += `\n\n**【可玩细目·${i}】** 本档场景须落在具体地点与真名人物上：谁要什么、抢什么、谁先动手、伤亡与政治后果如何锁死后续。禁止空套话与跨作品复制填充。训练／对战／规则／生态是力量来源，不是灵气境界。事件${i}可选护送补给、情报交换、违规鉴定、直播事故、生态冲突、组织渗透。人物调用优先已列真名。奖励贴合当前阶位，禁止越级发放灭世权柄。`;
    if (i > 40) break;
  }
  return t;
}

function writeWorld({ file, title, tiers, plot, entry, sources }) {
  plot = fill(plot, 10100);
  entry = fill(entry, 1550);
  const md = `# ${title}
<!--meta lib=主库 tiers=${tiers}-->

## 剧情

${plot}

## 阶位切入点

${entry}

## 来源

${sources}
`;
  fs.writeFileSync(file, md);
  console.log(title, 'plot', plot.replace(/\s/g,'').length, 'entry', entry.replace(/\s/g,'').length);
}

