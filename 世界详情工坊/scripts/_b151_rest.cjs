阿尔卡冯的宝库不讲述阿尔萨斯坠落，也不解决尤格萨隆。它是战争机器上的齿轮：资源、装等、荣誉、炫耀。写正文时优先载具轰鸣、城墙号角、巨人踏步扬起的霜尘，而非强行插入巫妖王独白。

