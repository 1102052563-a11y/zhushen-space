function stats(md) {
  const plot = (md.match(/## 剧情\s*([\s\S]*?)(?=## 阶位切入点)/) || [])[1] || '';
  const entry = (md.match(/## 阶位切入点\s*([\s\S]*?)(?=## 来源)/) || [])[1] || '';
  return {
    plot: plot.replace(/\s/g, '').length,
    entry: entry.replace(/\s/g, '').length,
  };
}

function padPlot(md, blocks) {
  let s = stats(md);
  let i = 0;
  while (s.plot < 10000 && i < blocks.length) {
    md = md.replace('**【叙事基调 · 雷区】**', blocks[i] + '\n**【叙事基调 · 雷区】**');
    s = stats(md);
    i++;
  }
  while (s.plot < 10000) {
    const filler =
      '补充史实纹理：地名、人名、机关名、战利品层级与可观察气味／光／通讯噪声须继续钉死在原作公开资料上，禁止跨世界套话。契约者局部改写以追杀／取代／更惨方式回流。';
    md = md.replace('**【叙事基调 · 雷区】**', filler + '\n**【叙事基调 · 雷区】**');
    s = stats(md);
    if (filler.length < 10) break;
  }
  return md;
}

