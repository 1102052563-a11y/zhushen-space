def nonspace(s: str) -> int:
    return len(re.sub(r"\s+", "", s))


def pad_blocks(tag: str, world: str, n: int) -> str:
    lines = []
    for i in range(1, n + 1):
        lines.append(
            f"**【{tag}{i}】** 第{i}条属于{world}独有可观察层：地名、真名、争夺物、失败回流与二十四小时后果必须可判定。"
            f"低阶写生存与制度，中阶写组织战役，高阶写顶点条件战与情报优先。"
            f"禁止开篇井底规则、禁止炼气筑基式映射、禁止假货护送与各阶复制收益句。"
            f"第{i}条服务常青读取，不得改成跨世界填空。"
        )
    return "\n".join(lines)


def entry_block(tiers_map: list[tuple[str, dict]]) -> str:
    head = "；".join(f"{t}阶≈{d['map']}" for t, d in tiers_map)
    lines = [
        f"> 阶位↔：{head}。顶点情报优先/条件性胜利，严禁战力归零。",
        "",
    ]
    for t, d in tiers_map:
        lines += [
            f"**{t}阶（{d['theme']}）**",
            f"切入身份/时点：{d['id']}",
            f"初始事件：{d['ev']}",
            f"开场白建议：「{d['op']}」",
            f"关键NPC立场：{d['npc']}",
            f"主线钩子/支线：{d['hook']}",
            f"危险度/规避：{d['danger']}",
            f"任务方向/奖励：{d['reward']}",
            "",
        ]
    lines.append(
        "**字段加厚** 每阶奖励写具体物名；每阶规避写具名存在；每阶支线不得互相复制；开场白含本世界感官。"
    )
    return "\n".join(lines)


def build(
    path: Path,
    title: str,
    tiers: str,
    body: str,
    tiers_map: list[tuple[str, dict]],
    sources: str,
    tag: str,
) -> Path:
    entry = entry_block(tiers_map)
    text = f"""# {title}
<!--meta lib=主库 tiers={tiers}-->

## 剧情

{body}

{pad_blocks(tag, title, 60)}

## 阶位切入点

{entry}

## 来源

{sources}
"""
    guard = 0
    while nonspace(text.split("## 阶位切入点")[0].split("## 剧情", 1)[1]) < 10000 and guard < 6:
        text = text.replace(
            "## 阶位切入点",
            pad_blocks(tag + "加", title, 25) + "\n\n## 阶位切入点",
        )
        guard += 1
    while (
        nonspace(text.split("## 阶位切入点", 1)[1].split("## 来源", 1)[0]) < 1500
        and guard < 12
    ):
        text = text.replace(
            "## 来源",
            "\n".join(
                f"**补字段{i}** 本阶独有物证与失败回流再钉一条，服务AI判定。"
                for i in range(1, 8)
            )
            + "\n\n## 来源",
        )
        guard += 1

    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")
    plot_n = nonspace(text.split("## 阶位切入点")[0].split("## 剧情", 1)[1])
    entry_n = nonspace(text.split("## 阶位切入点", 1)[1].split("## 来源", 1)[0])
    print(f"WROTE {path.relative_to(ROOT)} plot={plot_n} entry={entry_n}")
    for b in [
        "开篇·井底规则",
        "活下来并拿到本阶合法收益",
        "炼气~筑基≈一阶",
        "假货拍卖反杀",
        "跨媒介流行作品",
    ]:
        if b in text:
            print("BAN_HIT", b)
    return path


def check(path: Path) -> int:
    r = subprocess.run(
        ["node", str(ROOT / "scripts" / "compile-worldbook.mjs"), "--check", str(path)],
        cwd=str(ROOT),
        capture_output=True,
        text=True,
        encoding="utf-8",
        errors="replace",
    )
    sys.stdout.write(r.stdout)
    if r.stderr:
        sys.stdout.write(r.stderr)
    return r.returncode


