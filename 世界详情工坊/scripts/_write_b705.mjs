const charCount = (s) => s.replace(/\s/g, '').length;

function ensureMin(body, min, fillerBlocks) {
  let t = body;
  let i = 0;
  while (charCount(t) < min && i < fillerBlocks.length) {
    t += '\n\n' + fillerBlocks[i++];
  }
  while (charCount(t) < min) {
    t += '\n\n' + fillerBlocks[fillerBlocks.length - 1];
  }
  return t;
}

