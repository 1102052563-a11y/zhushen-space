const files = [];
for (const d of ['产出/批次253', '产出/批次254']) {
  for (const f of fs.readdirSync(path.join(ROOT, d)).filter((x) => x.endsWith('.md'))) {
    files.push(path.join(ROOT, d, f));
  }
}

for (const fp of files) {
  let t = fs.readFileSync(fp, 'utf8');
  const name = t.match(/^#\s+(.+)$/m)[1].trim();
  const boosts = uniqueBoost[name] || [];
  let n = 0;
  while (plotChars(t) < 10050 && n < 80) {
    n++;
    const b = boosts[(n - 1) % Math.max(boosts.length, 1)] || `${name}阶段推进要点${n}`;
    const block =
      `\n\n**【${name}·实操${n}】**\n` +
      `${b} 执行时记录：地点、在场真名、物证编号、时限、失败态。本条仅服务「${name}」锚点，不与其他条目复用句式。` +
      `三阶奖励锁：通行/医疗/信任/残片；禁止终局权柄。顶点仅情报与条件性胜利。`;
    t = t.replace('\n## 阶位切入点', block + '\n\n## 阶位切入点');
  }
  n = 0;
  while (entryChars(t) < 1520 && n < 30) {
    n++;
    const block =
      `\n\n**三阶·${name}钩${n}**\n` +
      `独有：用实操${n}的物证做一次交换或潜入；加粗真名NPC；报酬与出卖风险分列；失败死亡率中高；规避顶点。`;
    t = t.replace('\n## 来源', block + '\n\n## 来源');
  }
  fs.writeFileSync(fp, t, 'utf8');
  console.log(name, '剧情', plotChars(t), '切入', entryChars(t));
}

console.log('--- machine check ---');
