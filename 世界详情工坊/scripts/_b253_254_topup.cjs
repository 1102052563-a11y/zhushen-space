function plotChars(text) {
  const m = text.match(/## 剧情\s*\n([\s\S]*?)\n## 阶位切入点/);
  if (!m) return 0;
  return m[1].replace(/\s/g, '').length;
}
function entryChars(text) {
  const m = text.match(/## 阶位切入点\s*\n([\s\S]*?)\n## 来源/);
  if (!m) return 0;
  return m[1].replace(/\s/g, '').length;
}

