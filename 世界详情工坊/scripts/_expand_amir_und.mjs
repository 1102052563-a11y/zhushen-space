import fs from 'fs';

function expand(file, plotBlock, entryBlock) {
  let t = fs.readFileSync(file, 'utf8');
  if (!t.includes('【扩写块A】')) {
    t = t.replace('## 阶位切入点', plotBlock + '\n## 阶位切入点');
  }
  if (!t.includes('【切入扩写】')) {
    t = t.replace('\n## 来源', entryBlock + '\n## 来源');
  }
  fs.writeFileSync(file, t);
  const plot = t.split('## 阶位切入点')[0].replace(/\s/g, '').length;
  const ent = (t.split('## 阶位切入点')[1] || '').split('## 来源')[0].replace(/\s/g, '').length;
  console.log(file, 'plot', plot, 'entry', ent);
}

