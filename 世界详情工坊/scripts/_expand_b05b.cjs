const fs = require('fs');

function inject(file, more) {
  let t = fs.readFileSync(file, 'utf8');
  const marker = '## 阶位切入点';
  if (!t.includes(marker)) throw new Error('no entry ' + file);
  t = t.replace(marker, more + '\n\n' + marker);
  fs.writeFileSync(file, t, 'utf8');
  const plot = t.split('## 阶位切入点')[0].replace(/\s/g, '').length;
  const entry = (t.split('## 阶位切入点')[1] || '').split('## 来源')[0].replace(/\s/g, '').length;
  console.log(file, 'plot', plot, 'entry', entry);
}

