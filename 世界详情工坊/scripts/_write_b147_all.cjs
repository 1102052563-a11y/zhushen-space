const OUT = path.join(__dirname, '..', '产出', '批次147');

function stripWs(s) {
  return s.replace(/\s/g, '');
}

function assertPlot(name, plot, entry) {
  const pn = stripWs(plot).length;
  const en = stripWs(entry).length;
  if (pn < 10000) throw new Error(`${name} 剧情不足: ${pn}`);
  if (en < 1500) throw new Error(`${name} 切入点不足: ${en}`);
  console.log(`OK draft ${name} plot=${pn} entry=${en}`);
}

function writeWorld(name, metaTiers, plot, entry, sources) {
  assertPlot(name, plot, entry);
  const body = `# ${name}
<!--meta lib=主库 tiers=${metaTiers}-->

## 剧情

${plot.trim()}

## 阶位切入点

${entry.trim()}

## 来源

${sources.map((u) => {
    const title = u.replace(/^https?:\/\//, '').slice(0, 60);
    return `- [${title}](${u})`;
  }).join('\n')}
`;
  const fp = path.join(OUT, `${name}.md`);
  fs.writeFileSync(fp, body, 'utf8');
  console.log('WROTE', fp, 'bytes', Buffer.byteLength(body, 'utf8'));
}

