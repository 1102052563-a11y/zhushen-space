function countChars(s) {
  return s.replace(/\s/g, '').length;
}

function extractSection(md, h) {
  const re = new RegExp(`## ${h}\\s*([\\s\\S]*?)(?=\\n## |$)`);
  const m = md.match(re);
  return m ? m[1] : '';
}

function checkNamesUnique(worlds) {
  const all = new Map();
  const dups = [];
  for (const w of worlds) {
    for (const c of w.cast) {
      if (all.has(c.name)) dups.push(`${c.name} in ${w.name} & ${all.get(c.name)}`);
      else all.set(c.name, w.name);
    }
  }
  return dups;
}

function localCheck(md, name) {
  const errs = [];
  if (!md.includes('<!--meta lib=休闲 tiers=休闲-->')) errs.push('meta missing');
  if (!md.includes('## 剧情')) errs.push('no 剧情');
  if (!md.includes('## 休闲切入点')) errs.push('no 休闲切入点');
  if (!md.includes('## 来源')) errs.push('no 来源');
  const plot = extractSection(md, '剧情');
  const entry = extractSection(md, '休闲切入点');
  const pc = countChars(plot);
  const ec = countChars(entry);
  if (pc < 6000) errs.push(`剧情 ${pc}<6000`);
  if (ec < 1500) errs.push(`切入 ${ec}<1500`);
  const required = [
    '【作品来源】', '【世界定位】', '【世界观 · 舞台设定】', '【地理 · 生活舞台】',
    '【故事主线 · 情感线】', '【可攻略角色 / 主要人物】', '【人际关系网 / 社团势力】',
    '【情感事件 · 名场面】', '【隐藏剧情 · 真结局 · 伏笔】', '【氛围基调 · 雷区】',
  ];
  for (const r of required) if (!plot.includes(r)) errs.push(`缺段 ${r}`);
  for (const b of BANNED) if (md.includes(b)) errs.push(`禁词 ${b}`);
  const combat = ['力量体系', '战力', '阶位', '巅峰战力', '危险度'];
  for (const c of combat) if (md.includes(c)) errs.push(`战斗词 ${c}`);
  const sources = (md.match(/^- \[/gm) || []).length;
  if (sources < 3) errs.push(`来源 ${sources}<3`);
  return { errs, pc, ec };
}

