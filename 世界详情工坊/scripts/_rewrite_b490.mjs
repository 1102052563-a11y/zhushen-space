function assertUniqueNames() {
  const map = new Map();
  for (const w of worlds) {
    for (const c of w.cast) {
      if (map.has(c.name)) {
        throw new Error(`人名跨文件重复: ${c.name} 已在 ${map.get(c.name)}，又出现在 ${w.name}`);
      }
      map.set(c.name, w.name);
    }
  }
  return map.size;
}

function writeAll() {
  fs.mkdirSync(OUT, { recursive: true });
  const n = assertUniqueNames();
  console.log('unique cast names:', n);
  for (const w of worlds) {
    const body = buildFile(w);
    for (const b of BANNED) {
      if (body.includes(b)) throw new Error(`${w.name} 命中禁句: ${b}`);
    }
    const fp = path.join(OUT, w.file);
    fs.writeFileSync(fp, body, 'utf8');
    console.log('wrote', w.file, 'chars≈', body.replace(/\s/g, '').length);
  }
}

