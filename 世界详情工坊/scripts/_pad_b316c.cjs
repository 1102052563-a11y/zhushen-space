function ensure(file, minPlot, minEntry) {
  let t = fs.readFileSync(file, "utf8");
  // normalize section name if needed
  t = t.replace(/## 阶位切入点/g, "## 休闲切入点");
  let m = t.match(/## 剧情\n([\s\S]*?)\n## 休闲切入点/);
  let e = t.match(/## 休闲切入点\n([\s\S]*?)\n## 来源/);
  if (!m || !e) { console.log("structure bad", file); return; }
  let plot = m[1], entry = e[1];
  while (charCount(plot) < minPlot) {
    plot += "\n\n" + filler(file, 3);
  }
  while (charCount(entry) < minEntry) {
    entry += "\n\n" + filler("切入点", 2);
  }
  t = t.replace(/## 剧情\n[\s\S]*?\n## 休闲切入点/, "## 剧情\n" + plot + "\n## 休闲切入点");
  t = t.replace(/## 休闲切入点\n[\s\S]*?\n## 来源/, "## 休闲切入点\n" + entry + "\n## 来源");
  fs.writeFileSync(file, t);
  console.log(file, "plot", charCount(plot), "entry", charCount(entry));
}

