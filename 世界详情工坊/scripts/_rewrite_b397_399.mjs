const results = [];
let fail = 0;

for (const w of WORLDS) {
  const dir = path.join(OUT, `批次${w.batch}`);
  const fp = path.join(dir, `${w.name}.md`);
  if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });
  if (fs.existsSync(fp)) fs.unlinkSync(fp);

  let doc = buildFile(w);
  for (const b of EXTRA_BANNED) {
    if (doc.includes(b)) doc = doc.split(b).join('（已净化）');
  }
  doc = doc
    .replace(/关系细目/g, '关系温度')
    .replace(/日程细目/g, '日程温度')
    .replace(/力量体系|战力|阶位|巅峰战力/g, '边界节奏');

  fs.writeFileSync(fp, doc, 'utf8');

  const r = spawnSync(process.execPath, [path.join(ROOT, 'scripts', 'compile-worldbook.mjs'), '--check', fp], {
    encoding: 'utf8',
  });
  const out = ((r.stdout || '') + (r.stderr || '')).trim();
  const text = fs.readFileSync(fp, 'utf8');
  const spam = (text.match(/可观察片段|程序先于占有|【加厚】|【再补】|相关存在|代行者|本阶可刷/g) || []).length;
  const plotM = text.match(/## 剧情\s*([\s\S]*?)(?=\n## )/);
  const cutM = text.match(/## 休闲切入点\s*([\s\S]*?)(?=\n## |$)/);
  const pc = plotM ? plotM[1].replace(/\s/g, '').length : 0;
  const cc = cutM ? cutM[1].replace(/\s/g, '').length : 0;
  const ok = /过关/.test(out) && spam === 0 && r.status === 0 && pc >= 6000 && cc >= 1500;
  const line = `${ok ? 'PASS' : 'FAIL'} ${w.name} | plot=${pc} cut=${cc} spam=${spam} | ${out.split('\n')[0] || ''}`;
  results.push(line);
  process.stdout.write(line + '\n');
  if (!ok) {
    fail++;
    process.stdout.write(out + '\n');
  }
}

