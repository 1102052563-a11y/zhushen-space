function wc(s) { return (s || '').replace(/\s/g, '').length; }

function build(name, tiers, plot, entry, sources) {
  const body = [
    `# ${name}`,
    `<!--meta lib=主库 tiers=${tiers}-->`,
    '',
    '## 剧情',
    '',
    plot.trim(),
    '',
    '## 阶位切入点',
    '',
    entry.trim(),
    '',
    '## 来源',
    '',
    sources.trim(),
    '',
  ].join('\n');
  const fp = path.join(OUT, `${name}.md`);
  fs.writeFileSync(fp, body, 'utf8');
  const p = wc(plot), e = wc(entry), t = wc(body);
  console.log(`${name} 剧情${p} 切入${e} 总${t} ${p >= 10000 && e >= 1500 ? 'OK' : 'FAIL'}`);
  return { name, p, e, ok: p >= 10000 && e >= 1500 };
}

