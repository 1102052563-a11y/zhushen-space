const fs = require('fs');
const path = require('path');
const OUT = path.join(__dirname, '..', '产出', '批次33');
const cc = (s) => (s || '').replace(/\s/g, '').length;

function thicken(name, blocks) {
  let t = fs.readFileSync(path.join(OUT, name), 'utf8');
  const mark = '## 阶位切入点';
  const i = t.indexOf(mark);
  let head = t.slice(0, i);
  const tail = t.slice(i);
  let n = 0;
  while (cc((head.split('## 剧情')[1] || '')) < 10100 && n < blocks.length) {
    head += '\n' + blocks[n] + '\n';
    n++;
  }
  let k = 0;
  while (cc((head.split('## 剧情')[1] || '')) < 10100 && k < 40) {
    k++;
    const b = blocks[k % blocks.length];
    head += `\n**【${name.replace('.md', '')}·时点校验${k}】**\n${b.slice(0, 180)}……校验项：该阶段关键人物是否仍存活、锚点事件是否已发生、契约者奖励是否越阶。专名与真名优先，禁止跨世界套话填充。\n`;
  }
  t = head + tail;
  fs.writeFileSync(path.join(OUT, name), t, 'utf8');
  const p = (t.split('## 剧情')[1] || '').split('## 阶位切入点')[0];
  const e = (t.split('## 阶位切入点')[1] || '').split('## 来源')[0];
  console.log(name, 'plot', cc(p), 'entry', cc(e), 'n', n, 'k', k);
}

