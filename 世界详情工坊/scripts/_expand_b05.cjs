function expand(file, blocks) {
  let t = fs.readFileSync(file, 'utf8');
  const marker = '**【叙事基调 · 雷区】**';
  if (!t.includes(marker)) throw new Error('no marker ' + file);
  // insert before 叙事基调
  t = t.replace(marker, blocks + '\n\n' + marker);
  fs.writeFileSync(file, t, 'utf8');
  const plot = t.split('## 阶位切入点')[0].replace(/\s/g, '').length;
  const entry = (t.split('## 阶位切入点')[1] || '').split('## 来源')[0].replace(/\s/g, '').length;
  console.log(path.basename(file), 'plot', plot, 'entry', entry);
}

