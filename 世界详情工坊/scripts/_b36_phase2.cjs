const strip = (s) => (s || "").replace(/\s/g, "").length;
function stats(c) {
  const plot = (c.split("## 剧情")[1] || "").split("## 阶位切入点")[0] || "";
  const entry = (c.split("## 阶位切入点")[1] || "").split("## 来源")[0] || "";
  return { plot: strip(plot), entry: strip(entry) };
}
function ensurePlot(base, pads) {
  let c = base;
  let i = 0;
  while (stats(c).plot < 10000 && i < pads.length) {
    c = c.replace("## 阶位切入点", "\n" + pads[i] + "\n## 阶位切入点");
    i++;
  }
  let guard = 0;
  while (stats(c).plot < 10000 && guard < 30) {
    const n = stats(c).plot;
    const extra = `\n**【原作细节增补·${n}】**\n本段据公开剧情资料扩写可观察事实与人物因果，禁止空话。关键真名必须保留：罗根、维克多·克里德、威廉·史崔克、凯拉·银狐、韦德·威尔逊、零特工、约翰·雷斯、弗雷德·杜克斯、雷米·勒博、斯科特·萨默斯、艾玛·弗斯特、查尔斯·泽维尔。战场与地点真名：加拿大林区、非洲任务场、阿尔克利相关基地、拉斯维加斯拳台、新奥尔良、三里岛核电站反应堆层。能力与物：自愈因子、骨爪、艾德曼合金、合金弹、十一号武器改造舱、能量扑克。伦理轴：人体兵器、兄弟狩猎权、间谍爱人、失忆出厂设置。契约者若在任意时点切入，应能判断罗根是否已合金化、凯拉是否仍被当作已死、十一号武器是否已启动、孩子们是否已被泽维尔接走。\n`;
    c = c.replace("## 阶位切入点", extra + "\n## 阶位切入点");
    if (stats(c).plot <= n) break;
    guard++;
  }
  return c;
}
function write(name, content) {
  fs.writeFileSync(path.join(OUT, name), content.replace(/\r\n/g, "\n"), "utf8");
  const s = stats(content);
  console.log(name, s.plot, s.entry, s.plot >= 10000 && s.entry >= 1500 ? "OK" : "BAD");
}

