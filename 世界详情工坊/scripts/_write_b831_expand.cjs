function expandFile(name, insertBeforeEntry) {
  const file = path.join(dir, `${name}.md`);
  let t = fs.readFileSync(file, "utf8");
  if (t.includes("【可攻略角色补充细节】") || t.includes("【故事主线补充】")) {
    console.log("SKIP expand", name);
    return;
  }
  t = t.replace("## 休闲切入点", insertBeforeEntry + "\n## 休闲切入点");
  // also expand entry if short
  fs.writeFileSync(file, t, "utf8");
  const parts = t.split("## 休闲切入点");
  const plotN = countNoSpace(parts[0]);
  const rest = parts[1] || "";
  const entryPart = rest.split("## 来源")[0] || rest;
  console.log("EXPAND", name, "plot", plotN, "entry", countNoSpace(entryPart));
}

