function countNoSpace(s) {
  return s.replace(/\s/g, "").length;
}

function pack(name, plot, entry, sources) {
  const body = `# ${name}
<!--meta lib=休闲 tiers=休闲-->

## 剧情

${plot}

## 休闲切入点

${entry}

## 来源

${sources.map((s) => `- [${s[0]}](${s[1]})`).join("\n")}
`;
  const file = path.join(dir, `${name}.md`);
  fs.writeFileSync(file, body, "utf8");
  const plotN = countNoSpace(plot);
  const entN = countNoSpace(entry);
  console.log("WROTE", name, "plot", plotN, "entry", entN, plotN >= 6000 && entN >= 1500 ? "OK" : "SHORT");
}

