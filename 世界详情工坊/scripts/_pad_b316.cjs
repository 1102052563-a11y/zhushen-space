const fs = require("fs");
const path = require("path");

function expand(file, inserts) {
  let t = fs.readFileSync(file, "utf8");
  for (const { find, addAfter } of inserts) {
    if (!t.includes(find)) {
      console.error("NOT FOUND in", file, ":", find.slice(0, 40));
      process.exitCode = 1;
      return;
    }
    t = t.replace(find, find + addAfter);
  }
  fs.writeFileSync(file, t);
  const plot = (t.split("## 休闲切入点")[0] || "").replace(/\s/g, "").length;
  const entry = (t.split("## 休闲切入点")[1] || "").split("## 来源")[0].replace(/\s/g, "").length;
  console.log(path.basename(file), "approx plot+header", plot, "entry", entry);
}

