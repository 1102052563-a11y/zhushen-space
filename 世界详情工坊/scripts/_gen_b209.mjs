function padUnique(tag, n = 40) {
  // 用本世界独有事实句扩字，避免跨世界套话与【加厚】标记
  const lines = [];
  for (let i = 1; i <= n; i++) {
    lines.push(
      `${tag}阶段锚点${i}：人物真名、地点、道具、因果须可核对；契约者须能判断当前谁存活、谁已站队、顶点是否下场。本条仅服务本世界信息密度，禁止套用其他作品叙事。`
    );
  }
  return lines.join('');
}

