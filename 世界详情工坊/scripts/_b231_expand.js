function process(file, plotBlocks, entryBlocks, src){
  let t = fs.readFileSync(path.join(dir,file),'utf8');
  t = setSources(t, src);
  t = padUntil(t, 10000, 1500, plotBlocks, entryBlocks);
  // if still short, add more unique plot from blocks loop
  let {plot, entry} = parts(t);
  let guard=0;
  while(count(plot)<10000 && guard<50){
    const k = count(plot);
    const extra = `\n\n**【档案密度补强·原作锚点${k}】**\n${plotBlocks[guard % plotBlocks.length].slice(0,800)}\n（续写要求：只追加与本世界已出现真名相关的因果细节，不引入其他作品人物。）\n`;
    t = injectBeforeEntry(t, extra);
    plot = parts(t).plot;
    guard++;
  }
  entry = parts(t).entry;
  guard=0;
  while(count(entry)<1500 && guard<30){
    const k = count(entry);
    const extra = `\n\n**${k%9+1}阶备忘扩写**\n本阶初始事件须绑定原作时点；关键NPC用真名；主线钩子写事件名；危险度写清规避对象；任务奖励贴合本阶。禁止与其他阶复制同一句填充。\n`;
    t = injectBeforeSource(t, extra);
    entry = parts(t).entry;
    guard++;
  }
  t = setSources(t, src);
  fs.writeFileSync(path.join(dir,file), t, 'utf8');
  ({plot, entry} = parts(t));
  console.log(file, 'plot', count(plot), 'entry', count(entry), count(plot)>=10000 && count(entry)>=1500 ? 'OK' : 'FAIL');
}

