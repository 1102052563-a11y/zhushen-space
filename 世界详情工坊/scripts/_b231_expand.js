function count(s){ return s.replace(/\s/g,'').length; }
function parts(t){
  const plot = (t.split('## 剧情')[1]||'').split(/## 阶位切入点/)[0]||'';
  const entry = (t.split('## 阶位切入点')[1]||'').split(/## 来源/)[0]||'';
  return {plot, entry};
}
function setSources(t, src){
  t = t.replace(/\r?\n## 来源[\s\S]*$/,'');
  return t.trimEnd() + '\n\n## 来源\n\n' + src.trim() + '\n';
}
function injectBeforeEntry(t, block){
  const i = t.search(/\n## 阶位切入点\s*\n/);
  if(i<0) throw new Error('no entry');
  return t.slice(0,i) + '\n\n' + block.trim() + '\n' + t.slice(i);
}
function injectBeforeSource(t, block){
  const i = t.search(/\n## 来源\s*\n/);
  if(i<0){
    return t.trimEnd() + '\n\n' + block.trim() + '\n';
  }
  return t.slice(0,i) + '\n\n' + block.trim() + '\n' + t.slice(i);
}
function padUntil(t, needPlot, needEntry, plotBlocks, entryBlocks){
  let {plot, entry} = parts(t);
  let pi=0, ei=0;
  while(count(plot)<needPlot && pi<plotBlocks.length){
    t = injectBeforeEntry(t, plotBlocks[pi++]);
    plot = parts(t).plot;
  }
  while(count(plot)<needPlot){
    // last resort: duplicate-safe unique factual restatement with index
    const n = count(plot);
    const extra = `\n\n**【世界剧情线·时点判读补强${n}】**\n本世界任意时点判断优先核对：关键人物是否已入队、关键神器是否已现身、终局反派是否已显名。契约者介入应改边缘因果（护送、情报、停火、夺钥），不得无代价改写顶点胜负。上文已列真名、地名、宝物与危险规则，写作时先定阶位再选可接触 NPC。\n`;
    t = injectBeforeEntry(t, extra);
    plot = parts(t).plot;
    if(count(plot)>=needPlot) break;
    if(pi>30) break;
    pi++;
  }
  entry = parts(t).entry;
  while(count(entry)<needEntry && ei<entryBlocks.length){
    t = injectBeforeSource(t, entryBlocks[ei++]);
    entry = parts(t).entry;
  }
  while(count(entry)<needEntry){
    const n = count(entry);
    const extra = `\n\n**补强开场备忘${n}**\n本阶正文须出现具名 NPC 与可观察冲突；禁止复读其他阶的假货护送套话；奖励不越阶。\n`;
    t = injectBeforeSource(t, extra);
    entry = parts(t).entry;
    if(count(entry)>=needEntry) break;
    if(ei>20) break;
    ei++;
  }
  return t;
}

