function padUnique(base, extras, min) {
  let t = base;
  let i = 0;
  while (t.replace(/\s/g, '').length < min && i < extras.length) {
    t += '\n\n' + extras[i];
    i++;
  }
  // 若仍不足，用本世界专属日程/细节句循环，禁止跨世界套话
  let j = 0;
  while (t.replace(/\s/g, '').length < min && j < 40) {
    const e = extras[j % extras.length];
    t += '\n\n' + e.replace(/（本世界专属[：:]/, '（再补·' + (j + 1) + '：');
    j++;
  }
  return t;
}

