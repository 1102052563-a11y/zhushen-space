import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const DIR = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..', '产出', '批次528');

function expand(file, insertBeforeEntry) {
  const fp = path.join(DIR, file);
  let t = fs.readFileSync(fp, 'utf8').replace(/\r\n/g, '\n');
  const marker = '\n## 阶位切入点\n';
  if (!t.includes(marker)) throw new Error('no entry marker ' + file);
  if (t.includes('【扩写档案·本世界独有】')) {
    console.log('skip already expanded', file);
    return;
  }
  t = t.replace(marker, '\n' + insertBeforeEntry.trim() + '\n' + marker);
  fs.writeFileSync(fp, t, 'utf8');
  const i = t.indexOf('## 剧情');
  const j = t.indexOf('## 阶位切入点');
  const k = t.indexOf('## 来源');
  const plot = t.slice(i, j).replace(/\s/g, '').length;
  const entry = t.slice(j, k).replace(/\s/g, '').length;
  console.log(file, 'plot', plot, 'entry', entry, plot >= 10000 && entry >= 1500 ? 'OK' : 'NEED MORE');
}

