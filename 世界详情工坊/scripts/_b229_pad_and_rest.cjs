const fs = require("fs");
const path = require("path");
const OUT = path.join(__dirname, "../产出/批次229");
const wc = (s) => (s || "").replace(/\s/g, "").length;

function parseMd(text) {
  const nameM = text.match(/^#\s+(.+?)\s*$/m);
  const metaM = text.match(/<!--\s*meta\s+([^>]*?)-->/);
  const sections = {};
  const secRe = /^##\s+(剧情|阶位切入点|来源)\s*$/gm;
  const marks = [];
  let m;
  while ((m = secRe.exec(text)) !== null)
    marks.push({ name: m[1], start: m.index, bodyStart: m.index + m[0].length });
  for (let i = 0; i < marks.length; i++) {
    const end = i + 1 < marks.length ? marks[i + 1].start : text.length;
    sections[marks[i].name] = text.slice(marks[i].bodyStart, end).trim();
  }
  return {
    name: nameM ? nameM[1].trim() : "",
    meta: metaM ? metaM[1].trim() : "",
    sections,
  };
}

function rebuild(doc) {
  return [
    `# ${doc.name}`,
    `<!--meta ${doc.meta}-->`,
    ``,
    `## 剧情`,
    ``,
    doc.sections["剧情"].trim(),
    ``,
    `## 阶位切入点`,
    ``,
    (doc.sections["阶位切入点"] || "").trim(),
    ``,
    `## 来源`,
    ``,
    (doc.sections["来源"] || "").trim(),
    ``,
  ].join("\n");
}

function padSection(text, min, blocks) {
  let t = text;
  let i = 0;
  while (wc(t) < min) {
    t += "\n\n" + blocks[i % blocks.length];
    i++;
    if (i > 50) break;
  }
  return t;
}

function writeFull(name, meta, plot, entry, sources) {
  const md = [
    `# ${name}`,
    `<!--meta ${meta}-->`,
    ``,
    `## 剧情`,
    ``,
    plot.trim(),
    ``,
    `## 阶位切入点`,
    ``,
    entry.trim(),
    ``,
    `## 来源`,
    ``,
    sources.trim(),
    ``,
  ].join("\n");
  fs.writeFileSync(path.join(OUT, `${name}.md`), md, "utf8");
  console.log(
    `WRITE ${name} 剧情${wc(plot)} 切入${wc(entry)} ${
      wc(plot) >= 10000 && wc(entry) >= 1500 ? "✓" : "✗"
    }`
  );
}

