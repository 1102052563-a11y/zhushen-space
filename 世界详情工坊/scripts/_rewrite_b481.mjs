for (const w of worlds) {
  const body = buildFile(w);
  const { issues, plot, entry } = check(body, w.file);
  const cast = [...body.matchAll(/- \*\*([^*]+?)（/g)].map((x) => x[1]);
  cast.forEach((n) => allNames.push({ n, f: w.file }));
  const ok = issues.length === 0;
  table.push({ file: w.file, ok, plot, entry, issues });
  fs.writeFileSync(path.join(OUT, w.file), body, 'utf8');
  console.log(`${ok ? 'PASS' : 'FAIL'} | ${w.file} | 剧情=${plot} | 切入=${entry} | ${issues.join(';') || 'ok'}`);
}

