function check(text, file) {
  const issues = [];
  for (const b of BANNED) {
    if (text.includes(b)) issues.push(`banned:${b}`);
  }
  const req = [
    '【作品来源】',
    '【世界定位】',
    '【世界观 · 舞台设定】',
    '【地理 · 生活舞台】',
    '【故事主线 · 情感线】',
    '【可攻略角色 / 主要人物】',
    '【人际关系网 / 社团势力】',
    '【情感事件 · 名场面】',
    '【隐藏剧情 · 真结局 · 伏笔】',
    '【氛围基调 · 雷区】',
  ];
  for (const r of req) {
    if (!text.includes(r)) issues.push(`missing:${r}`);
  }
  if (!/lib=休闲/.test(text) || !/tiers=休闲/.test(text)) issues.push('meta');
  if (!text.includes('## 休闲切入点')) issues.push('no-entry-h2');
  const cc = (s) => s.replace(/\s/g, '').length;
  const plot = (text.split('## 休闲切入点')[0].split('## 剧情')[1] || '');
  const entry = ((text.split('## 休闲切入点')[1] || '').split('## 来源')[0] || '');
  if (cc(plot) < 6000) issues.push(`plot=${cc(plot)}`);
  if (cc(entry) < 1500) issues.push(`entry=${cc(entry)}`);
  const src = (text.match(/^- \[/gm) || []).length;
  if (src < 3) issues.push(`src=${src}`);
  return { issues, plot: cc(plot), entry: cc(entry) };
}

