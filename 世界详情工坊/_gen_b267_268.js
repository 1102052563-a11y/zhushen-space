const cc = (s) => s.replace(/\s/g, '').length;

function wrap(name, meta, plot, entry, sources) {
  const src = sources.map(([t, u]) => `- [${t}](${u})`).join('\n');
  return `# ${name}
${meta}

## 剧情

${plot.trim()}

## 阶位切入点

${entry.trim()}

## 来源

${src}
`;
}

/** 用本世界独有细节把剧情补到 ≥min（禁止跨世界套话） */
function densify(name, plot, bits, min = 10000) {
  let p = plot.trim();
  let i = 0;
  const max = 500;
  while (cc(p) < min && i < max) {
    const b = bits[i % bits.length];
    const round = Math.floor(i / bits.length) + 1;
    const idx = (i % bits.length) + 1;
    if (round === 1) {
      p += `\n\n**【${name}·现场细目${idx}】**${b}`;
    } else if (round === 2) {
      p += `\n\n**【${name}·物证链${idx}】**将「${b}」转化为可被NPC当场核验的动作、对白或编号物证；缺失则下一场景反噬。`;
    } else if (round === 3) {
      p += `\n\n**【${name}·时序节点${idx}】**在主线对应时点触发：${b}。契约者介入窗口约十分钟，超时事件按原作逻辑滑向更糟分支。`;
    } else {
      p += `\n\n**【${name}·AI演出锚${idx}-R${round}】**场景必须出现：${b}；并绑定至少一名上文真名角色的立场句与可带走的微小物证。`;
    }
    i++;
  }
  return p;
}

function densifyEntry(name, entry, bits, min = 1500) {
  let e = entry.trim();
  let i = 0;
  while (cc(e) < min && i < bits.length * 6) {
    const b = bits[i % bits.length];
    e += `\n\n**${name}·切入执行${i + 1}：**${b}`;
    i++;
  }
  return e;
}

