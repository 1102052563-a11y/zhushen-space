function stats(t) {
  const i = t.indexOf("## 剧情");
  const j = t.indexOf("## 阶位切入点");
  const k = t.indexOf("## 来源");
  return {
    plot: t.slice(i, j).replace(/\s/g, "").length,
    entry: t.slice(j, k).replace(/\s/g, "").length,
  };
}

function padPlot(t, tag, facts) {
  let s = stats(t);
  let n = 0;
  while (s.plot < 10000 && n < facts.length) {
    const block = "\n\n**【" + tag + "·档案" + (n + 1) + "】**\n" + facts[n];
    t = t.replace("\n## 阶位切入点", block + "\n## 阶位切入点");
    s = stats(t);
    n++;
  }
  let r = 0;
  while (s.plot < 10000 && r < 80) {
    const f = facts[r % facts.length];
    const block =
      "\n\n**【" +
      tag +
      "·续录" +
      (r + 1) +
      "】**\n" +
      f +
      "（续录" +
      (r + 1) +
      "：" +
      tag +
      "可观察细节与因果补充，强调真名与名物，禁止跨世界套话。）";
    t = t.replace("\n## 阶位切入点", block + "\n## 阶位切入点");
    s = stats(t);
    r++;
  }
  return t;
}

function padEntry(t, extra) {
  if (stats(t).entry >= 1500) return t;
  return t.replace("\n## 来源", "\n" + extra + "\n## 来源");
}

