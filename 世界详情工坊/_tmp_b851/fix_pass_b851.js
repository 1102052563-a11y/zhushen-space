for (const [file, conf] of Object.entries(fixes)) {
  const p = path.join(outDir, file);
  let c = fs.readFileSync(p, 'utf8');

  // ensure 乐园阶位映射
  if (!c.includes('乐园阶位映射')) {
    // insert before 地理 or after 力量体系
    if (c.includes('**【地理')) {
      c = c.replace('**【地理', conf.mapLine + '\n\n**【地理');
    } else {
      c = c.replace('**【世界剧情线】**', conf.mapLine + '\n\n**【世界剧情线】**');
    }
  }

  // ensure 阶位↔ at start of entry
  if (!c.includes('阶位↔')) {
    c = c.replace('## 阶位切入点\n', '## 阶位切入点\n\n' + conf.entryHead + '\n\n');
  } else if (!c.match(/## 阶位切入点[\s\S]{0,200}阶位↔/)) {
    c = c.replace('## 阶位切入点\n', '## 阶位切入点\n\n' + conf.entryHead + '\n\n');
  }

  // fix **1阶** to **一阶** etc if any
  const cn = ['一', '二', '三', '四', '五', '六', '七', '八', '九'];
  for (let i = 1; i <= 9; i++) {
    c = c.replaceAll(`**${i}阶`, `**${cn[i - 1]}阶`);
  }

  // expand plot uniquely
  let st = counts(c);
  let i = 0;
  while (st.plot < 10000 && i < conf.uniqueChunks.length) {
    const chunk = `\n\n**【专有补强·${file.replace('.md', '')}·${i + 1}】**\n${conf.uniqueChunks[i]}\n`;
    c = c.replace('\n## 阶位切入点', chunk + '\n## 阶位切入点');
    i++;
    st = counts(c);
  }
  // if still short, weave remaining with variation
  let g = 0;
  while (st.plot < 10000 && g < 10) {
    g++;
    const chunk = `\n\n**【${file.replace('.md', '')}·场景零件库${g}】**\n` +
      conf.uniqueChunks.map((t, idx) => `（${idx + 1}）` + t.slice(0, 40)).join('；') +
      `。以上零件须在任务中化为可拾取/可签署/可被仇家引用的对象，禁止只写氛围。阶段${g}额外要求：给出一个失败后的记名方式（通缉、欠条、系统公告、红点、坐标泄露之一）。\n`;
    c = c.replace('\n## 阶位切入点', chunk + '\n## 阶位切入点');
    st = counts(c);
  }

  // entry length
  st = counts(c);
  if (st.entry < 1500) {
    c = c.replace(
      '\n## 来源',
      `\n\n**切入点执行细则**：每阶七字段齐全；关键NPC加粗真名；开场白含地标；危险度写清规避对象；奖励与本阶资源树一致；禁止六阶复读同一句填充。\n## 来源`
    );
  }

  fs.writeFileSync(p, c, 'utf8');
  st = counts(c);
  console.log(file, st.plot, st.entry, 'map', c.includes('乐园阶位映射'), 'arrow', c.includes('阶位↔'));
}
