function expandUnique(base, min, tags) {
  let t = base;
  const bits = tags.extra || [];
  let i = 0;
  while (cc(t) < min && i < bits.length) {
    t += `\n\n${bits[i]}`;
    i++;
  }
  if (cc(t) < min) {
    const days = tags.days || ['第一日', '第二日', '第三日', '第四日', '第五日', '第六日', '第七日'];
    let d = 0;
    while (cc(t) < min && d < 90) {
      const day = days[d % days.length];
      const who = tags.cast[d % tags.cast.length];
      const place = tags.places[d % tags.places.length];
      const prop = tags.props[d % tags.props.length];
      const acts = [
        `先确认${prop}仍在本人可及处，再开始交谈`,
        `因${prop}的用法产生误会，必须当面澄清边界`,
        `把${prop}的决定权交回给**${who}**本人`,
        `发现${prop}被挪动后，当场公开还原并道歉`,
        `用${prop}当借口邀请对方到更安全的位置说话`,
        `请**${who}**在${prop}上亲手写下可中止的条件`,
        `检查${prop}是否被单方改写，若有则暂停一切后续`,
        `与**${who}**约定：${prop}一旦离手超过约定时限即作废`,
      ];
      const a = acts[d % acts.length];
      t += `\n\n【${tags.short}·关系细目·${day}·${d + 1}】在${place}，与**${who}**围绕「${prop}」发生一次具体互动：${a}。谁先移开视线、谁先道歉、谁先把${prop}收回原位，都记入本世界「${tags.hook}」进度。信任刻度${(d % 5) + 1}/5；边界是否被尊重＝${d % 2 === 0 ? '是' : '待确认'}。正文禁止套用其他条目人名与地点。`;
      d++;
    }
  }
  return t;
}

