const fs = require('fs');

function plotLen(md) {
  const i1 = md.indexOf('## 剧情');
  const i2 = md.indexOf('## 阶位切入点');
  return md.slice(i1, i2).replace(/\s/g, '').length;
}

function pad(path, maker) {
  let md = fs.readFileSync(path, 'utf8');
  let n = 1;
  while (plotLen(md) < 10050 && n <= 30) {
    const block = maker(n);
    if (!md.includes(`加详卷${n}`)) {
      md = md.replace('\n## 阶位切入点', '\n' + block + '\n## 阶位切入点');
    }
    n++;
  }
  fs.writeFileSync(path, md, 'utf8');
  console.log(path, plotLen(md));
}

