function writeWorld(dir, name, metaTiers, body) {
  const outDir = path.join(ROOT, dir);
  fs.mkdirSync(outDir, { recursive: true });
  const fp = path.join(outDir, name + '.md');
  fs.writeFileSync(fp, body.replace(/\n/g, '\r\n'), 'utf8');
  const plot = body.split('## 阶位切入点')[0].replace(/^[\s\S]*?## 剧情\s*/, '');
  const entry = (body.split('## 阶位切入点')[1] || '').split('## 来源')[0];
  const src = body.split('## 来源')[1] || '';
  const links = (src.match(/\]\(https?:\/\/[^)]+\)/g) || []).length;
  console.log(`${name}  剧情${count(plot)} 切入${count(entry)} 来源链${links}`);
}

