function densify(title, basePlot, baseEntry, beats) {
  let p = basePlot.trim();
  let e = baseEntry.trim();
  // Add long unique beat paragraphs
  for (const b of beats) {
    p += `\n\n${b}`;
  }
  // Ensure length with unique scene cards (world-specific names from beats)
  let i = 0;
  while (count(p) < 10100 && i < 80) {
    i++;
    const beat = beats[i % beats.length];
    const snip = beat.replace(/\s/g,'').slice(0, 40);
    p += `\n\n**【可玩切片·${title.slice(0,8)}·${i}】**现场必须出现：${snip}……延伸为一次完整遭遇（起因—对抗—代价—留下的物证名）。契约者选项至少两项且后果分叉，禁止「无事发生」。`;
  }
  i = 0;
  while (count(e) < 1600 && i < 30) {
    i++;
    e += `\n开场零件${i}：第二人称感官（声/味/触）+ 具名NPC一句台词 + 规则凭证出示；结束时给去向地名。`;
  }
  return {p, e};
}

