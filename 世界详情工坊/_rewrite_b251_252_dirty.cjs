function ensureLen(s, minPlot = 10050, minCut = 1550) {
  let plot = s.match(/## 剧情\n([\s\S]*?)\n## 阶位切入点/)[1];
  let cut = s.match(/## 阶位切入点\n([\s\S]*?)\n## 来源/)[1];
  const title = s.match(/^# (.+)$/m)[1];
  let n = 0;
  while ([...plot].length < minPlot && n < 20) {
    n++;
    plot += `\n\n**【${title}·原作向场面扩写${n}】**\n` +
      `本段只写可入库的任务事实：谁在场、缺席谁、哪扇门开着、哪本账在桌上、哪句对白改变立场。` +
      `契约者介入后，敌方以追杀、加码对价/下一场赛事、政治丑闻或人质替换回流。` +
      `禁止空话与跨世界套话。补充一件本世界独有物件：${title.includes('巨乳') ? '铃舌/排名板红墨/圣印裂痕/林徽' : '账册/白甲甲片/支付期限表/侍女袖扣'}。` +
      `战斗或对峙描写强调环境：狭窄通道、仪式阵眼、潮汐/城石回声、监视死角、撤退窗口。` +
      `人物保持真名与原作性格：女战士耻怒、神官自我合理化、姫骑士骂废物却护部下、迪尔克恐慌与残暴并存。` +
      `奖励保持本阶：通行、情报、残件、人脉，不越级。\n`;
  }
  n = 0;
  while ([...cut].length < minCut && n < 15) {
    n++;
    cut += `\n\n**切入扩写${n}**\n` +
      `开场必须有物件钩子+人名+抉择。关键NPC全部真名加粗，各附一句图什么/怕什么。` +
      `主线钩子写原作节点；支线写本阶独有。危险度写清规避对象；涉及顶点写超阶情报优先。` +
      `第二幕：报告上级/私下交易/直接救人三选一。第三幕：用时间压力逼决策。\n`;
  }
  return s.replace(/## 剧情\n[\s\S]*?\n## 阶位切入点/, `## 剧情\n${plot}\n## 阶位切入点`)
           .replace(/## 阶位切入点\n[\s\S]*?\n## 来源/, `## 阶位切入点\n${cut}\n## 来源`);
}

