function expand(file, plotPad, entryPad, extraFixes = []) {
  let t = fs.readFileSync(path.join(dir, file), 'utf8');
  for (const [a,b] of extraFixes) t = t.split(a).join(b);
  // insert plot pad before ## 阶位切入点
  if (!t.includes('【剧情加厚·原作细目】') && plotPad) {
    t = t.replace('\n## 阶位切入点', '\n' + plotPad + '\n\n## 阶位切入点');
  }
  // insert entry pad before ## 来源
  if (entryPad && !t.includes('【切入加厚】')) {
    t = t.replace('\n## 来源', '\n' + entryPad + '\n\n## 来源');
  }
  fs.writeFileSync(path.join(dir, file), t, 'utf8');
  const plot = t.split('## 阶位切入点')[0];
  const entry = (t.split('## 阶位切入点')[1] || '').split('## 来源')[0];
  console.log(file, 'plot', plot.replace(/\s/g,'').length, 'entry', entry.replace(/\s/g,'').length);
}

