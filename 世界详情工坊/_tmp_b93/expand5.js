const fs = require('fs');
const path = require('path');
const dir = '产出/批次93';

function pad(file, n, paragraphs) {
  let t = fs.readFileSync(path.join(dir, file), 'utf8');
  const key = `【补完块${n}·${file.replace('.md','')}】`;
  if (t.includes(key)) { console.log(file, 'skip', n); return; }
  const block = `\n**${key}**\n` + paragraphs.join('\n\n') + '\n';
  t = t.replace('## 阶位切入点', block + '\n## 阶位切入点');
  fs.writeFileSync(path.join(dir, file), t, 'utf8');
  const plot = t.split('## 阶位切入点')[0].replace(/\s/g,'').length;
  const entry = (t.split('## 阶位切入点')[1]||'').split('## 来源')[0].replace(/\s/g,'').length;
  console.log(file, 'plot', plot, 'entry', entry);
}

