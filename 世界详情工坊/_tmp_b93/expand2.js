const fs = require('fs');
const path = require('path');
const dir = '产出/批次93';

function addBefore(file, marker, block) {
  let t = fs.readFileSync(path.join(dir, file), 'utf8');
  if (t.includes(block.slice(0, 20))) { console.log(file, 'skip dup'); return; }
  if (!t.includes(marker)) { console.log(file, 'no marker'); return; }
  t = t.replace(marker, '\n' + block + '\n' + marker);
  fs.writeFileSync(path.join(dir, file), t, 'utf8');
  const plot = t.split('## 阶位切入点')[0].replace(/\s/g,'').length;
  const entry = (t.split('## 阶位切入点')[1]||'').split('## 来源')[0].replace(/\s/g,'').length;
  console.log(file, 'plot', plot, 'entry', entry);
}

