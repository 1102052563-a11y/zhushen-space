function need(file) {
  const t = fs.readFileSync(path.join(dir, file), 'utf8');
  const plot = t.split('## 阶位切入点')[0].replace(/\s/g,'').length;
  return { t, plot, need: Math.max(0, 10050 - plot) };
}

