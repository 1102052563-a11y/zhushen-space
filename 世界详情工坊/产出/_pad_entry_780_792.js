let ok = 0;
const still = [];
for (const t of targets) {
  let c = fs.readFileSync(t.p, 'utf8');
  if (!c.includes('## 休闲切入点')) {
    still.push({ title: t.title, why: 'no-entry' });
    continue;
  }
  if (!c.includes('切入身份（展开）')) {
    c = c.replace('\n## 来源', pad + '\n## 来源');
  }
  let ec = ((c.match(/## 休闲切入点\s*([\s\S]*?)(?=\n## |$)/) || [])[1] || '').replace(/\s/g, '').length;
  if (ec < 1500) {
    let more = '';
    for (let i = 1; i <= 8; i++) {
      more += `\n\n**切入细则${i}：** 在本世界第${i}个可重复日常节点（买饮料/还书/值日/等车/看夕阳/关窗/借充电器/代取快递），契约者必须完成一次「被看见的善举」或「被拒绝后的体面离开」。关系进度只记称呼变化与物件去向，不记伤害。与《${t.title}》的气质对齐。`;
    }
    c = c.replace('\n## 来源', more + '\n## 来源');
  }
  fs.writeFileSync(t.p, c);
  const pc = ((c.match(/## 剧情\s*([\s\S]*?)(?=\n## )/) || [])[1] || '').replace(/\s/g, '').length;
  ec = ((c.match(/## 休闲切入点\s*([\s\S]*?)(?=\n## |$)/) || [])[1] || '').replace(/\s/g, '').length;
  if (pc >= 6000 && ec >= 1500) ok++;
  else still.push({ title: t.title, pc, ec });
}
console.log(JSON.stringify({ ok, still: still.length, still: still.slice(0, 10) }, null, 2));
