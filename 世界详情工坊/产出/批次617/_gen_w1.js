开场白建议：「鸡面具在监控死角摘下半秒又戴上。斗鸡的声音第一次不加特效：『孩子，视频教不了你躲子弹。』柳皓彬的手机震个不停，全是『你是不是死了』的留言。」

关键NPC立场：**西罗志郎／李镇浩**（顶点，灭口优先）、**斗鸡**（条件性援助）、**土井界人**（已死或濒死的警示牌）、**柳皓彬**（护母护伴）、**白成俊**（可能弃车保帅）。

主线钩子/支线：转移朴智贤；保存组织账本镜像；与斗鸡交易「一堂不能上传的课」。支线——假死撤离、跨国港口换船、切断山冈洗钱链一环。

