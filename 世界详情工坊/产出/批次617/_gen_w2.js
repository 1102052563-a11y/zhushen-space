开场白建议：「水雾把喊声切成碎片。湿婆杜肩上还抗着半截湿婆林伽的绳索，眼睛却只盯着天上的白练。有人说上面住着神，有人说上面住着暴君——你很快会知道两者可以是同一个名字。」

关键NPC立场：**湿婆杜**（邀你同闯）、**桑加**（阻拦攀瀑）、**卡塔帕**（远处阴影，尚未相认）、**阿凡蒂卡**（山顶将遇）。

