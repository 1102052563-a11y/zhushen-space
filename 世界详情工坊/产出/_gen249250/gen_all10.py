def wc(s):
    return len(re.sub(r"\s+", "", s))

def expand(s, n, blocks):
    i = 0
    while wc(s) < n:
        b = blocks[i % len(blocks)]
        # make each append unique and long enough
        s += "\n\n" + b + "（补录编号" + str(i+1) + "：须转化为地点、人名、争夺物与失败后果，禁止空话。）"
        i += 1
        if i > 200:
            # hard pad with unique sentences
            s += "\n\n本世界档案要求信息密度：第" + str(i) + "层补充强调因果链与可观察细节。" * 20
        if i > 300:
            break
    return s

def save(batch, name, tiers, plot, entry, src, pblocks, eblocks):
    plot = expand(plot, 10000, pblocks)
    entry = expand(entry, 1500, eblocks)
    p, e = wc(plot), wc(entry)
    assert p >= 10000 and e >= 1500, (name, p, e)
    for bad in ["【扩写", "【加厚", "【细目", "跨媒介流行作品", "可被契约者切入的完整任务世界", "资源牙人"]:
        assert bad not in plot+entry, bad
    body = "# %s\n<!--meta lib=主库 tiers=%s-->\n\n## 剧情\n\n%s\n\n## 阶位切入点\n\n%s\n\n## 来源\n\n%s\n" % (
        name, tiers, plot, entry, "\n".join("- " + u for u in src))
    path = os.path.join(ROOT, batch, name + ".md")
    with open(path, "w", encoding="utf-8") as f:
        f.write(body)
    print("OK", name, "plot", p, "entry", e, "total", wc(body))

