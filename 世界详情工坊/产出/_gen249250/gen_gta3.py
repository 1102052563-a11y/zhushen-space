def wc(s):
    return len(re.sub(r"\s+", "", s))

def expand_to(s, min_n, blocks):
    i = 0
    while wc(s) < min_n and i < len(blocks)*5:
        s += "\n\n" + blocks[i % len(blocks)]
        i += 1
    return s

def write_md(batch, name, tiers, plot, entry, sources):
    p, e = wc(plot), wc(entry)
    if p < 10000:
        raise SystemExit(f"FAIL {name} plot={p}")
    if e < 1500:
        raise SystemExit(f"FAIL {name} entry={e}")
    for bad in ["【扩写", "【加厚", "【细目", "跨媒介流行作品", "可被契约者切入的完整任务世界", "原作主角（若已登场）", "资源牙人"]:
        if bad in plot+entry:
            raise SystemExit(f"BANNED {bad} in {name}")
    body = f"""# {name}
<!--meta lib=主库 tiers={tiers}-->

## 剧情

{plot}

## 阶位切入点

{entry}

## 来源

""" + "\n".join(f"- {u}" for u in sources) + "\n"
    path = os.path.join(ROOT, batch, name + ".md")
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, "w", encoding="utf-8") as f:
        f.write(body)
    print(f"OK {name}: plot={p} entry={e} file={len(body)}")

