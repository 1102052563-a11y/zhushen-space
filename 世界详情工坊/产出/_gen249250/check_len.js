const wc = s => s.replace(/\s/g,'').length;

function build(title, tiers, plot, entry, sources) {
  const body = `# ${title}\n<!--meta lib=主库 tiers=${tiers}-->\n\n## 剧情\n\n${plot}\n\n## 阶位切入点\n\n${entry}\n\n## 来源\n\n${sources.map(s=>'- '+s).join('\n')}\n`;
  const p = wc(plot), e = wc(entry);
  if (p < 10000) throw new Error(title+' plot '+p);
  if (e < 1500) throw new Error(title+' entry '+e);
  return body;
}

