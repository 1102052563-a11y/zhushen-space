function count(md) {
  const i = md.indexOf("## 剧情");
  const j = md.indexOf("## 阶位切入点");
  const k = md.indexOf("## 来源");
  return {
    plot: md.slice(i, j).replace(/\s/g, "").length,
    entry: md.slice(j, k).replace(/\s/g, "").length,
  };
}

function padTo(md, minPlot, minEntry, tag) {
  let c = count(md);
  let n = 0;
  while (c.plot < minPlot && n < 40) {
    n++;
    const block = `

**【${tag}·档案扩写${n}】**
本段补充可观察细节与因果链，服务 AI 在任意时点判断局势。地点、人物真名、争夺物与失败后果必须写进正文，禁止空话。雨、烟、金属、数据噪音、军号或瘟疫气味随世界切换。低阶先活，中阶先站队，高阶先问情报。契约者改局部可以，改终局核心必须付追杀与回流代价。权限、徽章、合同、王冠或义体序列号，往往比当场武力更决定谁能走进下一道门。
`;
    md = md.replace("## 阶位切入点", block + "\n## 阶位切入点");
    c = count(md);
  }
  n = 0;
  while (c.entry < minEntry && n < 30) {
    n++;
    const block = `

**切入补钩${n}**：本阶额外事件须绑定真名 NPC 与独特地点，禁止与其他阶复制同一句护送假货。失败=权限回收／灭口／记忆污染／编制开除／被王座点名之一，按世界选择。
`;
    md = md.replace("## 来源", block + "\n## 来源");
    c = count(md);
  }
  return md;
}

function write(name, body) {
  const md = padTo(body, 10050, 1550, name.slice(0, 8));
  const fp = path.join(dir, name);
  fs.writeFileSync(fp, md, "utf8");
  console.log(name, count(md));
}

