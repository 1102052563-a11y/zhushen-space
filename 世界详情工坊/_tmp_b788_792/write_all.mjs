const charCount = (s) => (s || '').replace(/\s/g, '').length;

function padTo(s, min, flavor) {
  if (charCount(s) >= min) return s;
  const seasons = ['春', '梅雨', '盛夏', '秋祭', '初雪', '年末', '情人节周', '黄金周', '盂兰盆', '新学期'];
  const bits = [];
  let i = 0;
  while (charCount(s + bits.join('')) < min) {
    const se = seasons[i % seasons.length];
    bits.push(`\n\n**${se}·关系温度补记**\n${flavor}。${se}里同一句关心会改含义：有人把共伞角度当站队，有人把多写的备注当求婚预告。契约者若连续三天选择倾听而非替对方做决定，好感以「被记住」而非「被征服」爬升；若只追求效率打卡，信物颜色会在无人注意时褪淡。纯日常线可完全不碰敏感菜单，只靠本舞台周常事件推进到安定 End。`);
    i++;
    if (i > 50) break;
  }
  return s + bits.join('');
}

function build(w) {
  const cast = w.cast.map((c) =>
    `- **${c.n}（${c.id}）**｜外貌：${c.look}｜性格：${c.per}｜角色类型：${c.ty}｜萌点/魅力：${c.ch}｜个人线剧情：${c.rt}｜与主角关系：${c.rel}`
  ).join('\n');
  const routes = w.routes.join('\n\n');
  const scenes = w.scenes.map((s, i) => `${i + 1}. ${s}`).join('\n');
  const factions = w.factions.map((f) => `- ${f}`).join('\n');
  const targets = w.targets.map((t) => `- ${t}`).join('\n');
  const hooks = w.hooks.map((h, i) => `${i + 1}. ${h}`).join('\n');
  const src = w.sources.map(([t, u]) => `- [${t}](${u})`).join('\n');

  let plot = `**【作品来源】**
${w.src}

**【世界定位】**
${w.pos}

**【世界观 · 舞台设定】**
${w.world}

**【地理 · 生活舞台】**
${w.geo}

**【故事主线 · 情感线】**
${w.story}

${routes}

**【可攻略角色 / 主要人物】**
${cast}

**【人际关系网 / 社团势力】**
${factions}

**【情感事件 · 名场面】**
${scenes}

**【隐藏剧情 · 真结局 · 伏笔】**
${w.hidden}

**【氛围基调 · 雷区】**
${w.tone}
`;

  plot = padTo(plot, 6200, w.flavor);

  let entry = `> 本世界为休闲/恋爱向，无生存压力与战斗。契约者以**日常身份**融入，核心玩法＝relationship 攻略 + 日常事件，而非任务厮杀。

切入身份：${w.entryId}

切入时点：${w.entryWhen}

初始处境：${w.entrySit}

开场白建议：「${w.entryOpen}」

可攻略对象：
${targets}

日常玩法钩子：
${hooks}

氛围/雷区：${w.entryTone}
`;
  entry = padTo(entry, 1600, w.flavor);

  return `# ${w.name}
<!--meta lib=休闲 tiers=休闲-->

## 剧情

${plot}

## 休闲切入点

${entry}

## 来源

${src}
`;
}

