function cleanCut(tail) {
  // drop 补 / 补笔 / 加长库 等凑字段，保留正式七字段阶位与来源
  const lines = tail.split(/\r?\n/)
  const keep = []
  let skip = false
  for (const line of lines) {
    if (/^\*\*[^*]+补/.test(line) || /^\*\*开场白/.test(line) || /^\*\*日常/.test(line) || /^\*\*氛围/.test(line) || /^\*\*一～八阶补/.test(line)) {
      skip = true
      continue
    }
    if (skip) {
      if (/^\*\*[一二三四五六七八]阶/.test(line) || /^## /.test(line) || /^> /.test(line)) {
        skip = false
      } else if (line.startsWith('**') && !line.includes('补')) {
        skip = false
      } else continue
    }
    // also skip standalone 补笔 blocks that start mid
    if (/补笔|开场白加长|开场白库|开场白再|日常玩法|日常钩子|氛围雷区/.test(line) && line.startsWith('**')) continue
    keep.push(line)
  }
  return keep.join('\n').replace(/\n{3,}/g, '\n\n')
}

