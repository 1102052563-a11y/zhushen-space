import fs from 'node:fs';
import path from 'node:path';

const base = 'C:/Users/Administrator/Desktop/前端卡/files/世界详情工坊/产出';
const nw = (s) => (s || '').replace(/\s/g, '').length;

function split(t) {
  const m1 = t.match(/^([\s\S]*?\n)## 剧情\n([\s\S]*?)\n## 休闲切入点\n([\s\S]*?)\n## 来源\n([\s\S]*)$/);
  if (!m1) throw new Error('parse fail');
  return { head: m1[1], plot: m1[2], entry: m1[3], src: m1[4] };
}
function join(p) {
  return p.head + '\n## 剧情\n' + p.plot + '\n## 休闲切入点\n' + p.entry + '\n## 来源\n' + p.src.replace(/\n+$/, '\n');
}
function ensure(file, plotAdd, entryAdd) {
  const fp = path.join(base, file);
  let t = fs.readFileSync(fp, 'utf8').replace(/^\uFEFF/, '').replace(/\r\n/g, '\n');
  let p = split(t);
  if (plotAdd && nw(p.plot) < 8200) p.plot = p.plot.trimEnd() + '\n\n' + plotAdd.trim();
  if (entryAdd && nw(p.entry) < 1850) p.entry = p.entry.trimEnd() + '\n\n' + entryAdd.trim();
  // soft top-up once if still short (unique text only, no loop paste of same block twice unless needed)
  if (plotAdd && nw(p.plot) < 8000) p.plot = p.plot.trimEnd() + '\n\n' + plotAdd.trim();
  if (entryAdd && nw(p.entry) < 1800) p.entry = p.entry.trimEnd() + '\n\n' + entryAdd.trim();
  fs.writeFileSync(fp, join(p), 'utf8');
  t = fs.readFileSync(fp, 'utf8');
  p = split(t);
  console.log(file, 'plot', nw(p.plot), 'entry', nw(p.entry),
    nw(p.plot) >= 8000 && nw(p.entry) >= 1800 ? 'OK' : 'SHORT');
}

