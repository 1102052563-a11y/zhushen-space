const fs = require('fs');
const path = require('path');
const OUT = path.join(__dirname, '..', '产出', '批次92');
const charCount = (s) => (s || '').replace(/\s/g, '').length;

function padFile(file, chunks) {
  let t = fs.readFileSync(path.join(OUT, file), 'utf8').replace(/\r\n/g, '\n');
  let plot = t.split('## 阶位切入点')[0].split('## 剧情')[1] || '';
  let i = 0;
  while (charCount(plot) < 10050 && i < chunks.length) {
    t = t.replace('\n## 阶位切入点\n', '\n' + chunks[i] + '\n## 阶位切入点\n');
    plot = t.split('## 阶位切入点')[0].split('## 剧情')[1] || '';
    i++;
  }
  while (charCount(plot) < 10050) {
    const extra = `\n**【补量·${file.replace('.md', '')}·${charCount(plot)}】**\n本段补强现场可观察细节：气味、灯光、脚步、通讯杂音、伤口位置、倒计时数字。AI写任何动作前后都要更新这些状态，避免前后矛盾。契约者改写局部胜负时，用更强规则/更多敌人/更短窗口回流，不削弱顶点。关键真名与卡片锚点必须反复可检索。禁止跨世界套话。\n`;
    t = t.replace('\n## 阶位切入点\n', extra + '\n## 阶位切入点\n');
    plot = t.split('## 阶位切入点')[0].split('## 剧情')[1] || '';
  }
  fs.writeFileSync(path.join(OUT, file), t);
  const entry = (t.split('## 阶位切入点')[1] || '').split('## 来源')[0] || '';
  console.log(file, charCount(plot), charCount(entry), /跨媒介流行作品/.test(t));
}

