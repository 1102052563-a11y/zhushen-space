开场白建议：「门是唯一的国境。门这边是呼吸，门那边是牙齿。有人用公文包砸门，有人用身体顶门——你站在哪一边？」

关键NPC立场：**尚华 / 成景**——护孕优先；**勇国 / 珍熙**——年轻人的冲动与恋爱；**延锡**——煽动锁门；**石宇**——逐渐被改变。

主线钩子/支线：钩子=守住或攻下一扇门；支线=分配水和食物；支线=安抚秀安；支线=收集外部新闻短信。

