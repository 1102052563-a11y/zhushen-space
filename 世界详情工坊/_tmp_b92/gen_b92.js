开场白建议：「轮轨声还很稳，短信却开始连环震动。女孩抱着生日礼物，男人盯着股票软件。下一秒，血溅在窗上的站名：首尔——这个名字很快会变成来时路。」

关键NPC立场：**石宇**——只护秀安，视你为变数；**秀安**——会求你帮忙；**尚华**——观察你是否「像人」；**延锡**——已开始盘算隔离谁。

