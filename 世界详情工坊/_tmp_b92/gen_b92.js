气味：血、消毒水、尿、烧焦机油。声音：KTX轮轨、尸吼、广播女声、棒球棍击骨。制度：检票、站名广播、企业来电。价格：一张通往釜山的票在末日变得无意义，一条命的价格变成「你让不让门」。

