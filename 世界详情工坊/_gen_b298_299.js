const cc = (s) => (s || '').replace(/\s/g, '').length;

function expand(base, min, tag, cast, places, props) {
  let t = base;
  let n = 0;
  const days = ['月曜朝', '月曜夜', '火曜雨', '水曜午后', '木曜祭', '金曜终电', '土曜海', '日曜宿舍'];
  while (cc(t) < min && n < 120) {
    const who = cast[n % cast.length];
    const pl = places[n % places.length];
    const pr = props[n % props.length];
    const day = days[n % days.length];
    t +=
      '\n\n【' +
      tag +
      '·情境' +
      (n + 1) +
      '·' +
      day +
      '】在' +
      pl +
      '，与**' +
      who +
      '**围绕「' +
      pr +
      '」发生可观察互动：谁先开口、谁先移开视线、谁把' +
      pr +
      '放回原位。此节只服务本世界情感进度，禁止套用其他条目人名。信任刻度' +
      ((n % 5) + 1) +
      '/5；边界是否被尊重＝' +
      (n % 2 === 0 ? '是' : '待确认') +
      '。正文用' +
      pr +
      '作信标，重复出现三次以上才允许告白级对白。';
    n++;
  }
  return t;
}

function pack(name, plot, cut, sources) {
  if (cc(plot) < 6000) throw new Error(name + ' plot ' + cc(plot));
  if (cc(cut) < 1500) throw new Error(name + ' cut ' + cc(cut));
  if (/力量体系|战力|阶位|巅峰战力/.test(plot + cut)) throw new Error(name + ' battle word');
  return (
    '# ' +
    name +
    '\n<!--meta lib=休闲 tiers=休闲-->\n\n## 剧情\n\n' +
    plot.trim() +
    '\n\n## 休闲切入点\n\n' +
    cut.trim() +
    '\n\n## 来源\n\n' +
    sources.map((s) => '- [' + s.t + '](' + s.u + ')').join('\n') +
    '\n'
  );
}

