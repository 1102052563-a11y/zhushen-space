const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '产出');

function charCount(s) {
  return (s || '').replace(/\s/g, '').length;
}

function writeWorld(relDir, name, metaTiers, plot, entry, sources) {
  const dir = path.join(ROOT, relDir);
  fs.mkdirSync(dir, { recursive: true });
  const body = [
    `# ${name}`,
    `<!--meta lib=主库 tiers=${metaTiers}-->`,
    '',
    '## 剧情',
    '',
    plot.trim(),
    '',
    '## 阶位切入点',
    '',
    entry.trim(),
    '',
    '## 来源',
    '',
    sources.trim(),
    '',
  ].join('\n');
  const fp = path.join(dir, `${name}.md`);
  fs.writeFileSync(fp, body, 'utf8');
  const plotC = charCount(plot);
  const entC = charCount(entry);
  const ok = plotC >= 10000 && entC >= 1500;
  console.log(`${ok ? 'OK' : 'FAIL'} ${name} 剧情${plotC} 切入${entC} bytes=${Buffer.byteLength(body, 'utf8')}`);
  return { name, plotC, entC, ok, fp };
}

