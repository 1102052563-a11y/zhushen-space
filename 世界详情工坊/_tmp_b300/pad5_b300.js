function need(fp) {
  const t = fs.readFileSync(fp,'utf8');
  const plot = t.split('## 剧情')[1].split('## 休闲切入点')[0].replace(/\s/g,'').length;
  const ent = t.split('## 休闲切入点')[1].split('## 来源')[0].replace(/\s/g,'').length;
  return {t, plot, ent, dp: Math.max(0,6000-plot), de: Math.max(0,1500-ent)};
}

function fill(fp, plotText, entText) {
  let t = fs.readFileSync(fp,'utf8');
  if (plotText) t = t.replace('\n## 休闲切入点\n', '\n'+plotText+'\n\n## 休闲切入点\n');
  if (entText) t = t.replace('\n## 来源\n', '\n'+entText+'\n\n## 来源\n');
  fs.writeFileSync(fp,t,'utf8');
  const n = need(fp);
  console.log(path.basename(fp), n.plot, n.ent, n.plot>=6000&&n.ent>=1500?'OK':'NO', 'need', n.dp, n.de);
}

