function densify(plot, entry, densers, entryPads) {
  let p = plot, e = entry, i = 0;
  while (p.replace(/\s/g,"").length < 10200 && i < densers.length) p += "\n" + densers[i++];
  while (p.replace(/\s/g,"").length < 10200) p += "原作因果链节点须以真名事件呈现。";
  i = 0;
  while (e.replace(/\s/g,"").length < 1600 && i < entryPads.length) e += "\n" + entryPads[i++];
  while (e.replace(/\s/g,"").length < 1600) e += "本阶事件与人名保持独有，勿与他阶复制。";
  return {p,e};
}

