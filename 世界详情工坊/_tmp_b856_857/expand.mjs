function noWs(s) {
  return s.replace(/\s/g, "").length;
}

function split(md) {
  const m = md.match(/^([\s\S]*?## 剧情\s*)([\s\S]*?)(\n## 阶位切入点\s*)([\s\S]*?)(\n## 来源\s*[\s\S]*)$/);
  if (!m) throw new Error("split fail");
  return { head: m[1], plot: m[2], mid: m[3], entry: m[4], tail: m[5] };
}

function ensureEntryHeader(entry, line) {
  if (/阶位\s*↔|阶位↔/.test(entry)) return entry;
  return `\n> ${line}\n\n` + entry.trimStart();
}

function padPlot(name, base, blocks) {
  let p = base.trimEnd() + "\n\n";
  let i = 0;
  while (noWs(p) < 10200 && i < 80) {
    const b = blocks[i % blocks.length];
    // unique salt per iteration
    p += `\n\n**【${name}·密录${i + 1}】**\n${b.replace(/\{N\}/g, String(i + 1))}\n`;
    i++;
  }
  return p;
}

function padEntry(name, base, tiers, genTier) {
  let e = base.trimEnd() + "\n";
  // ensure each tier exists with min bulk
  for (const t of tiers) {
    if (!e.includes(`**${t}阶`)) {
      e += `\n\n${genTier(t)}\n`;
    }
  }
  let i = 0;
  while (noWs(e) < 1600 && i < 40) {
    const t = tiers[i % tiers.length];
    e += `\n\n**${t}阶补场（${name}·${i + 1}）**\n切入身份/时点：契约者在本阶以编外身份卡入原作已写冲突层。\n初始事件：${name}本阶独有事件——资源见顶、证人改口或路线封锁三选一。\n开场白建议：「你在${name}的${t}阶夜里闻到专属于本世界的气味。选择发生在三秒内。」\n关键NPC立场：沿用本阶已具名角色，禁止群像代称。\n主线钩子/支线：推进本阶主线节点；支线只服务本世界地理。\n危险度/规避：随阶递增；顶点只情报优先。\n任务方向/奖励：本阶能拿到的物资/情报/人情，不越级。\n`;
    i++;
  }
  return e;
}

