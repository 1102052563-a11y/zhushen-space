const nw = (s) => (s || '').replace(/\s/g, '').length;

function split(t) {
  const m1 = t.match(/^([\s\S]*?\n)## 剧情\n([\s\S]*?)\n## 休闲切入点\n([\s\S]*?)\n## 来源\n([\s\S]*)$/);
  if (!m1) throw new Error('parse fail');
  return { head: m1[1], plot: m1[2], entry: m1[3], src: m1[4] };
}
function join(p) {
  return p.head + '\n## 剧情\n' + p.plot + '\n## 休闲切入点\n' + p.entry + '\n## 来源\n' + p.src;
}
function ensure(file, plotAdd, entryAdd) {
  const fp = path.join(base, file);
  let t = fs.readFileSync(fp, 'utf8').replace(/\r\n/g, '\n');
  let p = split(t);
  if (nw(p.plot) < 8000 && plotAdd) p.plot = p.plot.trimEnd() + '\n\n' + plotAdd.trim();
  if (nw(p.entry) < 1850 && entryAdd) p.entry = p.entry.trimEnd() + '\n\n' + entryAdd.trim();
  // top-up if still short
  let guard = 0;
  while (nw(p.plot) < 7500 && guard < 3) {
    p.plot += '\n\n' + plotAdd.trim();
    guard++;
  }
  guard = 0;
  while (nw(p.entry) < 1800 && guard < 3) {
    p.entry += '\n\n' + entryAdd.trim();
    guard++;
  }
  fs.writeFileSync(fp, join(p), 'utf8');
  t = fs.readFileSync(fp, 'utf8');
  p = split(t);
  console.log(file, 'plot', nw(p.plot), 'entry', nw(p.entry));
}

