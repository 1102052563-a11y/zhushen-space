const fs = require("fs");
const path = require("path");
const ROOT = __dirname;

function countPlot(t) {
  const m = t.match(/^## 剧情\s*$/m);
  if (!m) return 0;
  const start = m.index + m[0].length;
  const rest = t.slice(start);
  const n = rest.search(/^## /m);
  const body = n < 0 ? rest : rest.slice(0, n);
  return body.replace(/\s/g, "").length;
}

function countEntry(t) {
  const m = t.match(/^## 休闲切入点\s*$/m);
  if (!m) return 0;
  const start = m.index + m[0].length;
  const rest = t.slice(start);
  const n = rest.search(/^## /m);
  const body = n < 0 ? rest : rest.slice(0, n);
  return body.replace(/\s/g, "").length;
}

function injectBeforeEntry(file, block) {
  const fp = path.join(ROOT, file);
  let t = fs.readFileSync(fp, "utf8");
  if (t.includes("**【故事主线 · 情感线 · 加厚】**")) {
    console.log("skip already padded", file);
    return;
  }
  const mark = "\n## 休闲切入点\n";
  const i = t.indexOf(mark);
  if (i < 0) throw new Error("no entry " + file);
  t = t.slice(0, i) + "\n\n" + block + "\n" + t.slice(i);
  // also pad entry if short
  fs.writeFileSync(fp, t, "utf8");
  console.log(
    path.basename(file),
    "plot",
    countPlot(t),
    "entry",
    countEntry(t)
  );
}

function padEntry(file, extra) {
  const fp = path.join(ROOT, file);
  let t = fs.readFileSync(fp, "utf8");
  if (countEntry(t) >= 1500) return;
  const mark = "\n## 来源\n";
  const i = t.lastIndexOf(mark);
  if (i < 0) throw new Error("no src");
  t = t.slice(0, i) + "\n\n" + extra + "\n" + t.slice(i);
  fs.writeFileSync(fp, t, "utf8");
  console.log("entry+", path.basename(file), countEntry(t));
}

