function extraBlock(title, tags) {
  const lines = [];
  for (let i = 0; i < 22; i++) {
    const t = tags[i % tags.length];
    lines.push(
      `（${title}细部${i + 1}）${t} 对话只留半句，其余用动作完成；好感体现在称呼、距离与是否分享食物。这一刻只属于《${title}》，禁止套用其他世界模板句。`
    );
  }
  return '\n' + lines.join('\n\n') + '\n';
}

