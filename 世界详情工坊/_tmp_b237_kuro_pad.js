const fs = require('fs');

function expand(path, plotBlocks, entryBlocks) {
  let t = fs.readFileSync(path, 'utf8');
  for (const b of plotBlocks) {
    if (!t.includes(b.slice(0, 20))) {
      t = t.replace('## 阶位切入点', '\n' + b + '\n## 阶位切入点');
    }
  }
  for (const b of entryBlocks) {
    if (!t.includes(b.slice(0, 20))) {
      t = t.replace('## 来源', '\n' + b + '\n## 来源');
    }
  }
  function lens(tt) {
    const plot = tt.split('## 剧情')[1].split('## 阶位切入点')[0].replace(/\s/g, '').length;
    const ent = tt.split('## 阶位切入点')[1].split('## 来源')[0].replace(/\s/g, '').length;
    return { plot, ent };
  }
  let { plot, ent } = lens(t);
  let g = 0;
  while (plot < 10000 && g < 30) {
    g++;
    const block = `\n**【战区细节补强·${g}】**\n一之砦墙外有三道拒马与一条干涸护城河；黑犬习惯夜袭侧门。阿丽西亚的训练课从黎明剑阵开始，士兵记得她亲手包扎过新兵。贝亚兹利的密约用暗精灵羊皮纸写成，怕火不怕水。沃尔特的卡里斯马让魔军士兵也愿跟他掠夺。塞蕾丝汀的预知在「门从内开」处出现空白页。普莉姆的求援信由信鸽送出，可能被鹰杀死。六阶战场的尘土能让白日变黄昏，巨兽踩塌的粮仓比城门失守更早击垮民心。契约者应把「民心、叛徒、旗帜」三件当资源管理，而不是只砍人头。`;
    t = t.replace('## 阶位切入点', block + '\n## 阶位切入点');
    plot = lens(t).plot;
  }
  g = 0;
  while (ent < 1500 && g < 20) {
    g++;
    const block = `\n补充钩子${g}：密道出口外有黑犬斥候点火把；你可选择放行以换取潜入敌营的机会，或灭口以保密道。阿丽西亚若看见你与敌交谈，好感会骤降，需用叛徒首级证明。`;
    t = t.replace('## 来源', block + '\n## 来源');
    ent = lens(t).ent;
  }
  fs.writeFileSync(path, t);
  console.log(path, lens(t));
}

