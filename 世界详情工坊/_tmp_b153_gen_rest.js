function expandPlot(base, extraBlocks) {
  let plot = base;
  for (const b of extraBlocks) plot += '\n' + b;
  // ensure length
  const strip = s => s.replace(/\s+/g, '');
  let i = 0;
  while (strip(plot).length < 10200) {
    plot += `\n**【档案加详·${i + 1}】**` + extraBlocks[i % extraBlocks.length].slice(0, 400);
    i++;
    if (i > 30) break;
  }
  return plot;
}

