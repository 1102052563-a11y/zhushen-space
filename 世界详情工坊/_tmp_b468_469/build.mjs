function assertClean(name, text) {
  for (const bad of FORBIDDEN) {
    if (text.includes(bad)) throw new Error(`[${name}] 含禁句: ${bad}`);
  }
}

function count(s) {
  return (s || '').replace(/\s/g, '').length;
}

