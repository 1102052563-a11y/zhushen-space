function microDaily(title, items) {
  return [
    `**【${title}·微观日常备忘】**`,
    ...items.map((x, i) => `${i + 1}. ${x}`),
    '以上事件每一件都可转化为：谁先蹲下收拾、谁替谁圆场、谁在事后单独道谢——但台词与道具必须用本舞台专属物，禁止套用其他条目的黑话。',
  ].join('\n');
}

function entryPad(title, lines) {
  return [
    `补充细节（仅服务「${title}」）：`,
    ...lines.map((x) => `- ${x}`),
  ].join('\n');
}

