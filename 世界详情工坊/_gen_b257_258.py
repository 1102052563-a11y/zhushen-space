def ensure_plot(plot, name, min_c=10050):
    p = plot
    bits = details(name)
    i = 0
    while wc(p) < min_c:
        p += f"\n\n**【场景颗粒·{i+1}】**{bits[i % len(bits)]}"
        if i >= len(bits):
            p += f"（延伸{i}：将上条化为可被NPC当场检验的动作与对白。）"
        i += 1
        if i > 100:
            break
    return p

def ensure_entry(e, min_c=1550):
    x = e
    i = 0
    while wc(x) < min_c:
        i += 1
        x += f"\n执行补强{i}：本阶须落地具名NPC当场立场句、可夺物证、失败二十四小时内反噬势力；奖励不越阶。"
        if i > 25:
            break
    return x

