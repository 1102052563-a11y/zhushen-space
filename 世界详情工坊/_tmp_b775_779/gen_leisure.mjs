function pad(s, n) {
  // expand unique detail blocks if under n (chars without whitespace)
  let t = s;
  let c = t.replace(/\s/g, '').length;
  if (c >= n) return t;
  const extra = [];
  const seeds = [
    '日常节律：晨间闹钟与便当盒的开合声决定当天节奏；午休是共享便当或社团室闲聊的黄金时段；放学后的河堤／商店街／神社参道是告白与误会高发地；夜里的电话或短信决定好感是否冻结。',
    '物件锚点：学生证、社团钥匙、雨伞、便当盒、手帕、祭典面具、旧照片、手写便条——每一次递出会被解读为关系进度。',
    '声音锚点：铃声、蝉鸣、雨打屋檐、图书馆翻页、校广播、远处祭典太鼓。正文宜反复用这些可观察细节，而非空喊主题。',
    '关系计量：浅粉＝第一次被正确叫名；樱＝第一次被护短；深樱＝第一次单独约会；朱红＝公开并肩或告白。连续缺席同一人日程＝好感冻结而非仇恨。',
    '失败态：冷战、转学传闻、社团解散危机、家庭反对、错过毕业前的一句话。失败不是闯关失败，而是第二天如何面对同一张课桌。',
    '季节事件：春＝转校／樱花；梅雨＝共伞；夏＝祭典与烟火；秋＝文化祭；冬＝期末与告白窗口；毕业季＝True／Bittersweet 分岔。',
  ];
  let i = 0;
  while (c < n && i < 40) {
    const block = seeds[i % seeds.length] + `（本世界专属补述${i + 1}：请把上述节律落到「${path.basename}」舞台的具体地点与真名角色互动上。）`;
    extra.push(block);
    t = s + '\n\n' + extra.join('\n');
    c = t.replace(/\s/g, '').length;
    i++;
  }
  return t;
}

function charBlock(c) {
  return `- **${c.name}（${c.role}）**｜外貌：${c.look}｜性格：${c.personality}｜角色类型：${c.type}｜萌点/魅力：${c.moe}｜个人线剧情：${c.route}｜与主角关系：${c.rel}`;
}

function build(doc) {
  const chars = doc.chars.map(charBlock).join('\n');
  const events = doc.events.map((e, i) => `${i + 1}. **${e.title}**——${e.desc}`).join('\n');
  const hookList = doc.hooks || [
    '社团／共同目标线：用出勤与分工换对话',
    '季节祭典线：共伞、烟火、文化祭摊位',
    '家庭与秘密线：一次摊牌与可退出的倾听',
    '后日谈预备线：毕业前的称呼练习',
  ];
  const hooks = hookList.map((h, i) => `${i + 1}. **${h}**`).join('\n');
  const routes = doc.routes.map((r) => `**${r.name}线**：${r.body}`).join('\n');

  let plot = `**【作品来源】**
${doc.source}

**【世界定位】**
${doc.position}

**【世界观 · 舞台设定】**
${doc.world}

**【地理 · 生活舞台】**
${doc.geo}

**【故事主线 · 情感线】**
${doc.story}

${routes}

**共通后日谈倾向**
${doc.after}

**【可攻略角色 / 主要人物】**
${chars}

**【人际关系网 / 社团势力】**
${doc.relations}

**【情感事件 · 名场面】**
${events}

**【隐藏剧情 · 真结局 · 伏笔】**
${doc.hidden}

**【氛围基调 · 雷区】**
${doc.tone}
`;

  // ensure plot length
  const needPlot = 6200;
  if (plot.replace(/\s/g, '').length < needPlot) {
    plot += `\n\n**【日常节律 · 物件 · 声音 · 关系计量】**\n${doc.daily || ''}\n`;
    // unique micro scenes
    const micros = (doc.micro || []).join('\n');
    if (micros) plot += `\n**【微观日常事件池】**\n${micros}\n`;
  }
  while (plot.replace(/\s/g, '').length < needPlot) {
    plot += `\n\n**【舞台气味补记】**\n本世界专属温度来自：${doc.smell || '季节风、粉笔灰、便当热气'}。契约者写场景时应先定「今天是星期几、社团第几次活动、信箱有没有便签」，再决定人物语气。忌用跨世界套话。\n`;
  }

  let entry = `> 本世界为休闲/恋爱向，无生存压力与数值对决。契约者以**日常身份**融入，核心玩法＝relationship 攻略 + 日常事件，而非任务厮杀。

切入身份：${doc.entry.id}

切入时点：${doc.entry.time}

初始处境：${doc.entry.sit}

开场白建议：「${doc.entry.opener}」

可攻略对象：
${doc.entry.targets.map((t) => `- **${t.name}**：${t.how}`).join('\n')}

日常玩法钩子：
${hooks}

氛围/雷区：${doc.entry.vibe}
`;

  while (entry.replace(/\s/g, '').length < 1550) {
    entry += `\n\n补充节奏：第1–2日只做落地与建信；第3–5日引入社团/家庭钩子；个人线心结在第2周后摊牌。失败时优先冷战与错过，而不是「打不过」。\n`;
  }

  const sources = doc.sources.map((s) => `- [${s.label}](${s.url})`).join('\n');

  return `# ${doc.name}
<!--meta lib=休闲 tiers=休闲-->

## 剧情

${plot.trim()}

## 休闲切入点

${entry.trim()}

## 来源

${sources}
`;
}

