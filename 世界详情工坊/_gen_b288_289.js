// 扩写块：确保字数（每世界独立内容）
function expanders(title) {
  const blocks = [];
  const days = ['朝', '午', '暮', '夜', '雨', '祭', '试', '假', '雪', '风'];
  for (let i = 0; i < 10; i++) {
    blocks.push(
`**《${title}》生活切片${i + 1}·${days[i]}**  
在这个世界里，关系推进靠可观察细节：谁先开口、谁让出路、谁记得忌口。切片${i + 1}发生在日常动线中——教室或街道或宿舍门口——对话只留半句，其余用动作完成。好感体现在称呼变化、共享食物、是否允许进入私人房间。这一刻只属于《${title}》，禁止套用其他世界模板句，禁止写成战斗任务。`
    );
  }
  for (let i = 0; i < 8; i++) {
    blocks.push(
`**《${title}》情感因果${i + 1}**  
若契约者选择诚实，NPC 会在下一场景用「多等三秒」回报；若选择利用秘密，则微笑变薄、任务仍完成但私话关闭。因果${i + 1}不涉及等级或战力，只改写信任与结局朝向（HE 的共同生活 / BE 的误解固定）。`
    );
  }
  return blocks;
}

function build(w) {
  let plot = w.plot;
  // strip leading title lines already in plot - plot includes full header
  const entryPack = entries[w.title];
  if (!entryPack) throw new Error('no entry for ' + w.title);
  let entry = makeEntry(w.title, entryPack);

  // ensure lengths
  const plotBodyMatch = plot.match(/## 剧情\n([\s\S]*)$/);
  let plotBody = plotBodyMatch ? plotBodyMatch[1] : plot;
  const head = plot.split(/## 剧情\n/)[0] + '## 剧情\n';

  plotBody = padTo(plotBody.trim(), 6200, expanders(w.title));
  entry = padTo(entry.trim(), 1600, [
    `**关系进度补充（${w.title}）**\n低：试探。中：并排。高：托付后背式的信任（非战斗意义）。`,
    `**禁忌提醒（${w.title}）**\n忌OOC；忌把休闲写成猎杀；忌抹去原作心结。`,
  ]);

  const src = sources(w.links);
  const full = `${head}${plotBody}\n\n## 休闲切入点\n\n${entry}\n\n## 来源\n\n${src}\n`;
  return full;
}

