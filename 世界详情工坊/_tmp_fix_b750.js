function tagOf(f) {
  if (f.startsWith('搾精')) return 'sakusei';
  if (f.startsWith('第3')) return 'oyasumi';
  if (f.startsWith('家属')) return 'kazoku';
  if (f.includes('といれ') || f.includes('花子')) return 'hanako';
  return 'hyakki';
}

function countSection(c, name) {
  const re = new RegExp('## ' + name + '[\\s\\S]*?(?=\\n## |$)');
  const m = c.match(re);
  return m ? m[0].replace(/\s/g, '').length : 0;
}

