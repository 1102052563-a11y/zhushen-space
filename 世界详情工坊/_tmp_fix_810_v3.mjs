const nw = (s) => (s || '').replace(/\s/g, '').length;

function parse(t) {
  t = t.replace(/\r\n/g, '\n');
  const m = t.match(
    /^(#[^\n]*\n(?:<!--[^\n]*-->\n)*)\n*## 剧情\n+([\s\S]*?)\n+## 休闲切入点\n+([\s\S]*?)\n+## 来源\n+([\s\S]*)$/,
  );
  if (!m) throw new Error('parse fail');
  return { head: m[1].trimEnd(), plot: m[2].trim(), entry: m[3].trim(), src: m[4].trim() };
}
function dump(p) {
  return (
    p.head +
    '\n\n## 剧情\n\n' +
    p.plot +
    '\n\n## 休闲切入点\n\n' +
    p.entry +
    '\n\n## 来源\n' +
    p.src +
    '\n'
  );
}

