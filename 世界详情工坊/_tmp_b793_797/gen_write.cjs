const ns = (s) => (s || '').replace(/\s/g, '').length;

function pad(base, extras, min, tag) {
  let t = base;
  const bag = extras && extras.length ? extras.slice() : [];
  let i = 0;
  while (ns(t) < min && i < bag.length) {
    t += '\n\n' + bag[i];
    i++;
  }
  let j = 0;
  while (ns(t) < min && j < 120) {
    const e = bag[j % Math.max(bag.length, 1)] || '本世界推进看称呼、独处借口、第三人视线与物证。';
    t +=
      '\n\n**【' +
      tag +
      '·再叙·' +
      (j + 1) +
      '】**\n' +
      String(e).replace(/^\*\*【[^】]+】\*\*\n?/, '') +
      '\n补述：场景气味、未完成动作、可退出边界、翌日称呼、物证之一。失败=冷战/回避，不是死亡。';
    j++;
  }
  while (ns(t) < min) {
    t +=
      '\n\n**【' +
      tag +
      '·密度】**\n日常锚：门铃、鞋尖、窗外蝉、未读消息、共用杯子、雨檐、终电、冰箱灯、门链。好感=主动联系频率；压力=第三人视线。';
  }
  return t;
}

function make(name, plot, entry, sources, plotX, entryX, tag) {
  return (
    '# ' +
    name +
    '\n<!--meta lib=休闲 tiers=休闲-->\n\n## 剧情\n\n' +
    pad(plot, plotX, 6200, tag || name.slice(0, 8)) +
    '\n\n## 休闲切入点\n\n' +
    pad(entry, entryX, 1550, (tag || name.slice(0, 6)) + '切') +
    '\n\n## 来源\n\n' +
    sources +
    '\n'
  );
}

