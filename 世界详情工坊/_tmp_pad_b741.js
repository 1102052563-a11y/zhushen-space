const fs = require('fs');
const path = require('path');
const outDir = String.raw`C:\Users\Administrator\Desktop\前端卡\files\世界详情工坊\产出\批次741`;
const noSpace = s => (s||'').replace(/\s/g,'').length;
function stats(md){
  const j=(md.match(/## 剧情\s*([\s\S]*?)(?=## 休闲切入点)/)||[])[1]||'';
  const q=(md.match(/## 休闲切入点\s*([\s\S]*?)(?=## 来源)/)||[])[1]||'';
  return {j:noSpace(j), q:noSpace(q), jRaw:j, qRaw:q};
}
function padFile(file, uniqueBags){
  let md = fs.readFileSync(path.join(outDir,file),'utf8');
  let c = stats(md);
  let i=0;
  while(c.j < 6000 && i < uniqueBags.story.length){
    const chunk = '\n\n' + uniqueBags.story[i++] + '\n';
    md = md.replace('## 休闲切入点', chunk + '\n## 休闲切入点');
    c = stats(md);
  }
  // if still short, synthesize more unique sentences from seeds
  let k=0;
  while(c.j < 6000 && k<50){
    const seed = uniqueBags.storySeeds[k % uniqueBags.storySeeds.length];
    const chunk = `\n\n**【细部档案·${k+1}】**\n${seed.replace(/\{N\}/g, String(k+1))}\n`;
    md = md.replace('## 休闲切入点', chunk + '\n## 休闲切入点');
    c = stats(md); k++;
  }
  i=0;
  while(c.q < 1500 && i < uniqueBags.entry.length){
    const chunk = '\n\n' + uniqueBags.entry[i++] + '\n';
    md = md.replace('## 来源', chunk + '\n## 来源');
    c = stats(md);
  }
  k=0;
  while(c.q < 1500 && k<30){
    const seed = uniqueBags.entrySeeds[k % uniqueBags.entrySeeds.length];
    const chunk = `\n\n**【切入细目·${k+1}】**\n${seed.replace(/\{N\}/g, String(k+1))}\n`;
    md = md.replace('## 来源', chunk + '\n## 来源');
    c = stats(md); k++;
  }
  fs.writeFileSync(path.join(outDir,file), md, 'utf8');
  c = stats(md);
  console.log(file, c.j, c.q, c.j>=6000&&c.q>=1500?'OK':'SHORT');
}

