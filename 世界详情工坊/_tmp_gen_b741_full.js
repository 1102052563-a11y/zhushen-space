const noSpace = s => (s||'').replace(/\s/g,'').length;
function stats(md){
  const j=(md.match(/## 剧情\s*([\s\S]*?)(?=## 休闲切入点)/)||[])[1]||'';
  const q=(md.match(/## 休闲切入点\s*([\s\S]*?)(?=## 来源)/)||[])[1]||'';
  return {j:noSpace(j), q:noSpace(q)};
}
function ensureLen(base, needJ, needQ, pack){
  let md = base;
  let c = stats(md);
  // inject unique expansions before 休闲切入点 and before 来源
  const storyExtra = pack.storyExtra;
  const entryExtra = pack.entryExtra;
  if(c.j < needJ){
    md = md.replace('## 休闲切入点', `\n\n${storyExtra}\n\n## 休闲切入点`);
  }
  if(stats(md).q < needQ){
    md = md.replace('## 来源', `\n\n${entryExtra}\n\n## 来源`);
  }
  // if still short, repeat unique day-by-day with different facts
  let guard=0;
  while(stats(md).j < needJ && guard++<20){
    md = md.replace('## 休闲切入点', `\n\n${pack.moreStory(guard)}\n\n## 休闲切入点`);
  }
  guard=0;
  while(stats(md).q < needQ && guard++<20){
    md = md.replace('## 来源', `\n\n${pack.moreEntry(guard)}\n\n## 来源`);
  }
  return md;
}
