const noWs = (s) => s.replace(/\s/g, '').length;

function wrap(name, tiers, plot, entry, sources) {
  return `# ${name}\n<!--meta lib=主库 tiers=${tiers}-->\n\n## 剧情\n\n${plot.trim()}\n\n## 阶位切入点\n\n${entry.trim()}\n\n## 来源\n\n${sources.trim()}\n`;
}

function padTo(plot, min, pads) {
  let p = plot;
  let i = 0;
  while (noWs(p) < min && i < pads.length * 3) {
    const block = pads[i % pads.length];
    const n = Math.floor(i / pads.length) + 1;
    p += `\n\n**【阶段档案·${block.title}·补${n}】**\n${block.body}`;
    i++;
  }
  return p;
}

