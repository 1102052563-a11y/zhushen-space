function padWorld(s, n, tag) {
  let t = s;
  let i = 0;
  const tips = [
    `写正文时优先可观察细节：称呼变化、座位距离、递物时的手指、回避视线的角度、季节气味与房间陈设。`,
    `第N层关系推进应比上一层多一句「说不出口的话」。`,
    `日常钩子优先：一起吃饭、班表交换、雨天共伞、夜路沉默、任务后的便利店。`,
    `情感落地靠「名字被正确叫出」与「秘密被接住」，而非口号。`,
  ];
  while (t.replace(/\s/g, '').length < n && i < 50) {
    i++;
    t += `\n【${tag}·观察备忘${i}】${tips[i % tips.length]}本世界独有：把「${tag}」三个字换成可听可触的场面再写。`;
  }
  return t;
}

function build(w) {
  const plotParts = [
    `**【作品来源】**\n${w.source}`,
    `**【世界定位】**\n${w.position}`,
    `**【世界观 · 舞台设定】**\n${w.world}`,
    `**【地理 · 生活舞台】**\n${w.geo}`,
    `**【故事主线 · 情感线】**\n${w.story}`,
    `**【可攻略角色 / 主要人物】**\n${w.chars}`,
    `**【人际关系网 / 社团势力】**\n${w.relations}`,
    `**【情感事件 · 名场面】**\n${w.scenes}`,
    `**【隐藏剧情 · 真结局 · 伏笔】**\n${w.hidden}`,
    `**【氛围基调 · 雷区】**\n${w.tone}`,
    `**【日常推进备忘 · ${w.short}】**\n${w.memo}`,
  ];
  let plot = plotParts.join('\n\n');
  plot = padWorld(plot, 6200, w.short);

  let entry = `> 本世界为休闲／恋爱向档案（成人向原作取其人物与情感舞台侧），无生存压力与战斗力体系。契约者以**日常身份**融入，核心玩法＝relationship 攻略 + 日常事件，而非任务厮杀。

切入身份：${w.entryId}

切入时点：${w.entryTime}

初始处境：${w.entrySit}

开场白建议：「${w.entryOpen}」

可攻略对象：
${w.entryTargets}

日常玩法钩子：
${w.entryHooks}

氛围／雷区：${w.entryTone}
`;
  entry = padWorld(entry, 1600, w.short + '切入');

  const sources = w.sources.map((s) => `- [${s[0]}](${s[1]})`).join('\n');
  return `# ${w.name}
<!--meta lib=休闲 tiers=休闲-->

## 剧情

${plot}

## 休闲切入点

${entry}

## 来源

${sources}
`;
}

