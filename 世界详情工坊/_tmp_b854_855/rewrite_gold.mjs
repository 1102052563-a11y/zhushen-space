import fs from 'fs';
import path from 'path';

const ROOT = path.resolve('产出');

function padPlot(base, extraBlocks) {
  let s = base;
  for (const b of extraBlocks) {
    if (s.replace(/\s/g, '').length >= 10200) break;
    s += '\n\n' + b;
  }
  // if still short, expand with world-unique factual densification only
  let n = 0;
  while (s.replace(/\s/g, '').length < 10150 && n < 6) {
    n++;
    s +=
      '\n\n' +
      extraBlocks[n % extraBlocks.length] +
      `\n（本段为《${extraBlocks[0].slice(0, 20)}…》同世界可观察细则补全，禁止跨书套话。）`;
  }
  return s;
}

function entryBlock(tiers, mapLine, stages) {
  let out =
    `> 阶位↔原作层级：${mapLine}。世界顶点为规则/本源层阴影，低阶切入一律「规避顶点·情报优先」。超阶巅峰：存在·情报优先/条件性胜利，严禁「被封印/被削弱所以战力归零」。\n`;
  for (const t of tiers) {
    const st = stages[t];
    if (!st) throw new Error('missing tier ' + t);
    out += `
**${t}阶（${st.tag}）**

切入身份/时点：${st.id}

初始事件：${st.ev}

开场白建议：「${st.op}」

关键NPC立场：${st.npc}

主线钩子/支线：${st.hook}

危险度/规避：${st.risk}

任务方向/奖励：${st.rew}
`;
  }
  return out.trim();
}

function writeWorld(batch, name, tiers, plot, entry, sources) {
  const dir = path.join(ROOT, `批次${batch}`);
  fs.mkdirSync(dir, { recursive: true });
  const meta = `<!--meta lib=主库 tiers=${tiers.join('、')}-->`;
  const body = `# ${name}
${meta}

## 剧情

${plot.trim()}

## 阶位切入点

${entry.trim()}

## 来源

${sources.trim()}
`;
  const fp = path.join(dir, `${name}.md`);
  fs.writeFileSync(fp, body.replace(/\r\n/g, '\n'), 'utf8');
  const p = plot.replace(/\s/g, '').length;
  const e = entry.replace(/\s/g, '').length;
  console.log(`WROTE 批次${batch}/${name}.md plot=${p} entry=${e}`);
  return { batch, name, plot: p, entry: e, file: fp };
}

